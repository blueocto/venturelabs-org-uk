			<h1>Apply for a trainee licence to give driving instruction</h1>
			<p>Apply to the Driving Standards Agency for a trainee licence to give driving instruction once you have passed the first two approved driving instructor (ADI) qualification tests.</p>
			
			<h3>Before you start</h3>
			<p>You can use this service to apply for a trainee licence to give driving instruction.</p>
			<p>You can use it between 6.00 am and midnight, Monday to Sunday.</p>
			
			<h3>What you will need</h3>
			<p>This service is available to registered users only.</p>
			<p>You will need:</p>
			<ul>
				<li>your online services user ID</li>
				<li>your password</li>
				<li>a valid credit or debit card to pay the fee</li>
				<li>your personal reference number (PRN)</li>
				<li>a printer</li>
			</ul>
			
			<h3>Payment</h3>
			<p>Payment will be taken from your account as soon as your application has been received. If your application is refused, the Driving Standards Agency (DSA) will give you a full refund.</p>
			
			<h3>Sending your application</h3>
			<p>When you have completed the screens, you will need to print a copy of the final page. You can do this using your browser print option.</p>
			<p>You should send the documents listed to the DSA address below:</p>
			<ul>
				<li>the printed final page</li>
				<li>a passport sized photograph (with your PRN written on the back)</li>
				<li>your 21T form</li>
				<li>ADI 3L (MD) form (if applying for a first trainee licence)</li>
			</ul>
			
			<p>Driving Standards Agency
