		<p>When you decide to merge businesses, through takeover, acquisition or a joint venture, you may be reducing competition in the market(s) in which you operate. If it is found that your merger will substantially lessen competition it may be prohibited or you may have conditions imposed upon you.</p>
		<p>The Office of Fair Trading (OFT) is responsible for investigating mergers in the first instance. If it believes a merger might result in substantially reduced competition, the OFT can refer it to the Competition Commission (CC) for an in-depth inquiry. The Secretary of State for Business, Innovation &amp; Skills (BIS) may also consider and refer to the CC any mergers which raise wider public interest concerns - mainly in the defence, media or certain financial markets. Mergers are only investigated if they meet certain criteria.</p>
		<p>This merger control process allows the OFT to identify mergers where such competition issues may arise (or the Secretary of State for BIS in the case of wider public interest issues), so that they can be examined in greater detail by the CC.</p>
