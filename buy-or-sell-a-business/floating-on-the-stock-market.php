		<p>Floating your business on a stock market involves selling a percentage of your business in the form of shares, which are subsequently traded. There is a choice of stock markets in the UK, but the two largest are the London Stock Exchange and PLUS Markets Group.</p>
		<p>Joining a stock market turns your business into a 'public company'. This presents a range of benefits, including access to capital, and the opportunity to increase your businesses profile and motivate staff through the introduction of share option schemes. There are also additional responsibilities and costs which come with being a publicly quoted company and these require careful consideration. You should note that floating on a stock market is not always the best option for obtaining growth capital and you should make sure to consider all your options before making a final decision.</p>
		<p>This guide explains some of the benefits and matters for consideration, when thinking of floating your business on a stock exchange.</p>
