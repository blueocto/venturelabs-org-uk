		<h1>Complain against an undischarged bankrupt or disqualified company director</h1>
		<h2>What undischarged bankrupts and persons subject to bankruptcy restrictions are not allowed to do</h2>
		<p>Disqualified directors are not allowed to be involved in the promotion, management or formation of a limited company without court permission. The ban applies to all registered and unregistered companies formed in England, Wales, Northern Ireland and Scotland. The ban also applies to foreign companies that are registered in the UK.</p>
		<p>Undischarged bankrupts and persons subject to bankruptcy restrictions as a result of a bankruptcy restrictions order (BRO) or bankruptcy restrictions undertaking (BRU) are not allowed to:</p>
		<ul>
			<li>obtain credit of &pound;500 or more without telling the person or institution from whom they are trying to obtain such credit that they are an undischarged bankrupt or is subject to bankruptcy restrictions</li>
			<li>engage - whether directly or indirectly - in any business under a name other than that in which they were made bankrupt without disclosing that name to all persons with whom they enter into any business transaction</li>
			<li>act as director of a company - or directly or indirectly take part in or be concerned in the promotion, formation or management of a company - without court permission</li>
		</ul>
