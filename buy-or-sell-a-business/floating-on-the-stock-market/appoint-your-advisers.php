		<h1>Floating on the stock market</h1>
		<h2>Appoint your advisers</h2>
		<p>Getting the right advisers is key to a successful flotation. They are the experts who will guide you through the demanding legal, regulatory, financial and marketing processes.</p>
		<p>A corporate finance adviser will be the central adviser to your company during the admission process and throughout your company's life on a public market. A range of other advisers will also play integral roles in supporting you through the admission process and thereafter. These usually include a broker, a reporting accountant, and legal, public relations and investor relations companies.</p>
		<p>Bad advice, or using an adviser with a poor reputation, could seriously affect your business' reputation, ability to attract investors and float successfully. Some prefer to choose companies who have worked together previously as teams.</p>
		<p>Advisers' professional fees, which can run into six-figure sums depending on the size of the flotation, will be the main cost in floating your business.</p>
		<h3>Adviser roles</h3>
		<p>The corporate adviser will be responsible for guiding your company through the application and admission process. They work closely with the company and its lawyers to compile the necessary admission documents and make the formal application to the stock exchange.</p>
		<p>Most companies seeking a flotation use a <strong>corporate adviser</strong> to guide them through the process. The corporate adviser makes the application to join the stock market on behalf of the company. They are responsible for ensuring all the information in the application is complete and dealing with any requests for additional information.</p>
		<p>Companies floating on AIM must appoint a nominated adviser (nomad) who is approved by the London Stock Exchange to act in such a capacity. <a href="http://www.londonstockexchange.com/companies-and-advisors/aim/advisers/advisers.htm" target="_blank" rel="noopener noreferrer">Find information on advisers for AIM on the London Stock Exchange website</a>.</p>
		<p>If you're floating on PLUS, your adviser will need to be a member of PLUS.</p>
		<p>You'll also need a <strong>stockbroker</strong> to generate interest in your business in the investment community.</p>
		<p>Your <strong>corporate lawyer</strong> will be responsible for the due diligence process and for verifying statements in the prospectus and other documents.</p>
		<p>An <strong>accountant</strong> will be needed to conduct the financial due diligence and prepare any financial statements needed for the admission documents. You will also need to appoint the services of a corporate broker to seek investor interest in your business - the key reason for floating on a stock market.</p>
		<p>For large-scale floats it's also worth considering the services of a financial public relations company to reach wider audiences of investors.</p>

