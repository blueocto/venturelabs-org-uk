		<h1>Preparing to sell your business</h1>
		<h2>Ways to sell your business</h2>
		<p>There are various ways you could sell your business, with the options available depending on factors like your business' type, size and sector. Most businesses are sold in a trade sale to another business - usually to one operating in the same or a related field.</p>
		<p>Other options available to you could include:</p>
		<ul>
			<li>finding a private-equity buyer</li>
			<li>a management/employee buyout - perhaps with the help of a venture capital firm or bank loan</li>
			<li>attracting a private investor</li>
		</ul>
		<p>There are several different sale options - the one best for you will depend on your individual circumstances and the legal status of your business. The buyer will also have an opinion on deal structure and how they wish to make an acquisition, so you'll need to know what you want to achieve and how you would like to structure a sale early on. This will save time and money, and avoid unnecessary delays.</p>
		<h3>Partial or full sale</h3>
		<p>You may want to sell the entire business or keep a small stake in it. The buyer may prefer you to retain partial ownership and continue your involvement. This can give the business continuity and the buyer confidence that the business will do well.</p>
		<h3>Sale of assets</h3>
		<p>Instead of selling the business itself, you could sell assets like equipment, intellectual property or your customer list. This may be attractive to a buyer who doesn't want to take on liabilities and obligations.</p>
		<p>For example, the buyer might not want to take on your employees. You will be left with whatever assets and liabilities are not included in the sale. In this case, tax and legal advice is an essential factor in deciding the most suitable deal structure.</p>
		<h3>Immediate or phased payment</h3>
		<p>You can ask for payment in full when the sale is completed, or you may be prepared to accept payment in instalments. The buyer may well prefer to pay in instalments. But you will be at risk, for example if the buyer cannot make future payments.</p>
		<p>Some buyers will want to make a series of payments based on profits, in which case you may be contracted to stay with the business for a period of time. This is often known as an 'earn out'.</p>
		<p>Your choices can affect whether buyers are interested and how much they are prepared to offer. They can also affect the tax treatment of the sale. <a href="../../../tax-payroll-and-company-information/selling-or-closing-your-business/check-tax-and-legal-issues-when-selling-a-business.php">Use our interactive tool to investigate the tax and legal issues when selling or closing your business</a>.</p>
		<p>You can get guidance from an adviser. For help in selecting one, see the page in this guide on <a href="choosing-advisers-when-selling-your-business.php">choosing advisers when selling your business</a>.</p>
