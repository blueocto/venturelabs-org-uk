		<h1>Preparing to sell your business</h1>
		<h2>Streamline your business operations</h2>
		<p>The more confidence a buyer has in your business, the more attractive your business will become and the higher the price they are likely to offer. It's essential to set out a clearly defined strategy in your <b>business plan</b>.</p>
		<p>You also need to show that you have got a <b>strong management team</b> in place. If your business is too dependent on your own skills, it might damage the price it can fetch - and could even make it impossible to sell. Appointing deputy or departmental managers can enhance a company's value by alleviating that risk. You may also want to encourage key employees to stay by considering appropriate incentive schemes.</p>
		<p>Aim to <strong>reduce your dependence</strong> on too few customers or on one or two key suppliers. Show how your <b>customer base</b> is expanding and formalise any informal deals you have with customers and suppliers.</p>
		<p>You should also:</p>
		<ul>
			<li> ensure you're complying with health and safety, employment and other legislation - consider asking your legal advisers to review the business</li>
			<li> settle any legal disputes</li>
			<li> make sure you have clear ownership of any intellectual property - see our guide on <a href="../../../create-innovate-and-protect/protecting-your-intellectual-property/find-value-you-didnt-know-existed-in-your-business/protecting-intellectual-property.php">protecting intellectual property</a></li>
			<li> ensure property contracts are sorted out</li>
			<li> put in place suitable management information systems</li>
			<li> ensure your finances are in good order - see the page in this guide on how to <a href="../preparing-to-sell-your-business.php">show strong financial performance</a></li>
		</ul>
		<p>The sooner you start planning, the more effectively you can do all this. There is a strong case for setting out your exit strategy in your original business plan. This will prevent sudden or misguided decisions about leaving the business which could leave you financially worse off or make the business less attractive to potential buyers.</p>
		<p>Throughout the sale process, continue to demonstrate that you will be flexible and co-operative. Show that you would also be willing to spend some time after the sale helping the buyer get acclimatised to the business. If you think it will help the sale, be prepared to work for the company for a fixed period after the sale is completed.</p>
