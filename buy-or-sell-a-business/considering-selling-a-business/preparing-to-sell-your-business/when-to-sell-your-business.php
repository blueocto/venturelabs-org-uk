		<h1>Preparing to sell your business</h1>
		<h2>When to sell your business</h2>
		<p>Selling at the right time can have a significant impact on the price you get for your business.</p>
		<p>If possible, <b>plan ahead</b> so that you can pick the best moment rather than being rushed into a quick sale. For example, if you plan to retire in five years' time, it's a good idea to start planning the sale of your business now.</p>
		<p>It's also wise to keep your plans confidential until the sale is imminent. This will prevent a negative reaction from customers and suppliers, and eliminate unnecessary worry for your employees.</p>
		<h3>Economic and financial considerations</h3>
		<p>The general state of the economy - and your sector in particular - can have an effect. It's easier for a trade buyer to fund a purchase when their own business is doing well, interest rates are low and banks are keen to lend.</p>
		<p>The state of your business is a more important factor. Aim to sell when <b>profits</b> are increasing and look likely to grow further. Consider the impact of sales cycles or seasonal fluctuations in your business - you might have fuller order books at a particular time of year. For more information, see the page in this guide on how to <a href="../preparing-to-sell-your-business.php">show strong financial performance</a>.</p>
		<h3>Grooming your business</h3>
		<p>Planning well in advance also allows you to groom other aspects of your operations to ensure your business is as attractive to buyers as possible. It can also highlight any issues which might have an impact on a sale. For example, you should ensure that:</p>
		<ul>
			<li>equipment is well-maintained</li>
			<li>key contracts are in order</li>
			<li>there is no outstanding litigation or unresolved disputes</li>
			<li>you are complying with all legislation</li>
		</ul>
		<p>For more information, see the page in this guide on how to <a href="streamline-your-business-operations.php">streamline your business operations</a>.</p>
