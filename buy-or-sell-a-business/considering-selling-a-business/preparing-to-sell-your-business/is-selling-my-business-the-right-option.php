		<h1>Preparing to sell your business</h1>
		<h2>Is selling my business the right option?</h2>
		<p>Before putting your business up for sale you must give careful consideration to your reasons for doing so. You will probably be asked about your reasons for selling by potential buyers, who will need to be comfortable with your motivation and answers.</p>
		<p>You need to consider four key questions:</p>
		<ul>
			<li>What are my objectives as the <b>owner</b> of the business? For example, you might want to realise some or all of your investment in the business to fund your retirement.</li>
			<li>What are my objectives as <b>manager</b> of the business? For example, you might want to retire as soon as possible or prefer to have an ongoing involvement with the business.</li>
			<li>What are my objectives for the <b>business</b> itself? For example, the business might need new investment in order to grow.</li>
			<li><strong>Who else</strong> will be affected and what will they want? For example, other shareholders, managers and employees, and even key customers and suppliers.</li>
		</ul>
		<p>Selling part or all of the business may be the best way to achieve your objectives. You might, for instance, want to sell your business outright, leaving you with no financial or management involvement. For more information, see the page in this guide on <a href="ways-to-sell-your-business.php">ways to sell your business</a>.</p>
		<p>But a sale may not always be the best solution. And, of course, it may not always be realistic either - for more information, see the page in this guide: <a href="is-a-sale-realistic.php">is a sale realistic?</a></p>
		<p>There's a range of other exit routes that may suit your needs better. If, for example, you want to retire but already have enough money, you could pass the business on to your children. You could sell to your employees. Or you could try a stock-market flotation - this could raise capital to develop your business, while making it easier to sell part of or your entire stake in the business.</p>
		<p>For more information on your different exit options, see our guide on how to <a href="../consider-your-exit-strategy-when-starting-up.php">consider your exit strategy when starting up</a>.</p>
