		<h1>Preparing to sell your business</h1>
		<h2>Is a sale realistic?</h2>
		<p>You can only sell your business if someone is prepared to pay for it. If you can't identify strong reasons - that can be easily substantiated - why your business would make a good acquisition, it's likely to be difficult to find a buyer.</p>
		<p>Ask yourself the following questions:</p>
		<ul>
			<li>Is the business <b>healthy</b>? A business in trouble is difficult to sell and potential buyers are likely to wait until they can get assets at a knockdown price - although sometimes financial distress can be a motivating factor to a buyer who can see a turnaround opportunity.</li>
			<li> Are the <b>basics</b> in place to make the business attractive? Buyers like well-organised businesses with strong management. For more information, see the page in this guide on how to <a href="streamline-your-business-operations.php">streamline your business operations</a>.</li>
			<li> Does the business have a <b>good financial record</b>? Buyers prefer a record of smoothly increasing profits with good growth potential. For more information, see the page in this guide on how to <a href="../preparing-to-sell-your-business.php">show strong financial performance</a>.</li>
			<li> Can you identify <b>potential trade buyers</b> and a good reason why they should want to buy your business? Buying a business can be disruptive and expensive. Potential buyers may prefer to concentrate on their existing operations.</li>
			<li> Are the <b>existing management team</b> interested in buying the business? You may find that they are the only potential buyer and that they only offer a modest price.</li>
		</ul>
		<p>It usually pays to start planning a sale well in advance. This gives you time to groom the business - fixing any issues which could dramatically affect its value and making it as attractive as possible to potential buyers.</p>
		<p>You may also want to get a preliminary valuation before you offer it for sale. For more information read our guide on how to <a href="../value-and-market-your-business.php">value and market your business</a>.</p>
