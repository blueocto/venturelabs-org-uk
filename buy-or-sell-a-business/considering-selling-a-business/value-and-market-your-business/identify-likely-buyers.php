		<h1>Value and market your business</h1>
		<h2>Identify likely buyers</h2>
		<p>Target those who you think would be prepared to pay a good price for your business because:</p>
		<ul>
			<li>you're the market leader in a particular segment - a competitor might want <strong>access to your customers</strong> so they can cross-sell their products</li>
			<li>you may have a product that fills a gap in their <strong>product range</strong></li>
			<li>they might be able to use your <strong>distribution</strong> channels to sell their product range</li>
			<li>they might benefit from <strong>economies of scale</strong> in areas such as purchasing, production and sales</li>
			<li>they might want access to your <strong>people and skills</strong></li>
			<li>you can show an excellent <strong>potential for growth</strong></li>
			<li>an <strong>overseas company</strong> might want to expand into the UK market</li>
		</ul>
		
		<p>Make sure you identify every likely candidate for your business - including private individuals and investment groups. At the same time, you want to be sure that any potential purchaser is serious. Crucially, you should satisfy yourself that they would be able to afford to buy your business. Your advisers should be able to help you establish this.</p>
		<p>For more information on finding buyers, see the page in this guide on <a href="/buy-or-sell-a-business/considering-selling-a-business/value-and-market-your-business/research-sources-of-potential-buyers.php">research sources of potential buyers</a>.</p>
