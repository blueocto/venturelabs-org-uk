	<h1>Value and market your business</h1>
	<h2>Prepare a sales memorandum</h2>
	<p>The <strong>sales memorandum</strong> is the initial marketing document you use to spark interest in your business. Your corporate finance adviser or business broker, if you have one, helps produce this as a key task in selling your business.</p>
	<p>The sales memorandum provides potential purchasers with basic information about the company and what the sale might include. It should contain details of:</p>
	<ul>
		<li>the preferred sale structure, eg assets and goodwill or share transfer</li>
		<li>which industry your business is involved in and how long you have been trading</li>
		<li>key financial figures such as profit, cashflow, value of assets and total debts</li>
		<li>similar financial figures for previous years and how they have changed</li>
		<li>number of employees, with job titles, and location of premises</li>
	</ul>

	<p>If there are any special features of your business, they need to be highlighted. For example: "Our unique patented product is the UK market leader."</p>
	<p>The sales memorandum should of course be truthful while presenting the business as positively as possible. You should highlight any opportunities for growth or profit improvement. The aim is to interest potential purchasers, so that they want to know more.</p>
	<p>Pay attention to presentation. Use clear, concise language and present financial information in a way which is visually appealing, ie through charts and tables.</p>
	<p>The document shouldn't include confidential information such as names of customers or your pricing structure. You can reveal more detailed information later on in the process of marketing your business, when you have had a chance to gauge how serious prospective purchasers are. See the page in this guide on how to <a href="/buy-or-sell-a-business/considering-selling-a-business/value-and-market-your-business/approach-potential-buyers.php">approach potential buyers</a>.</p>
	
