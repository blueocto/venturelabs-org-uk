		<h1>Value and market your business</h1>
		<h2>Approach potential buyers</h2>
		<p>Most sellers prefer to approach potential buyers through their adviser to help maintain confidentiality. Knowing that the business is for sale could upset your customers and employees. Competitors may also try to use the sale to find out your trade secrets.</p>
		<p>Using your adviser also leaves you free to concentrate on running the business. Remember that the time taken up making initial contact with and providing further information to buyers can be significant.</p>
		<p>The adviser starts by writing to potential interested parties to assess their interest. After their interest has been assessed, the adviser sends a sales memorandum or summary brief to a shortlist of potential buyers. Before giving them any more information, the adviser assesses how serious they are. Your adviser may try to keep your identity secret in the early stages.</p>
		<p>Prospective buyers are usually asked to sign a non-disclosure agreement (sometimes referred to as a confidentiality undertaking). They agree not to use or pass on any information they find out about your business. To find out more, see our guide on <a href="/create-innovate-and-protect/protecting-your-intellectual-property/patent-protection-and-your-business/non-disclosure-agreements.php">non-disclosure agreements</a>.</p>
		<p>Once buyers are seriously interested, they usually want to meet to ask more questions. Your adviser may sometimes ask them to make an opening or indicative offer before they meet you.</p>
		<p>After this, you become involved in more detailed negotiations. For more information on the next stages of selling your business, see our guide on how to <a href="/buy-or-sell-a-business/selling/complete-the-sale-of-your-business.php">complete the sale of your business</a>.</p>
		<p>You can <a href="http://web.archive.org/web/20130301091232/http://www.livingstonepartners.co.uk/practical_guide_downloads" target="_blank" rel="noopener noreferrer">get guidance on selling unquoted companies on the Livingstone Partners website</a>.</p>
		
