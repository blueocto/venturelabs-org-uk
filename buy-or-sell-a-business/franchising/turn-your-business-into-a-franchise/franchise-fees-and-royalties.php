		<h1>Turn your business into a franchise</h1>
		<h2>Franchise fees and royalties</h2>
		<p>When you franchise your business, you make money from fees the franchisee pays you.</p>
		<p>You charge an initial fee for the purchase of the franchise. This fee contributes towards the costs you have in developing and marketing the franchise concept.</p>
		<p>Most companies charge an initial fee. Fees vary but usually they are at least &pound;15,000. The fee is based on the cost of setting the franchisee up in business. This initial fee enables the franchisee to invest a larger percentage of their capital in setting up and developing the business.</p>
		
		<h3>Continuing fees</h3>
		<p>Your profits come from the continuing fees that franchisees pay you. Typically, they pay a management service fee based on turnover. If you are supplying them with products or other supplies, you can also profit from the mark-up on the prices at which you sell to them. Both these methods give you a common interest: the more the franchisee sells, the more profits you both make.</p>
		<p>Setting the right level of continuing fees requires careful judgement. If the fees are too high, the franchise will not be attractive to new franchisees and existing franchisees might struggle to keep going. Fees should be based on the services provided by the franchisor and the costs incurred in providing them.</p>
		<p>Advertising and marketing fees are also usually charged as a fixed percentage of the sales achieved by franchisees. These can range from 1 per cent to as much as 5 per cent of sales. These fees are used to fund the regional and national marketing, advertising and brand awareness initiatives that you carry out on behalf of your franchisees.</p>
		<p>Your bank may have specialist franchising advisers who can work with you on your franchising business plan and the fees you plan to charge. You may also want to take advice from your accountant or a specialist consultant.</p>
		
