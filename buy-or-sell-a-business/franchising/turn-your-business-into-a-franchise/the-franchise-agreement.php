		<h1>Turn your business into a franchise</h1>
		<h2>The franchise agreement</h2>
		<p>When you franchise your business, you - the franchisor - enter into a legal agreement with the franchisee. A clear, written contract is essential.</p>
		<p>This franchise agreement sets out what rights and obligations you each have. Key issues include:</p>
		<ul>
			<li>what territory the franchisee can operate in and whether they have exclusive rights in that territory</li>
			<li>what rights the franchisee has to use your intellectual property - eg your trade marks</li>
			<li>what restrictions there are on what the franchisee can do</li>
			<li>what fees the franchisee will pay</li>
			<li>how you will help the franchisee initially - eg training</li>
			<li>what continuing support you will provide - eg national marketing campaigns or administrative support</li>
			<li>how long the franchise lasts and what happens at the end of the term</li>
			<li>what happens if either of you wants to bring the agreement to an end or sell your business</li>
		</ul>

		<p>Agreements tend to be in favour of the franchisor. However, if the agreement is too one-sided in the franchisor's favour, it will be difficult to attract potential franchisees. For more information on what franchisees look for in a franchise agreement, see our guide on how to <a href="/buy-or-sell-a-business/franchising/buy-a-franchise.php">buy a franchise</a>. </p>
		<p>Franchise agreements involve complex legal issues.</p>
		<p>You should take advice from a lawyer with franchising expertise. <a href="https://www.thebfa.org/affiliate-members/" target="_blank" rel="noopener noreferrer">Find lawyers with franchising expertise on the British Franchise Association (BFA) website</a>.</p>

