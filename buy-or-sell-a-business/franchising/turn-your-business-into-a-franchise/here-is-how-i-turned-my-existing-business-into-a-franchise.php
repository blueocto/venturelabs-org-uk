		<h1>Here's how I turned my existing business into a franchise</h1>
		<h2>Here's how I turned my existing business into a franchise</h2>
		<p>For Neil and Mandy Chapman, a career break to indulge their passion for sailing, turned into a franchise business success story. Ten years after starting up as a small-scale yacht brokerage in Wales, Boatshed.com is now the world's largest used yacht brokerage group. Here, Neil explains how franchising worked for them.</p>
		
		<h3>What I did</h3>
		<h4>Build a strong business</h4>
		<p>"In 1999, my wife and I took 10 months' sabbatical to go on the sailing trip of a lifetime, during which we began to wonder if we could make a living buying and selling yachts. We subsequently bought a small yacht brokerage in Milford Haven. While business was OK, we knew that to grow, we needed to overcome the geographical drawbacks, ie how could we get more people to travel to the far corner of Wales to look at a boat? The internet seemed an obvious solution.</p>
		<p>"We brainstormed the website name before designing and launching the site. We included large numbers of photos &ndash; about 60 per boat - and detailed descriptions based on personal viewings of each vessel, so that people felt they weren't making the trip to see us 'on spec'. This proved the key to success. Not only were we reaching far more potential customers, those that came in person had a much higher propensity to buy than in the more traditional yacht brokerage scenario.</p>
		<p>"When sales soared from 12 yachts to 118 in our first year as owners, we knew we'd hit on a great business model."</p>
		
		<h4>Develop the franchise model</h4>
		<p>"The idea of franchising was kick-started by a competitor wanting to buy the business, but whom we licensed to use our brand and computer system instead. We sought advice from franchise specialists at Baker Tilly who helped tremendously with all the legal aspects and drafting of agreements. Two more Boatshed businesses were added shortly, in Ireland and Gibraltar.</p>
		<p>"In each case we sailed over and talked to potential franchisees in person. We registered the company names for each and gave them their own Boatshed branded website, linked into our central Boatshed.com computer system so that listings could be shared. The business grew from there, via word of mouth and targeted marketing.</p>
		<p>"While the benefits of shared listings were clear - better customer choice, shared costs, etc - the website and underlying systems couldn't cope as the business grew. We enlisted a specialist IT company to build a site that could. This has since evolved into a fully integrated brokerage administration system that serves the 70-plus international franchisees we have today."</p>
		
		<h4>IT support for franchisees</h4>
		<p>"Our computer system generates a host of management information and support that is immediately accessible to all franchisees. This is very important as it means that while we control the system centrally and are always accessible to franchisees if they need us, they have a high degree of autonomy and don't have to contact us with every little query.</p>
		<p>"We've also developed comprehensive training programmes and manuals, all of which are available online and through a variety of related media such as podcasts. As well as meeting face-to-face at regular conferences and company events, we use online forums and Skype to facilitate daily contact and sharing of best practice between the Boatshed businesses.</p>
		<p>"The overall model has proved so successful that now we get up to a dozen enquiries per day from people wanting to open a Boatshed.com franchise."</p>
		
		<h3>What I'd do differently</h3>
		<h4>Don't say yes to everyone</h4>
		<p>"Early on, it was tempting to recruit everyone who wanted to become a franchisee, even when our instincts told us they weren't quite right. We should have stuck to our guns and rejected a couple of applicants who we did take on, but subsequently had to get rid of."</p>
		
