		<h1>Turn your business into a franchise</h1>
		<h2>Is franchising right for your business?</h2>
		<p>Many businesses have used franchising successfully, including well-known names like McDonalds, Prontaprint, Domino's, Molly Maid, Thorntons, Snap-on, Rosemary Conley and Select Appointments. However, franchising doesn't suit every business.</p>
		<p>To start with, your business needs to be successful. Nobody will want to buy the right to franchise a business that doesn't make money. A franchised business needs to be profitable enough to make money for both the franchisee and you.</p>
		<p>More broadly, your business needs to be one that can be replicated in different locations by your franchisees. Businesses that need high skills levels or professional qualifications can be more difficult to franchise but all the major optician chains have succeeded in doing so.</p>
		<p>At the same time, you need to offer your franchisees something that makes it worth their while paying you, instead of simply setting up their business independently. For example, you might have a recognised brand name, provide equipment or supplies they need, or help with training and marketing support. For more information, see the page in this guide on how to <a href="/buy-or-sell-a-business/franchising/turn-your-business-into-a-franchise/develop-your-business-format.php">develop your business format</a>.</p>
		<p>Having a well-organised and well-run franchise helps you recruit franchisees and is a strong incentive for franchisees to remain part of the franchise at the end of the initial franchise period.</p>
		<p>For more information on how franchisees assess opportunities, see our guide on how to <a href="/buy-or-sell-a-business/franchising/buy-a-franchise.php">buy a franchise</a>.</p>
		
		<h3>Your resources</h3>
		<p>You also need to think about the demands franchising places on you. You need to invest in developing and marketing the format. If you have limited financial resources, or are already working flat out running your business, you may not be able to do this.</p>
		<p>Finally, you need to have the right skills and attitude to make franchising a success. You need to be able to sell your concept to potential franchisees, and to work with and control them. Rather than dealing directly with customers yourself, you profit by helping your franchisees to be successful.</p>
		<p>For more information, <a href="https://www.thebfa.org/" target="_blank" rel="noopener noreferrer">read about businesses that suit franchising on the British Franchise Association (BFA) website</a>.</p>
		
