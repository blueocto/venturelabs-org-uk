			<h1>Turn your business into a franchise</h1>
			<h2>Franchising options</h2>
			<p>If you have a successful business and are looking to expand, you might want to open additional outlets. Franchising your business can be a very effective way of doing this. Instead of financing and managing the new outlets yourself, you work with independent franchisees.</p>
			<h3>Business format franchising</h3>
			<p>With business format franchising, you put together a complete business package. You then licence this format to franchisees. They run their own businesses, but use your methods and trading name.</p>
			<ul>
				<li>You provide an operations manual, detailing how to set up and manage a new outlet. For more information, see the page in this guide on how to <a href="/buy-or-sell-a-business/franchising/turn-your-business-into-a-franchise/develop-your-business-format.php">develop your business format</a>.</li>
				<li>You agree a contract with your franchisee setting out what rights and obligations you each have. For example, you might provide marketing support. For more information, see the page in this guide on <a href="/buy-or-sell-a-business/franchising/turn-your-business-into-a-franchise/the-franchise-agreement.php">the franchise agreement</a>.</li>
				<li>The franchisee pays you for the right to use your business concept. For more information, see the page in this guide on <a href="/buy-or-sell-a-business/franchising/turn-your-business-into-a-franchise/franchise-fees-and-royalties.php">franchise fees and royalties</a>.</li>
				<li>You train and support the franchisee through their start-up period. Support could include ongoing marketing, business growth help, regular regional meetings or how to negotiate purchasing contracts.</li>
			</ul>

			<h3>Other franchising arrangements</h3>
			<p>There are other business arrangements which are sometimes also referred to as franchising, including:</p>
			<ul>
				<li>selling a <strong>licence</strong> allowing someone else to manufacture and sell your product, but without telling them how to run their business</li>
				<li>using an <strong>agent</strong>, who sells your product on your behalf</li>
				<li>setting up a <strong>distribution agreement</strong>, whereby you sell your products to another business that then sells them to their own customers</li>
			</ul>

			<p>This guide focuses on business format franchising. For more information on using agents and distributors, see our guide on how to <a href="/sales-and-marketing/keeping-your-customers/reach-your-customers-effectively.php">reach your customers effectively</a> or <a href="/sales-and-marketing/selling/identify-potential-sales-channels.php">use our interactive tool to identify the sales channels most likely to benefit your business</a>.</p>
			
