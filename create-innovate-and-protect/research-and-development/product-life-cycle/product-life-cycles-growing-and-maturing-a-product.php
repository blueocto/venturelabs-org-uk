		<h1>Product life cycle</h1>
		<h2>Product life cycles - growing and maturing a product</h2>
		<p>At this point in your product's life cycle, you should be putting your efforts into increasing your product's market share and creating a brand preference for your customers.</p>
		<p>This should be a period of rapid growth in both sales and profits for your product or service. Your profits should rise through an increase in output and more competitive pricing.</p>
		<p>You should also consider the following:</p>
		<ul>
			<li>maintaining product quality and including additional features or support services for the product</li>
			<li>maintaining pricing to increase demand for the product</li>
			<li>increasing distribution channels to cope with additional demand</li>
			<li>aiming promotion at a wider audience</li>
		</ul>

		<p>If your profits are still low, you could consider reducing the price of the product or service to increase the volume of sales.</p>
		
		<h3>Product maturity stage</h3>
		<p>If your product or service makes it to the maturity stage, this should be the longest part of its product life cycle.</p>
		<p>At this point, the market has often reached saturation as a result of competitors releasing their own version of your product. You will notice your product or service experiences a decreasing rate of sales, which should eventually stabilise.</p>
		<p>You should try to differentiate your product or service from others that your competitors offer. You can do this by focusing and highlighting any branding, trademarks, or customer testimonials that may give you an advantage.</p>
		<p>You will probably notice the following trends at the product maturity stage:</p>
		<ul>
			<li>product features may have to be enhanced to make your product more appealing than competitors'</li>
			<li>pricing may need to be lowered due to increased competition</li>
			<li>distribution becomes more intensive and incentives may need to be offered</li>
			<li>promotion will focus on the difference between existing products</li>
		</ul>

