		<h1>Product life cycle</h1>
		<h2>Product life cycle checklist</h2>
		<p>By understanding the life cycle of your product or service, you can react to situations that arise within your business. Being organised and prepared for each stage gives you the opportunity to create maximum profitability. This checklist outlines the key points to consider throughout your product or service's life cycle.</p>
		
		<h3>Pre-development</h3>
		<ul>
			<li>Have you got an initial idea for a product or service that you believe will be profitable?</li>
			<li>Who could you work with to generate new ideas and embed a research and development process?</li>
			<li>Consider how each stage of the product life cycle will practically affect the business and its profitability.</li>
			<li>What services of specialist professionals - eg designers, materials experts, retailers, customers and recycling experts - do you need to employ at different stages of your product development?</li>
			<li>Are you setting out to create a sustainable product or service with a closed loop life cycle?</li>
		</ul>

		<h3>Development stage</h3>
		<ul>
			<li>Set key performance indicators and plan potential outcomes before developing your product or service and analyse any barriers to success and the risks involved in the whole product development and marketing process.</li>
			<li>Improve your development processes by getting help or feedback from team members, suppliers and business associates, or customers.</li>
			<li>Produce prototypes of products and work with designers, suppliers and manufacturers to ensure you have the correct pricing for the materials used.</li>
			<li>Sort out potential intellectual property issues, such as registering any relevant patents or copyrights.</li>
		</ul>

		<h3>Introducing your product</h3>
		<ul>
			<li>Do you intend to launch with an intense marketing effort? Focusing on establishing a clear brand identity and connecting with the right partners can help you promote your product.</li>
			<li>Consider setting up consumer tests, or providing samples or trials to key target markets. Customer testimonials can increase your product or service's popularity.</li>
			<li>Price the product or service as high as you believe you can feasibly sell it, and to reflect the quality level you are providing.</li>
			<li>Try to limit the product or service to a specific type of consumer - being selective can boost demand.</li>
		</ul>

		<h3>Product growth - increasing profits</h3>
		<ul>
			<li>Maintain quality and consider including additional features or support services for the product or service to grow your market share.</li>
			<li>Keep your pricing as high as is reasonable to keep demand and profits high.</li>
			<li>Increase distribution channels to cope with additional demand.</li>
			<li>Increase your promotion and marketing campaigns and aim them at a wider audience.</li>
			<li>Consider skimming product prices if your profits are too low.</li>
		</ul>

		<h3>Maturity stage</h3>
		<ul>
			<li>Market saturation means you have to embrace some changes to your product, service or prices.</li>
			<li>Update or improve the original version of your product or service to lure back existing customers and entice new ones.</li>
			<li>Consider lowering your prices to attract consumers from rival businesses.</li>
			<li>Use the difference between your product and others as a means of promotion.</li>
		</ul>

		<h3>Decline - end of product life</h3>
		<ul>
			<li>Maintain the product or service and wait for competitors to withdraw from the market first.</li>
			<li>Reduce costs and find another use for the product or service.</li>
			<li>Harvest the product or service before discontinuing it.</li>
			<li>Discontinue the product or service when your profit disappears, or when you unveil a successor.</li>
		</ul>

