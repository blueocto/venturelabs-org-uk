		<h1>Product life cycle</h1>
		<h2>Development stage of a product life cycle</h2>
		<p>Product development is the first stage of the product life cycle - only beginning when you have found and started to develop a new idea.</p>
		<p>You may be an inventor who has already come up with a new product or service idea, and are ready to bring that idea to market. However, if you are a business that needs some help to regularly develop new ideas, you might want to partner with innovators, designers, university researchers or manufacturers to develop your ideas generation processes. Then you can work as a team to develop the idea.</p>
		<p>Read our guide on <a href="/create-innovate-and-protect/research-and-development/support-networks-and-facilities-for-innovation-and-r-and-d.php">support networks and facilities for innovation and R&amp;D</a>.</p>
		<p>At the development stage of the product life cycle, you should ensure that your idea will meet:</p>
		<ul>
			<li>potential customer expectations</li>
			<li>design, resource and manufacturing requirements</li>
			<li>the strategy outlined in your business plan</li>
		</ul>

		<p>You should plan for all the potential outcomes and risks in advance so you can analyse what is involved in the process. At this stage you should not be concerned about sales or introducing the product. Your main focus should be on working with a team of designers, manufacturers or product development experts on:</p>
		<ul>
			<li>producing prototypes</li>
			<li>testing prototyped product</li>
			<li>sourcing and pricing materials</li>
			<li>intellectual property issues</li>
		</ul>

		<p>To further develop your product, you should:</p>
		<ul>
			<li>consult team members on development plans</li>
			<li>speak to suppliers and other business associates</li>
			<li>communicate with customers about your plans</li>
			<li>consider the environmental impacts of your product</li>
			<li>ask a group of potential customers to test your product and give feedback - you can use this to develop the product and, later in the product's life cycle, to market it</li>
		</ul>

		<p>When developing your product or service you need to establish the level of quality you are aiming for, and how many different versions you want to develop to generate interest at launch.</p>
		<p>You should also ensure that all intellectual property rights - eg patents and trademarks - are obtained before you launch the product or service. Doing this protects you from other competitors copying the idea and hurrying through an alternative.</p>
		<p>How you plan to distribute and sell your product may also determine how it is developed. For instance, if you plan to sell online, it may well be worth designing the product and its packaging so that it is small enough to fit through a customer's letterbox, so that customers don't miss out on deliveries when not at home.</p>
		
