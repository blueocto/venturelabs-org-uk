		<h1>Product life cycle</h1>
		<h2>Product life cycles - decline stage</h2>
		<p>Due to factors such as technological advances, fashion, innovation or changing consumer tastes, the sales of most products will decline at some stage. You will know when your product reaches the decline stage of its life cycle because you will notice a significant downturn in the revenue it generates.</p>
		<p>At this stage, you should consider the following options:</p>
		<ul>
			<li>maintaining the product in the hope that your other competitors will withdraw their versions before you, which may create an increase in demand again</li>
			<li>reducing your costs and finding another use for the product - entering into another niche area could increase profits</li>
			<li>reducing marketing support, 'harvesting' the product, coasting along until profits dry up and then discontinuing the product</li>
			<li>discontinuing the product when your profit disappears, or when you unveil a successor product</li>
		</ul>

		<p>Some of these methods can form an 'extension strategy' that prolongs the life of your current product or service. This may give you enough time to create a new version, or an entirely new product. However, this strategy will only temporarily delay a product or service's decline.</p>
		<p>By understanding the product life cycle of all of your products and services, you can ensure that at least one of your ventures is at growth or maturity stage, while another is in decline. By doing this you can guarantee a regular source of profit for all of your products.</p>
		
