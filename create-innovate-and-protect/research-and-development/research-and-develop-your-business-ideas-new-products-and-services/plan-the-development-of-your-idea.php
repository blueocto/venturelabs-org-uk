		<h1>Research and develop your business ideas, new products and services</h1>
		<h2>Plan the development of your idea</h2>
		<p>Try to identify the key stages or <strong>checkpoints</strong> in the development of your idea. Each checkpoint gives you a chance to evaluate the progress of your product or service and decide whether you need to make any changes. It will also allow you to consider at the end of each stage whether to progress with the idea. Any idea that has no realistic chance of success should be dropped before too much time or money is invested.</p>
		<p>Your checkpoints may include:</p>
		<ul>
			<li>
			<strong>designing</strong> - turning your idea into a product or service that can actually be sold
			</li>
			<li>
			<strong>prototyping</strong> - creating a useable example of your product or service, which can then be tested
			</li>
			<li>
			<strong>protecting</strong> - applying for a patent if you have invented a product or type of technology
			</li>
			<li>
			<strong>financing</strong> - raising the money you will need to get your business started
			</li>
			<li>
			<strong>operations</strong> - setting up the structure of your business, eg finding a suitable location, hiring staff, etc
			</li>
			<li>
			<strong>marketing</strong> - working out how you will sell your product or service
			</li>
		</ul>

		<h3>Judge your progress</h3>
		<p>If the goals of any of your checkpoints are not met, you need to analyse why this is the case. Ask yourself whether your objectives were unreasonable. If so, you may need to revise your objectives.</p>
		<p>There are circumstances when you should reconsider your original idea.</p>
		<p>These include:</p>
		<ul>
			<li>
			developing a product or service that isn't commercially viable and won't produce a reasonable financial return
			</li>
			<li>
			developing a product that is not technically viable, eg it cannot be manufactured or it doesn't meet performance requirements
			</li>
			<li>
			someone else releasing a product or service which is very similar or identical to yours, especially if they are a large or well-established competitor
			</li>
		</ul>

