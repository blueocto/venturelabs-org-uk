		<h1>Research and develop your business ideas, new products and services</h1>
		<h2>Test the market</h2>
		<p>Product testing is important throughout the design process. While you are developing your product or service it's a good idea to keep testing the market to make sure you are still on the right track. You can do this by using:</p>
		<ul>
			<li>
			<strong>Focus groups</strong> - ask small groups of your target customers what they want from your product or service.
			</li>
			<li>
			<strong>Questionnaires</strong> - try to get as wide a sample as possible.
			</li>
			<li>
			<strong>Prototypes</strong> - show an early version of your product to customers. You may find that your prototype will go through several stages of development as you refine your idea.
			</li>
		</ul>
		<p>You may need to respond to suggestions from users by modifying the design. Don't be discouraged, as most successful entrepreneurs do not view this as a failure, but as a learning curve.</p>
		<p>It's a good idea to send your product to a large or very reputable potential customer or user. A positive testimonial will prove invaluable as you approach other customers.</p>
		<p>You may want to consider testing even after your product goes on sale. Ongoing contact with customers can uncover both the shortcomings of your product and possible opportunities that you may have missed.</p>
		<p>Once you have a final product, you can then set about building a brand. A brand includes everything that is visible to the customer, such as the product name, its packaging and its delivery.</p>
		<p>You also need to consider your <strong>pricing policy</strong>. You need to cost all the materials, other inputs, machinery, processes and administrative time realistically. You will need to research different suppliers and the cost of marketing and distribution. Then check on the price at which your competitors are selling to customers. You can determine the price of your product or service so that it is attractive to customers as well as making you a profit.</p>

