		<h1>Research and develop your business ideas, new products and services</h1>
		<h2>Financing your idea and cost control</h2>
		<p>Securing adequate funding is one of the biggest obstacles many entrepreneurs face. Your funding needs may also change during the course of product development, as it may take longer or cost more than you first expected.</p>
		<p>Bank loans and overdrafts are the most common ways to raise money for a new business. But there are plenty of alternative options too, including:</p>
		<ul>
			<li>cashing in shares or other investments you may hold</li>
			<li>borrowing money from family or friends</li>
			<li>remortgaging your property</li>
			<li>non-bank finance - eg credit unions or peer-to-peer loans</li>
			<li>government grants</li>
			<li>investment from business angels or venture capitalists</li>
		</ul>
		<p>Remember to build into your financial forecasts a generous margin for contingencies and the unexpected. It's not worth investing money and then running out before your business has got off the ground.</p>
		<p>For advice on working out how much money you will need, and the pros and cons of the different funding options, see our guide on how to <a href="/finance-and-grants/finance-options/choose-the-right-finance-options/choose-the-right-finance-when-starting-up.php">choose the right finance when starting up</a>.</p>
		<p>It is important to plan any investment and control your costs carefully. You should:</p>
		<ul>
			<li>include future investment in products and services into your strategic business plan</li>
			<li>plan exactly where this investment will be directed</li>
			<li>justify the expenditure on every development project</li>
			<li>manage your cost</li>
		</ul>
		<p>Before making investment decisions, consider how much your business stands to gain from the new product or service. Weigh this against any risks you face.</p>
		<h3>Phasing new product development</h3>
		<p>One way to minimise your risks is to phase investment in projects. By reviewing a project at the end of each stage of development, you can identify products or services that are unlikely to be successful. If the product or service fails to meet established criteria, you should consider cancelling the project. If it does meet them, you can allocate the resources to allow it to reach the next development stage.</p>
		<h3>Cost control</h3>
		<p>It's essential to keep a close eye on costs when you develop new products and services to avoid them spiralling out of control. You should:</p>
		<ul>
			<li>estimate development costs in advance</li>
			<li>monitor spending throughout the development process</li>
			<li>introduce phased investment</li>
		</ul>
		<p>There are two main ways to estimate costs:</p>
		<ul>
			<li>a top-down approach where you consider previous comparable projects and use them as a benchmark</li>
			<li>a bottom-up approach where all team members agree the costs they expect to incur with one project manager, who will then estimate the total cost</li>
		</ul>
		<p>Remember that your costs could include staffing, materials, technology, product design, market research, prototyping and overhead costs.</p>
		<p>For more information on working with formal project management systems for the first time, see our guide on <a href="/grow-your-business/planning-how-to-grow/project-management-the-basics.php">project management - the basics.</a></p>

