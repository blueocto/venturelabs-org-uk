		<h1>Research and develop your business ideas, new products and services</h1>
		<h2>The product development process</h2>
		<p>The process of developing new products or services can be divided into a number of key stages:</p>
		<ul>
			<li>Idea generation - to come up with innovative new ideas.</li>
			<li>Idea distillation - to screen out any ideas not worth taking forward.</li>
			<li>Concept definition - to consider specifications such as technical feasibility and market potential. If you're planning a new product, you should also consider the design process at this point - see our guide on <a href=" /create-innovate-and-protect/design-for-business-success/how-can-I-use-design-in-my-business.php">how can I use design in my business?</a> You can also <a href="http://www.designcouncil.org.uk/resources/search/%2522product%2520design%2522?retain-filters=1" target="_blank" rel="noopener noreferrer">find information on the design process on the Design Council website</a>.</li>
			<li>Strategic analysis - to ensure your ideas fit into your business' strategic plans.</li>
			<li>Concept development - to create a prototype product or pilot service.</li>
			<li>Test marketing and finalising the concept - to modify the product or service according to customer, manufacturer and support organisations' feedback. This means deciding the best timing and process for piloting your new product or service.</li>
			<li>Product launch - before setting a date for your launch, you should determine how you will sell, promote and support your product or service. It is important to get it right the first time, but remember that any decisions to delay your launch should be balanced against the danger that your competitors will beat you to market.</li>
		</ul>
		<p>You can <a href="#">see our diagram of the product development process</a>.</p>
		<p>Some of these stages could overlap, but the presence of a staged product development process will help keep timing and costs under control. For more information on managing projects, see our guide on how to <a href="/create-innovate-and-protect/research-and-development/manage-your-research-design-and-development.php">manage your research, design and development</a>.</p>
		<h3>The lifecycle of products and services</h3>
		<p>All products and services have a lifecycle - this is the period that runs from the initial idea and development of a product to its withdrawal from the market and beyond. There are five key stages in the lifecycle of any product or service:</p>
		<ul>
			<li>Development</li>
			<li>Introduction</li>
			<li>Growth </li>
			<li>Maturity</li>
			<li>Decline</li>
		</ul>
		<p>Identifying where products or services are in their lifecycle is central to your profitability. For more information, see our guide on the <a href="/create-innovate-and-protect/research-and-development/product-life-cycle.php">product life cycle</a>.</p>

