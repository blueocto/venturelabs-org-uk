		<h1>Support networks and facilities for innovation and R&amp;D</h1>
		<h2>Chambers of Commerce</h2>
		<p>The British Chambers of Commerce form a network of 100 quality-accredited chambers throughout the UK.</p>
		<p>Becoming a member of a chamber gives a business access to services such as:</p>
		<ul>
			<li>training</li>
			<li>information</li>
			<li>resources</li>
			<li>networking</li>
			<li>savings on essential overheads</li>
		</ul>

		<p>Some services are available online. Larger chambers may offer schemes to encourage innovation and foster best practice. Many areas also have more informal networks, some supporting particular industries.</p>
		<p>You can <a href="http://www.britishchambers.org.uk/find-your-chamber/" target="_blank" rel="noopener noreferrer">find Chambers of Commerce in your area on the British Chambers of Commerce website</a>.</p>
