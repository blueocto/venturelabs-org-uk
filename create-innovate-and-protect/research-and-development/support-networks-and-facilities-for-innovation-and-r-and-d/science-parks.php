		<h1>Support networks and facilities for innovation and R&amp;D</h1>
		<h2>Science parks</h2>
		<p>A science park brings together a group of knowledge-based businesses, along with support and advice to help the businesses grow.</p>
		<p>The support each science park offers varies, but typical services include:</p>
		<ul>
			<li>help with premises</li>
			<li>technology expertise</li>
			<li>business services ranging from advice on intellectual property to security and cleaning facilities</li>
		</ul>

		<h3>How science parks differ</h3>
		<p>Supported through a variety of local, regional, national and European Union funds, the structure of science parks can vary and there is no common formula for ownership. Universities, local authorities, private companies and property developers can all be involved in different ways and to different levels.</p>
		<p>The UK now has over 100 science parks. 'Science park' is the umbrella term used for research parks, technology parks, incubators, innovation centres and technoparks. Different types of science park have different aims, for instance:</p>
		<ul>
			<li>
				an incubator supports new businesses during their early years
			</li>
			<li>
				some science parks specialise in a particular industry, such as biotechnology or information technology
			</li>
			<li>
				a research park may only allow research and development businesses on it
			</li>
		</ul>

		<p>Science parks have formal and operational links with centres of knowledge creation, such as universities, in areas such as:</p>
		<ul>
			<li>technology transfer</li>
			<li>sourcing venture capital</li>
			<li>student placements</li>
			<li>marketing assistance</li>
		</ul>

		<p>For example, businesses in the science park can tap into a university's resources and may be able to commercially exploit research being carried out there.</p>
		<p><a href="http://www.ukspa.org.uk/" target="_blank" rel="noopener noreferrer">Find out about science parks on the UK Science Park Association (UKSPA) website</a>.</p>
		
