			<p>Developing a unique product or idea can often be crucial to the success of a new business.</p>
			<p>Sharing new knowledge and original work which you intend to use commercially requires a high level of mutual trust. It is essential that you consider confidentiality before approaching another company or individual when seeking to develop your ideas. The UK law of confidentiality is the right to prevent disclosure of confidential information acquired in confidential circumstances.</p>
			<p>A non-disclosure agreement (NDA), also known as a confidentiality agreement, is a legal contract between you and another party not to disclose information that you have shared for a specific purpose.</p>
			<p>This guide explains NDAs, their key terms and their impact. The guide also highlights issues that you need to bear in mind when preparing and signing an NDA.</p>
			
