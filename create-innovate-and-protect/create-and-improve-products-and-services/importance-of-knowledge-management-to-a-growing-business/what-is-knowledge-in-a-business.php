			<h1>Importance of knowledge management to a growing business</h1>
			<h2>What is knowledge in a business?</h2>
			<p>Using knowledge in your business isn't necessarily about thinking up clever new products and services, or devising ingenious new ways of selling them. It's much more straightforward.</p>
			<p>Useful and important knowledge already exists in your business. It can be found in:</p>
			<ul>
				<li> the <strong>experience</strong> of your workforce</li>
				<li> the <strong>designs and processes</strong> for your goods and services</li>
				<li> your files or <strong>documents</strong> (whether held digitally, on paper or both)</li>
				<li> your <strong>plans</strong> for future activities, such as ideas for new products or services</li>
			</ul>
			
			<p>The challenge is to harness these intangible assets in a coherent and productive way.</p>
			<p>You can find out more about what intangible assets are in our guide on <a href="/grow-your-business/assessing-current-performance/identifying-protecting-and-transferring-your-assets.php">identifying, protecting and transferring your assets</a>.</p>
			<p>How a business creates, shares and applies knowledge in order to help it achieve its goals is known as <strong>knowledge management</strong>.</p>
			<p>An effective knowledge management strategy will make it possible for knowledge to be shared at a variety of levels including:</p>
			<ul>
				<li>between individuals</li>
				<li>among a team</li>
				<li>among an entire organisation</li>
				<li>between different organisations</li>
			</ul>
			
			<h3>Existing forms of knowledge</h3>
			<p>You have probably done <b>market research</b> into the need for your business to exist in the first place. If nobody wanted what you're selling, you wouldn't be trading. You can tailor this market knowledge to target particular customers with specific types of product or service. See our guide on <a href="/sales-and-marketing/marketing/market-research-and-market-reports.php">market research and market reports</a> to find out how market research can improve your business.</p>
			<p>Your files or <strong>documents</strong> from and about customers and suppliers hold a wealth of information which can be invaluable both in developing new products or services and improving existing ones.</p>
			<p>Your <b>workers</b> are likely to have <strong>skills and experience</strong> that you can use as an asset. Having staff who are knowledgeable can be invaluable in setting you apart from competitors.</p>
			<p>You should make sure that your employees' knowledge and skills are passed on to their colleagues and successors wherever possible, eg by</p>
			<ul>
				<li>holding brainstorming sessions</li>
				<li>organising training courses</li>
				<li>maintaining up-to-date documentation about processes and procedures</li>
			</ul>
			
			<p>See the page in this guide on how to <a href="/create-innovate-and-protect/create-and-improve-products-and-services/importance-of-knowledge-management-to-a-growing-business/create-a-knowledge-strategy-for-your-business.php">create a knowledge strategy for your business</a>.</p>
			<p>Your understanding of what customers want, combined with your workers' know-how, can be regarded as your <strong>knowledge base</strong>.</p>
			<p>Using this knowledge in the right way can help you run your business more efficiently, decrease business risks and exploit opportunities to the full. This is known as the <strong>knowledge advantage</strong>.</p>
			
