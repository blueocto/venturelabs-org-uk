		<h1>Choose the right premises for your business</h1>
		<h2>Search for premises</h2>
		<p>It's worth starting by drawing up a specification or 'spec', clearly setting out your requirements. Distinguish between what is essential and what is desirable. For more information, see the page in this guide on <a href="/premises-and-property/choosing-the-right-premises/choose-the-right-premises-for-your-business/specifying-your-premises-requirements.php">your requirements</a>.</p>
		<p>You can then circulate this spec to estate agents and surveyors that handle commercial properties in your area. Your local authority, Chamber of Commerce or trade association may also be able to help.</p>
		<p>Many local authorities maintain a register of available commercial property. It's also worth investigating any grants, loans on preferential terms and incentive schemes set up to tempt small businesses into urban areas, such as grants from inner city renewal projects, or cheaper premises for small businesses in designated areas. Some councils or local authorities have set up Enterprise Zones to boost local economies.</p>
		<p>You may also decide to search for suitable premises yourself. Commercial property is often advertised in property sections of local papers, as well as specialist publications. You could also try searching on the internet, or taking a tour of your area to look for advertising boards on local properties.</p>
		<p>Check any potential premises against your spec and eliminate any that don't at least meet all your essential requirements. Then you can draw up a shortlist of properties to visit.</p>
		<p>At this stage, you may want to involve professional advisers. For example, a surveyor can assess the condition of premises and give you an idea of their value. For more information, see our guides on <a href="/your-business-sector/property-management/commercial-property/buying-business-premises.php">buying business premises</a> and <a href="/premises-and-property/choosing-the-right-premises/renting-business-premises.php">renting business premises</a>.</p>
		<p>When you're ready to make an offer or agree the terms of a lease, your solicitor can help negotiate the deal and complete the legal work. For more information, see our guide on how to <a href="/finance-and-grants/expert-financial-advice/choose-and-work-with-a-solicitor.php">choose and work with a solicitor</a>.</p>
		
