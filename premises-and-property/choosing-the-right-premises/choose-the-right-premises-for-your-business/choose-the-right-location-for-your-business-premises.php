		<h1>Choose the right premises for your business</h1>
		<h2>Choose the right location for your business premises</h2>
		<p>A good location for your business is vital, but choosing the right one can be something of a balancing act. Ideally, the location should be convenient for your customers, employees and suppliers - without being too expensive.</p>
		<p>Factors to consider include:</p>
		<ul>
			<li>the level of passing trade - depending on the nature of your business, the level of passing trade can have a huge impact on the success of your venture</li>
			<li>the number of competitors - although some businesses, like estate agents, can benefit from being located in a cluster of similar businesses, for many others having too many close competitors can have a severe impact on sales and profitability</li>
			<li>transport links and parking - good public transport links and local parking facilities make it easier for employees and customers who don't live within walking distance</li>
			<li>delivery restrictions - these can cause problems for your suppliers, so you'll need to make sure that your premises are accessible if you expect to have regular deliveries</li>
			<li>congestion charges</li>
			<li>planning restrictions - make sure you check whether you're allowed to use the premises for the purpose you have in mind</li>
			<li>local authority charges and business rates for services such as waste collection - these can add greatly to the ongoing costs of locating in a particular area, which may make the premises less desirable from your point of view</li>
			<li>local amenities - employees generally prefer working in areas with good local facilities, and you may need to make regular trips to the bank or a postal depot</li>
			<li>what sort of area it is - the image of your business may well be affected by the nature of your location</li>
		</ul>

		<p>Whatever option you go for, there are likely to be advantages and disadvantages. An office in a rural setting might be relaxing, but could be awkward for staff or suppliers making deliveries. Being right in the middle of the city could be very convenient, but might also be expensive.</p>
		<p>Location has a major impact on cost. If you need premises in a prime location the extra costs may be justified.</p>
		
