		<h1>Cookies</h1>

		<p>Venture Labs puts small files (known as ‘cookies’) onto your computer to collect information about how you browse the site.</p>
		<p>Cookies are used to:</p>
		<ul>
			<li>measure how you use the website so it can be updated and improved based on your needs</li>
			<li>remember the notifications you’ve seen so that we don’t show them to you again</li>
		</ul>
		<p>Venture Labs cookies aren’t used to identify you personally.</p>
		<p>You’ll normally see a message on the site before we store a cookie on your computer.</p>
		<p>Find out more about <a href="https://ico.org.uk/for-the-public/online/cookies/" target="_blank" rel="noopener noreferrer">how to manage cookies</a>.</p>
		
		<h2>How cookies are used on Venture Labs</h2>
		
		<h3>Measuring website usage (Google Analytics)</h3>
		<p>We use Google Analytics software to collect information about how you use Venture Labs. We do this to help make sure the site is meeting the needs of its users and to help us make improvements, for example <a href="https://insidegovuk.blog.Venture Labs/2015/03/26/new-tool-to-see-trending-searches/" target="_blank" rel="noopener noreferrer">improving site search</a>.</p>
		<p>Google Analytics stores information about:</p>
		<ul>
			<li>the pages you visit on Venture Labs</li>
			<li>how long you spend on each Venture Labs page
			</li>
			<li>how you got to the site
			</li>
			<li>what you click on while you’re visiting the site</li>
		</ul>
		<p>We don’t collect or store your personal information (for example your name or address) so this information can’t be used to identify who you are.</p>
		<p>We don’t allow Google to use or share our analytics data.</p>
