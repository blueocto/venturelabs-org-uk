		<h1>New consultation launched</h1>
		<h2>Simpler Income Tax for Self employed and Small Partnerships</h2>
		<p><strong>What's the Consultation about?</strong></p>
		<p>The Government wants to make it easier for small businesses - that is, most self-employed sole traders and those in partnership with other individuals - to calculate their taxable income.</p>
		<p>From April 2013, the Government proposes to introduce a voluntary cash basis for small businesses to calculate their income tax along with simplified arrangements for some business expenses.</p>
		<p><strong>What are the proposals?</strong></p>
		<p>The proposals are, that those who choose to use the new regime will be taxed on the basis of their receipts less allowable payments for expenses, rather than needing to spend their time doing accounting designed more for big business.</p>
		<p>The Government is exploring proposals that small businesses with receipts of less than &pound;77,000 would be eligible to use the cash basis, and that they could continue to use it until their receipts rise to more than &pound;150,000 in any year.</p>
		<p>Those businesses using the cash basis would use simplified expenses for business use of their cars or home and personal use of business premises. Businesses that do not opt to use the cash basis could however choose to use these simplified expenses.<br />
			(The proposals do not include companies or property businesses.)</p>
		<p><strong>How can I find out more?</strong></p>
		<p>The consultation period runs until 22 June 2012, <a href="#">you can download the full consultation proposal Simpler Income Tax for the Simplest Small Businesses<span> - Opens in a new window</span></a> (PDF, 140K)</p>
