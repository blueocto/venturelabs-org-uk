		<h1>Wickes launches Tradebuilder website</h1>
		<h2>Tradesmen get a helping hand with a business information portal created in conjunction with Businesslink.gov.uk</h2>
		<p>Recent statistics released by <a href="http://www.theconstructionindex.co.uk/" target="_blank" rel="noopener noreferrer">the Construction Index</a> reveal that the first quarter of this year saw a 23 per cent increase in the number of construction companies going into administration in the UK compared to the last quarter of 2010. Whilst technically competent, many tradesmen lack a solid business background meaning that the failure rate of new businesses in the building sector is second only to restaurants.</p>
		<p>To help address this issue, Wickes has joined forces with government's online resource for businesses, Venture Labs, to launch <a href="#">www.wickestradebuilder.co.uk</a>. This first-of-its-kind business information website has been created especially for tradesmen in a move that sees expert industry-specific advice and guidance provided via a public/private link-up.</p>
		<p>New business owners in all sectors frequently lack relevant knowledge and expertise in areas such as finance, tax, and managing employees. This skills shortage is particularly acute in the building trade.</p>
		<p>This is reflected in research released by Wickes that reveals 38 per cent of British tradesmen do not seek any business advice, with 35 per cent stating that it is difficult to find the business information they need**. Just 2 per cent of those questioned currently seek business advice from a business advisory service and only 1 per cent go to a professional trade body for guidance.</p>
		<p><a href="#">Wickes Trade Builder</a> is a simple to access and navigate website developed to provide tailored business information for tradesmen. The site is the first online resource to provide the key information an independent tradesman needs to run a successful business, including template invoices, tips on getting and keeping customers, navigating the legalities surrounding the hiring of employees and apprentices, and tax assessment guidelines.</p>
		<p>** Research conducted amongst 500 British tradesmen between 15 - 22 August 2011 by Zebra Square</p>
		
