		<h2>Introduction</h2>
		<img src="../../_img/HS2140.gif" width="78" height="118" border="0" alt="" />
		<p>You have to consult with all your employees on health and safety. This does not need to be complicated. You can do this by simply listening and talking to them about:</p>
		<ul>
			<li>health and safety and the work that they do</li>
			<li>how risks are controlled</li>
			<li>the best ways of providing information and training</li>
		</ul>
		<p>Consultation is a two-way process, allowing staff to raise concerns and influence decisions on the management of health and safety. Your employees are often the best people to understand the risks in the workplace and involving them in making decisions shows them that you take their health and safety seriously.</p>
		<p>In a very small business, you may choose to consult employees directly. Alternatively, you might consult your employees through a health and safety representative. This can be someone who has been chosen by their colleagues or selected by a trade union. As an employer, you cannot decide who will be the representative.</p>

