			<h1>Extra help and support available for your start up</h1>
			<h2>Support for black, Asian and minority ethnic start ups</h2>
			<p>There are several established business support networks specifically for black, Asian and minority ethnic businesses that provide information about starting a business, how to get finance and advice and other help once the business is up and running. This support includes:</p>
			<ul>
				<li>The <strong>Asian Business Development Network</strong> (ABDN) can help you develop your business by sharing best practice and improving opportunities - <a href="https://web.archive.org/web/20120710012027/http://www.abdn.org.uk" target="_blank" rel="noopener noreferrer">find information on the work and services of the ABDN on the ABDN website</a></li>
				<li>The <strong>African Caribbean Business Network</strong> (ACBN) provides a business network helping African and Caribbean owned businesses in the UK - <a href="#">read about the work and services of the ACBN on the ACBN website</a></li>
				<li>The <strong>National Black Women's Network</strong> (NBWN) offers consultation and support for black women in business - <a href="http://nbwn.org/" target="_blank" rel="noopener noreferrer">find out about business networking events near you on the NBWN website</a></li>
			</ul>
