			<h1>Extra help and support available for your start up</h1>
			<h2>Sources of support for young entrepreneurs</h2>
			<p>There are a number of schemes that offer advice and support if you are a young entrepreneur. Many of them can also help if you are finding it difficult to raise finance.</p>
			
			<h3>The Prince's Trust</h3>
			<p>The Prince's Trust can help you test your idea, write a business plan, access mentoring and may even offer you financial support if you are struggling to raise money for your business.</p>
			<p>The Prince's Trust offers:</p>
			<ul>
				<li>employment advice</li>
				<li>business skills training</li>
				<li>business planning support</li>
				<li>start up funding</li>
				<li>ongoing support from a mentor</li>
			</ul>
			
			<p>You can <a href="https://www.princes-trust.org.uk/help-for-young-people/get-in-touch" target="_blank" rel="noopener noreferrer">register your interest on the Prince's Trust website</a>.</p>
			
			<h3>Shell LiveWIRE</h3>
			<p>The Shell LiveWIRE website offers discussion forums where young entrepreneurs can share their experiences, and offer solutions to business problems.</p>
			<p>Shell LiveWIRE is aimed specifically at young entrepreneurs aged between 16 and 30. It allows you to utilise a pool of experts with specialised knowledge. <a href="https://www.shell-livewire.org/" target="_blank" rel="noopener noreferrer">Find help and advice from the Shell LiveWIRE community on the Shell LiveWIRE website (registration required)</a>.</p>
			<p>Shell LiveWIRE Grand Ideas Awards offer up to five &pound;1,000 prizes a month to the most innovative and unusual ideas submitted by young entrepreneurs aged 16-30 with new businesses in their first 12 months of trading.</p>
			<p><a href="https://www.shell-livewire.org/listing/awards" target="_blank" rel="noopener noreferrer">Find out about criteria and how to apply for the Grand Idea Awards on the Shell LiveWIRE website</a>.</p>
			
			<h3>Young Enterprise</h3>
			<p>Young Enterprise has several programmes aimed at giving young people an understanding of how business works through running their own businesses. <a href="https://www.young-enterprise.org.uk/what-we-do/our-programmes-new/" target="_blank" rel="noopener noreferrer">Find details of Young Enterprise programmes on the Young Enterprise website</a>.</p>
			
			<h3>Working tax credits</h3>
			<p>If you are not earning very much in the first months of your business, you may also be entitled to Working Tax Credits. These are payments to top up the earnings of working people on low incomes, including the self-employed. For more information, see the page in this guide on <a href="/starting-up/further-support-for-your-business/extra-help-and-support-available-for-your-start-up/support-for-entrepreneurs-on-low-incomes.php">support for entrepreneurs on low incomes</a>.</p>
			<p><a href="https://www.gov.uk/tax-credits-calculator" target="_blank" rel="noopener noreferrer">Find out about Tax Credits on the HM Revenue &amp; Customs (HMRC) website</a>.</p>
			<p>You can also find out about Working Tax Credits by calling the HMRC Tax Credits Helpline on Tel 0845 300 3900.</p>
			
			<h3>Graduate entrepreneurs</h3>
			<p>If you're a graduate or student, you can <a href="https://web.archive.org/web/20120910070139/http://www.ncee.org.uk/students_and_graduates" target="_blank" rel="noopener noreferrer">access advice and support on the National Council for Entrepreneurship in Education (NCEE) website</a>.</p>
			
