			<h1>Extra help and support available for your start up</h1>
			<h2>Mentoring, networking, finance and training</h2>
			<p>Mentoring and business networking can allow you to benefit from the knowledge of other industry professionals.</p>
			<p>A business mentor is someone who has experience of running a business and can offer guidance, support and advice to a new entrepreneur.</p>
			<p>Business networking is an opportunity for you to meet with other business people, make new contacts, get new leads and find ways that you may be able to help each other.</p>
			<p>There are many networking events available through several different organisations, including trade associations and Chambers of Commerce.</p>
			<p><a href="http://www.britishchambers.org.uk/find-your-chamber/" target="_blank" rel="noopener noreferrer">Find your local Chamber of Commerce on the British Chambers of Commerce website</a>.</p>
			<p><a href="http://www.taforum.org/Members" target="_blank" rel="noopener noreferrer">Search for your local trade association on the Trade Association Forum website</a>.</p>
			<p>Networking is also increasingly available online, through groups set up on social networking websites or through specialist websites, such as LinkedIn.</p>
			
			<h3>Mentorsme.co.uk</h3>
			<p>Mentorsme.co.uk is the new government-supported national mentoring network. Many of the UK's major business mentoring organisations are represented here, and there are details of more than 10,000 business mentors for you to search for - many of whom provide their services free of charge.</p>
			<p>You can <a href="http://www.mentorsme.co.uk/" target="_blank" rel="noopener noreferrer">find business mentors on the Mentorsme.co.uk website</a>.</p>
			
			<h3>Financial support and ongoing training</h3>
			<p>The government can also provide financial support for your business in a number of ways.</p>
			<p>You may be able to obtain finance from <strong>Community Development Finance Institutions</strong> (CDFIs). These organisations provide loans and support to businesses and individuals who have had trouble getting finance from sources such as banks and building societies. <a href="http://www.findingfinance.org.uk/" target="_blank" rel="noopener noreferrer">Read about sourcing finance from CDFIs on the Finding Finance website</a>.</p>
			<p>The <strong>Enterprise Finance Guarantee</strong> (EFG) aims to help smaller credit-worthy businesses that are unable to obtain a conventional loan because of the current tight lending conditions. <a href="http://webarchive.nationalarchives.gov.uk/20120419053132/http://www.bis.gov.uk/policies/enterprise-and-business-support/access-to-finance/enterprise-finance-guarantee" target="_blank" rel="noopener noreferrer">Find out about the EFG on the Department for Business, Innovation &amp; Skills (BIS) website</a>.</p>
			<p>If you need to improve your business skills before or after starting up a business, you can apply for a Professional and Career Development Loan. This is a deferred repayment bank loan aimed at financing the training and qualifications that will help individuals further their career or business.</p>
			<p><a href="https://www.gov.uk/career-development-loans" target="_blank" rel="noopener noreferrer">Find out about Professional and Career Development Loans on the Directgov website</a>.</p>
			<p><a href="http://webarchive.nationalarchives.gov.uk/20120418175516/http://improve.businesslink.gov.uk/resources/business-support-finder" target="_blank" rel="noopener noreferrer">Search our business support finder for grants, loans, expertise and advice for which your business may be eligible</a>.</p>
			
