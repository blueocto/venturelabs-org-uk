			<h1>Extra help and support available for your start up</h1>
			<h2>Support for entrepreneurs on low incomes</h2>
			<p>If you receive benefits and want to set up a new business, it is worth discussing your business plans with a Jobcentre Plus adviser. They will be able to tell you how starting a business will affect your benefits and point you in the direction of practical support. You can also ask your adviser about the self-employed support package provided by Jobcentre Plus. This package is aimed at people who have been unemployed and claiming Jobseeker's Allowance for 13 weeks or longer.</p>
			<p>You may also be entitled to Working Tax Credits. These are payments to top up the earnings of working people on low incomes, including the self-employed. You can find out about Working Tax Credits by calling the HMRC Tax Credits Helpline on Tel 0845 300 3900.</p>
			<p>You can also get business support from many charitable organisations, including the Fredericks Foundation. <a href="http://www.fredericksfoundation.org/" target="_blank" rel="noopener noreferrer">Find information on loans available for new start-ups on the Fredericks Foundation website</a>.</p>
			<p>You can also <a href="https://www.gov.uk/moving-from-benefits-to-work/starting-your-own-business" target="_blank" rel="noopener noreferrer">find out about the Jobcentre Plus self-employed support package on the Directgov website</a>.</p>
			
