			<h1>The benefits of business mentoring</h1>
			<h2>Why business mentoring is important</h2>
			<p>If you have a gap in your knowledge or experience, then mentoring could work very well for you. For example, you may have a great idea for a business but need a bit of guidance to turn it into a successful venture.</p>
			<p>A mentor can provide you with a number of benefits, such as:</p>
			<ul>
				<li>guidance on developing and improving your business</li>
				<li>help with difficult decisions</li>
				<li>ideas for new products or services, or ways of working</li>
				<li>tips for your business that are gained from practical experience</li>
				<li>access to a network of contacts with other people</li>
			</ul>

			<p>A business mentor can also help you to:</p>
			<ul>
				<li>develop key business skills</li>
				<li>improve your problem solving abilities</li>
				<li>build confidence</li>
				<li>work on your personal development</li>
			</ul>

			<p>However, a business mentor won't sort out all your problems for you, tell you what to do or offer business advice. They don't act as consultants or take the place of any existing professional advisers.</p>
			
