			<h1>The benefits of business mentoring</h1>
			<h2>Online business mentoring</h2>
			<p>Online business mentoring or 'e-mentoring' is an interactive, remote relationship between a mentor and mentee. Some mentoring relationships are exclusively online, while others can involve a mixture of face-to-face and online contact.</p>
			<p>This type of mentoring is popular with people who prefer a more informal mentoring relationship. It also works well when it's difficult for mentors and mentees to meet face to face, eg for geographical reasons. Websites that provide this sort of mentoring often call it networking and forum advice.</p>
			
			<h3>How online business mentoring works</h3>
			<p>Business mentors use online forums and websites - similar to social networking sites - where they share their knowledge and experience. The mentor applies to register as a mentor, is vetted by the website and then has their profile viewed by potential mentees. The mentee is then free to contact the mentor. There is no reason why once this relationship is established it can't turn into a more formal mentoring relationship.</p>
			<p>Many websites will let you search by topic or industry sector to find a mentor. You can also post a request and invite mentors to respond, so that you can identify those who are best placed to help you.</p>
			
			<h3>Sources of online business mentoring</h3>
			<p>The new national business mentoring network - Mentorsme.co.uk - is a good source of experienced mentors who can offer online or face-to-face support. It includes many mentors with a financial background. This new service is backed by the Department for Business, Innovation and Skills.</p>
			<p><a href="http://www.mentorsme.co.uk/" target="_blank" rel="noopener noreferrer">Find business mentors on the Mentorsme.co.uk website</a>.</p>
			<p>There are many websites offering business mentoring or networking services targeted at a specific sector, such as Freelance Advisor and Enterprise Nation. Young entrepreneurs can use the Shell LiveWIRE service, which is aimed at people aged between 16 and 30 who are starting their own business.</p>
