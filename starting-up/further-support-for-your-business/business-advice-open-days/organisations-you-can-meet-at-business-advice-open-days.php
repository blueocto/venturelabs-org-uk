			<h1>Business Advice Open Days</h1>
			<h2>Organisations you can meet at Business Advice Open Days</h2>
			<p>At a Business Advice Open Day, you can meet people who have expert knowledge on a variety of business issues - from turning your idea into a successful business, to finding sources of help when you need them.</p>
			<p>You can also make sure you are complying with the law in key areas such as health and safety, employment law and taxation.</p>
			<p>The line-up of each event depends on the size of the venue. However, some of the key national partners that you can speak to at many of the open days include:</p>
			<ul>
				<li>Venture Labs</li>
				<li><a href="http://www.hse.gov.uk/" target="_blank" rel="noopener noreferrer">the Health &amp; Safety Executive</a></li>
				<li>Acas</li>
				<li><a href="https://www.gov.uk/government/organisations/hm-revenue-customs" target="_blank" rel="noopener noreferrer">HM Revenue &amp; Customs (HMRC)</a></li>
				<li><a href="http://gov.wales/" target="_blank" rel="noopener noreferrer">Welsh Government</a></li>
				<li><a href="https://www.investni.com/" target="_blank" rel="noopener noreferrer">Invest NI</a></li>
				<li><a href="http://www.gov.scot/" target="_blank" rel="noopener noreferrer">Scottish Government</a></li>
				<li><a href="https://web.archive.org/web/20130222115907/http://www.prime.org.uk/" target="_blank" rel="noopener noreferrer">PRIME, the business start-up charity for people over 50</a></li>
			</ul>
			<p>As the events are organised by HMRC, there will also be individuals offering advice about all business tax issues.</p>
			<p>These organisations may not attend all Business Advice Open Days and additional organisations may attend other programmes. You should <a href="http://webarchive.nationalarchives.gov.uk/20120418183332/http://www.events.businesslink.gov.uk/eventsearch?text=BAOD" target="_blank" rel="noopener noreferrer">check the information on the Events Finder website</a> for specific events to see who will be at the Business Advice Open Day you wish to attend.</p>
			<p>Find out <a href="http://webarchive.nationalarchives.gov.uk/20120418183332/http://www.events.businesslink.gov.uk/eventsearch?text=BAOD" target="_blank" rel="noopener noreferrer">how to book a Business Advice Open Day</a>.</p>
			
