			<h1>Business Advice Open Days</h1>
			<h2>Who should attend a Business Advice Open Day and why</h2>
			<p>The Business Advice Open Day events are open to all businesses - whether you are a sole proprietor, self employed, partnership or company - or thinking of starting a business in the near future. Thousands of businesses have already benefited from previous Open Days and last year over 25,000 seminar places were booked.</p>
			<h3>What do businesses and start-ups get out of it?</h3>
			<p>Whatever the nature of your business, a Business Advice Open Day will give you:</p>
			<ul>
				<li>access to seminars covering a wide range of topics useful for businesses and start-ups - note that these will vary from event to event</li>
				<li>practical, face to face advice on various matters including tax and how to avoid penalties</li>
				<li>help with online services from HM Revenue &amp; Customs (HMRC)</li>
				<li>advice on various matters including tax</li>
				<li>business advice from government organisations</li>
				<li>advice from other partner organisations</li>
			</ul>

			<p>You can find out how to register your activities with HMRC, manage the changes needed as your business grows - such as taking on staff - and know where to go for further help and support in the future.</p>
			<p>All events are free and there are no commercial exhibitors - so you will only obtain advice, help and support from government-backed organisations.</p>
			<p>Find out <a href="http://webarchive.nationalarchives.gov.uk/20111206100906/http://webarchive.nationalarchives.gov.uk/20111206100906/http://www.events.businesslink.gov.uk/eventsearch?text=BAOD" target="_blank" rel="noopener noreferrer">how to book a Business Advice Open Day with HMRC</a>.</p>
			
