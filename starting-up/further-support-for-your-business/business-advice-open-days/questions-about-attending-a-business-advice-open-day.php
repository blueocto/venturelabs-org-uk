			<h1>Business Advice Open Days</h1>
			<h2>Questions about attending a Business Advice Open Day</h2>
			<p>Before you attend a Business Advice Open Day, there may be some things you want to know about. Below are some common questions about the events.</p>
			
			<h3>Do I have to stay for the whole day?</h3>
			<p>No - you can drop in for as long as you wish. Choose only the sessions that are relevant to your business and make the most of your time.</p>
			
			<h3>Is there free parking at all the events?</h3>
			<p>Free parking cannot be guaranteed at every event but most venues offer some free parking on a first come, first served basis. If you are a Blue Badge holder please notify the Business Liaison Team and they will liaise with the venue on your behalf to identify the closest suitable facilities.</p>
			<p>Find Business Liaison Team contact details on the page in this guide on <a href="/starting-up/further-support-for-your-business/business-advice-open-days/how-to-contact-the-event-organisers.php">how to contact the event organisers</a>.</p>
			<p>The Business Liaison Team tries to identify other parking sites near to the venue if facilities at the venue are limited. You can also find details of these in the appropriate flyer.</p>
			
			<h3>Are events accessible to all? </h3>
			<p>Wherever possible, the open days use a venue with disabled access. Let the event organisers know in advance of any physical or communication difficulties, so that they can arrange to help you on the day.</p>
			
			<h3>Getting directions for an event</h3>
			<p>You can find directions to any Business Advice Open Day event online. Simply click on the event you are interested in, and click on 'directions'.</p>
			
			<h3>Is there catering at the event?</h3>
			<p>Light refreshments are provided at most venues, where the facilities allow. These will be on a cash basis and are likely to be tea, coffee biscuits etc.</p>
			
			<h3>Do I need to book in advance?</h3>
			<p>No - you can drop in on the day but it helps the organisers manage numbers if you can register in advance.</p>
			
			<h3>Need further help?</h3>
			<p>If you have any other specific needs regarding an event you plan to attend, please let the Business Liaison Team know in advance. Find Business Liaison Team contact details on the page in this guide on <a href="/starting-up/further-support-for-your-business/business-advice-open-days/how-to-contact-the-event-organisers.php">how to contact the event organisers</a>.</p>
			
