		<h3>VAT Helpline</h3>
		<p>If you cannot find the answer to your question on the HM Revenue &amp; Customs (HMRC) website, the easiest way is to ring the VAT Helpline where you can get most of your VAT questions answered.</p>
		<p>Before you ring, make sure you have your VAT registration number and postcode to hand. If you're not VAT-registered, you'll need your postcode - it's necessary so HMRC can keep a record of your call.</p>
		<p>It's not always possible to answer your question straightaway over the telephone because the helpline adviser may need to research the answer. If this happens, they'll take your details and call you back as soon as possible. The helpline adviser will tell you how long it will take.</p>
		<p>In some cases, HMRC might need you to write in, especially if they need to look at your paperwork. The helpline adviser will tell you if this needs to happen and where you need to write to.</p>
		<p><a href="https://www.gov.uk/government/organisations/hm-revenue-customs/contact/vat-enquiries" title="Phone the VAT Helpline, or send your VAT question by email or post" target="_blank" rel="noopener noreferrer">Find contact details for the VAT Helpline on the HMRC website</a></p>
		<h3>Business Advice Open Days</h3>
