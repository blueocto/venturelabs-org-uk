		<h1>Introduction to VAT</h1>
		<h2>What is VAT?</h2>
		<p>VAT is a tax that's charged on most business transactions in the UK. Businesses add VAT to the price they charge when they provide goods and services to:</p>
		<ul>
			<li>business customers - for example, a clothing manufacturer adds VAT to the prices they charge a clothes shop</li>
			<li>non-business customers - members of the public or 'consumers' - for example, a hairdressing salon includes VAT in the prices they charge members of the public</li>
		</ul>

		<p>If you're a VAT-registered business, in most cases you:</p>
		<ul>
			<li>charge VAT on the goods and services you provide</li>
			<li>reclaim the VAT you pay when you buy goods and services for your business</li>
		</ul>

		<p>If you are not VAT-registered then you cannot reclaim the VAT you pay when you purchase goods and services.</p>
		
