		<h1>Introduction to VAT</h1>
		<h2>The difference between exempt and zero-rated</h2>
		<p>If you sell zero-rated goods or services, they count as taxable supplies, but you don't add any VAT to your selling price because the VAT rate is 0 per cent.</p>
		<p>If you sell goods or services that are exempt, you don't charge any VAT and they're not taxable supplies. This means that you won't normally be able to reclaim any of the VAT on your expenses.</p>
		<p>Generally, you can't register for VAT or reclaim the VAT on your purchases if you sell <strong>only</strong> exempt goods or services. If you sell <strong>some</strong> exempt goods or services you may not be able to reclaim the VAT on all of your purchases.</p>
		<p>If you buy and sell only - or mainly - zero-rated goods or services you can apply to HM Revenue &amp; Customs to be exempt from registering for VAT. This could make sense if you pay little or no VAT on your purchases. You can find out how to apply for exemption from registration, get details of exempt and partly exempt goods and services and check the rates of VAT and how they apply to different goods and services in our guides below.</p>
