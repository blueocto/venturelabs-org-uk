		<h1>Introduction to VAT</h1>
		<h2>VAT glossary</h2>
		<p>These are some plain English definitions of common VAT terms that HM Revenue &amp; Customs (HMRC) uses:</p>
		<ul>
			<li><strong>accounting period:</strong> see tax period</li>
			<li><strong>acquisitions:</strong> goods brought into the UK from other EU countries - (goods brought into the UK from outside of the EU are known as Imports)</li>
			<li><strong>corporate body:</strong> an incorporated body such as a limited company, limited liability partnership, friendly, industrial or provident society</li>
			<li><strong>distance sales:</strong> where a business in one EU country sells and ships goods directly to consumers in another EU country, eg internet or mail-order sales</li>
			<li><strong>exports:</strong> goods sent to a non-EU country</li>
			<li><strong>despatches:</strong> goods sent to another EU country</li>
			<li><strong>imports:</strong> goods brought into the EU from another country</li>
			<li><strong>input tax:</strong> the VAT you pay on your purchases</li>
			<li><strong>output tax:</strong> the VAT you charge on your sales</li>
			<li><strong>place of supply:</strong> the country in which a supply of goods or services must be accounted for VAT purposes</li>
			<li><strong>self-billing:</strong> your customer issues your VAT invoice and sends a copy to you with their payment</li>
			<li><strong>supply:</strong> selling or otherwise providing goods or services, including barter and some free provision</li>
			<li><strong>supply of goods:</strong> when exclusive ownership of goods passes from one person to another</li>
			<li><strong>taxable person:</strong> any business entity that buys or sells goods or services and is required to be registered for VAT - this includes individuals, partnerships, companies, clubs, associations and charities</li>
			<li><strong>taxable supplies:</strong> all goods and services sold or otherwise supplied by a taxable person which are liable to VAT at the standard, reduced or zero rate</li>
			<li><strong>taxable turnover:</strong> the total value - excluding VAT - of the taxable supplies you make in the UK (excludes capital items like buildings, equipment, vehicles or exempt supplies)</li>
			<li><strong>tax period:</strong> the period of time covered by your VAT return, usually quarterly</li>
			<li><strong>tax point:</strong> the date when VAT has to be accounted for - for goods, this is usually when you send the goods to a customer or when they take them away, for services, this is usually when the service is performed</li>
			<li><strong>time of supply:</strong> see tax point</li>
		</ul>

