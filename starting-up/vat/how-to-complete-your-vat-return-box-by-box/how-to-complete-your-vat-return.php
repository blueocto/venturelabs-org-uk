		<h1>How to complete your VAT Return box by box</h1>
		<h2>How to complete your VAT Return</h2>
		<h3>Online returns</h3>
		<p>You must fill in every box on your return - don't leave any box blank - but you don't have to fill in box 3 or box 5 of the online return as these are completed automatically for you based on the information you put in boxes 1, 2 and 4. If you need to fill in negative amounts and 'not applicable' boxes when submitting your return online you need to:</p>
		<ul>
			<li>put a minus sign before the figure for negative amounts</li>
			<li>enter '0.00' for 'none' or 'not applicable'</li>
		</ul>

		<p><a href="/tax-payroll-and-company-information/vat/how-to-submit-your-vat-return-online.php">How to submit your VAT Return online</a></p>

		<h3>Paper returns</h3>
		<p>For accounting periods beginning on or after 1 April 2012, you can only submit a paper return if you have been advised that you are exempt from online filing. If you're filling in a paper return you need to fill in every box and:</p>
		<ul>
			<li>put the figure in brackets ( ) for negative amounts</li>
			<li>write 'NONE' for 'none' or 'not applicable'</li>
		</ul>

		<p>When filling in your return, you should read the notes on the return, and there's detailed guidance in VAT Notice 700/12 if you need more information.</p>
		<p>Please send your completed return to:</p>
		<p>HM Revenue &amp; Customs
			<br /> VAT Controller
			<br /> VAT Central Unit
			<br /> BX5 5AT</p>
		<p><a href="http://webarchive.nationalarchives.gov.uk/20111206102847/http://customs.hmrc.gov.uk/channelsPortalWebApp/channelsPortalWebApp.portal?_nfpb=true&_pageLabel=pageVAT_ShowContent&id=HMCE_CL_000070&propertyType=document" target="_blank" rel="noopener noreferrer">Find out about filling in your VAT Return in VAT Notice 700/12 on the HM Revenue &amp; Customs (HMRC) website</a></p>
		
		<h3>Flat Rate Scheme</h3>
		<p>The way you fill out your VAT Return is different if you are on the Flat Rate Scheme.</p>
