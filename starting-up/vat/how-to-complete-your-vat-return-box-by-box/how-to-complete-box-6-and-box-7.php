		<h1>How to complete your VAT Return box by box</h1>
		<h2>How to complete Box 6 and Box 7</h2>
		<h3>Box 6: your total sales excluding VAT</h3>
		<p>Enter the total figure for your sales (excluding VAT) for the period, that is, the sales on which you charged the VAT you put in Box 1. Additionally, you should include;</p>
		<ul>
			<li>any zero-rated and exempt sales or other supplies you made</li>
			<li>any amount you put in Box 8</li>
			<li>goods or services you have <strong>supplied</strong> that are subject to the reverse charge</li>
			<li>gold or services that you have <strong>purchased</strong> that are subject to the reverse charge</li>
			<li>exports outside the EU</li>
		</ul>

		<p>Take off the net amount of any credit notes you issued or debit notes you received.</p>
		<p>Don't include:</p>
		<ul>
			<li>loans, dividends and gifts of money</li>
			<li>insurance claims</li>
		</ul>
		
		<p><a href="/tax-payroll-and-company-information/vat/vat-rates-thresholds-fuel-scales-and-exchange-rates/vat-rates-explained-standard-reduced-zero-exempt.php">VAT rates explained: standard, reduced, zero, exempt</a></p>
		
		<h3>Reverse Charge Sales List</h3>
		<p>If you make reverse charge sales - sales to which a reverse charge is applied - you must notify HM Revenue &amp; Customs (HMRC) and send in regular Reverse Charge Sales Lists (RCSL).</p>
		<p><a href="/tax-payroll-and-company-information/vat/international-trade-international-visits-and-vat/reverse-charge-sales-list-rcsl.php">Reverse Charge Sales List (RCSL)</a></p>
		
		<h3>Box 7: your total purchases excluding VAT</h3>
		<p>Enter the total figure for your purchases (excluding VAT) for the period, including:</p>
		<ul>
			<li>the purchases on which you paid the VAT you put in Box 4</li>
			<li>anything you bought that the reverse charge procedure applies to</li>
			<li>any amount you put in Box 9</li>
		</ul>

		<p>Don't include:</p>
		<ul>
			<li>expenses like salaries and taxes</li>
			<li>anything outside the scope of VAT like vehicle licences, MOT certificates and local authority rates</li>
		</ul>

