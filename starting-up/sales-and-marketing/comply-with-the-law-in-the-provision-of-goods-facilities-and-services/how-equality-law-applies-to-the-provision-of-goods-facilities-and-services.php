		<h1>Comply with the law in the provision of goods, facilities and services</h1>
		
		<h2>How equality law applies to the provision of goods, facilities and services</h2>
		<p>Under the Equality Act 2010 people who access your goods, facilities and services are protected from discrimination on the basis of a 'protected characteristic'.</p>
		<p>The relevant protected characteristics are:</p>
		<ul>
			<li>sex</li>
			<li>disability</li>
			<li>religion or belief</li>
			<li>sexual orientation</li>
			<li>gender reassignment</li>
			<li>pregnancy and maternity</li>
			<li>race, which includes ethnic or national origins, colour and nationality</li>
		</ul>

		<p>Age is not currently covered.</p>
		<h3>Direct discrimination</h3>
		<p>This occurs when a person is treated less favourably than another person because of a protected characteristic. For example, if you refuse to serve a customer because of their sexual orientation.</p>
		<p>Direct discrimination can also take place if you discriminate against someone because:</p>
		<ul>
			<li>they associate with someone who has a particular protected characteristic</li>
			<li>you (wrongly) perceive that person to have - or deliberately treat them as if they have - a particular protected characteristic</li>
		</ul>
		
		<h3>Indirect discrimination</h3>
		<p>This occurs when there is a rule, policy or practice that applies generally but which particularly disadvantages people who share a particular protected characteristic.</p>
		<p>For example, a 'no dogs' policy in a hotel would indirectly discriminate against those who need a guide dog to help them access premises.</p>
		<p>However, such a policy or practice could be justified if it can be shown that its application is a proportionate way of achieving a legitimate aim.</p>
		
		<h3>Discrimination arising from disability</h3>
		<p>This occurs when a disabled person is treated unfavourably because of something connected with their disability and this unfavourable treatment cannot be justified. For example, you exclude a person with a mobility impairment from your premises because they use a wheelchair.</p>
		<p>You can justify this unfavourable treatment if you can show that it is intended to meet a legitimate objective in a fair, balanced and reasonable way.</p>
		<p>This type of discrimination cannot occur if you do not know, or could not reasonably be expected to know, that the person is disabled.</p>
		
		<h3>Harassment</h3>
		<p>This can occur when a service provider engages in unwanted conduct which is related to a relevant protected characteristic and which has the purpose or the effect of:</p>
		<ul>
			<li>violating the customer's dignity</li>
			<li>creating an intimidating, hostile, degrading, humiliating or offensive environment</li>
		</ul>
		
		<p>Harassment because of a person's pregnancy and maternity, religion and belief or sexual orientation are not protected directly under the harassment provisions but pregnancy and maternity harassment would amount to harassment related to sex. Also, where unwanted conduct related to sexual orientation or religion or belief results in a person suffering a detriment, that person could bring a claim of direct discrimination.</p>
		
		<h3>Victimisation</h3>
		<p>This occurs when a service provider treats a person badly because they have made or supported a complaint about discrimination or harassment, or because the service provider thinks that a person is doing or may do these things.</p>
		<p>A person is not protected from victimisation if they have maliciously made or supported an untrue complaint.</p>
		
		<h3>Reasonable adjustments</h3>
		<p>You are required to make changes, where needed, to improve service for disabled customers or potential customers. Reasonable changes are required wherever disabled customers or potential customers would otherwise be at a substantial disadvantage compared with non-disabled people. Service providers cannot charge disabled customers for reasonable adjustments.</p>
		
		<h3>Breastfeeding mothers</h3>
		<p>The law makes it clear that you must not discriminate against women outside work just because they are breastfeeding, regardless of the age of the child.</p>
