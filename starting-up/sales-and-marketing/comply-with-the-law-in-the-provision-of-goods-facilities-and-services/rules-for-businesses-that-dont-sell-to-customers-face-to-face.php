		<h1>Comply with the law in the provision of goods, facilities and services</h1>
		
		<h2>Rules for businesses that don't sell to customers face to face</h2>
		<p>If you sell to consumers - as opposed to other traders - without seeing them face-to-face, there are additional distance selling rules that you must stick to.</p>
		<p>When communicating with your customers, you must clearly inform them about:</p>
		<ul>
			<li>your business name</li>
			<li>its place of registration, registered number and registered office address</li>
			<li>your postal address (when advance payment is required)</li>
			<li>the goods or services (including a description)</li>
			<li>prices (including taxes)</li>
			<li>payment arrangements</li>
			<li>arrangements for delivery for goods or performance in the case of services</li>
			<li>the right to cancel during the 'cooling-off' period</li>
			<li>who is responsible for postage/delivery of returned goods</li>
			<li>how long prices are valid</li>
			<li>minimum duration of contracts</li>
		</ul>
		
		<p>Once an order has been placed you must send confirmation to the customer by post, email or fax. You must also make it clear if you intend to substitute unavailable goods with similar items.</p>
		<p>You must provide key information, such as pricing and the right to cancel, before the order is placed. You must also confirm all the required information in writing, at the latest when the goods are delivered or before the service you are providing comes to an end.</p>
		<p>The customer can cancel the agreement up to seven days after the goods are delivered or the service contract is agreed. Special rules apply if the written information has not been provided, or if the customer agrees to the service starting before this cooling-off period is over.</p>
		<p>Delivery of the goods or the beginning of the performance of a service must be within 30 days from the day after the consumer sent the order to you, unless otherwise agreed. The customer must be informed if you are unable to meet this deadline - they are not obliged to agree to a revised date and can cancel the contract. Any money paid must be refunded within 30 days of cancellation of the contract.</p>
		<p>Customers who enter contracts lasting more than a year must be given details of how the agreement can be terminated.</p>
