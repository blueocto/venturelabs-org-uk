		<h1>Prepare a business plan</h1>
		<h2>Using the business plan template</h2>
		<p>The business plan document is an interactive form designed to simplify the process of creating your business plan.</p>
		<p>The document is a Microsoft Word template and is also available to download as an OpenDocument file in ODT format.</p>
		<p>Bullet points have been used in the document to help you structure your thoughts into clear and concise messages. The fields will expand as you type into them, so there is plenty of space for everything you need to say.</p>
		<p>The template should be read in conjunction with the guide 'Prepare a business plan'. It is recommended that you print off both the guide and the template and read both before completing the template.</p>
		<p>The template asks a number of detailed questions about the proposed business - its markets, costs, etc. They may demand research prior to completing it. The time spend is an investment which will pay dividends when you get the business started as well as helping to convince potential finance providers that you are in control of the business.</p>
		<p>Here are the step-by-step instructions for using the business plan template:</p>
		<ol>
			<li>To start work on your business plan, download our business plan template below to create a new business plan document for you to complete.</li>
			<li>Save the document - give it a memorable name and store it in a suitable and secure location. Remember to also save the document regularly as you work on it.</li>
			<li>The template uses protected forms. The cursor will only point to and allow changes to be made in the 'field' boxes. You cannot make changes to any other part of the document.</li>
			<li>The best way to move through the document is to use the cursor keys. To navigate using the mouse pointer, simply click into each field. The best place to click will normally be near the left margin of the field. If the cursor jumps to the cell below, you can use the cursor keys to navigate back to the field you want to complete.</li>
			<li>You can change the headings of any of the sections to suit your needs. If you do this, the name will also need to be changed within the contents page at the start of the document.</li>
			<li>This document has a number of tables that are used to simplify the calculations you'll need to do when you start a business. To start entering data into a table, double-click on the table. This will activate it and allow you to click into individual cells. Financial and descriptive information can then be entered in the cells provided. Each table contains an automated formula that will do the maths on your behalf, calculating the summaries and totals you need to understand your business model.</li>
			<li>Once you have completed all the information, click anywhere out of the table to return to the rest of the document. If you have accidentally moved around the Excel document, you may see that the wrong bit of the table is now showing on the screen and will therefore be printed. To avoid this, always make sure that the appropriate bit of the table can be seen before clicking out.</li>
		</ol>
		<p>You can close or print the document at any time in the normal way. To re-open it, open Microsoft Word, click file then open and navigate to its location in the normal way.</p>
		<p>This template is intended for business planning purposes only. If the financial tables are to be used for any other purpose other than cashflow management, then we strongly recommend that you consult an accountant or tax advisor.</p>
		<p>Please note that this business plan template may not necessarily follow the same format as one a bank may ask you to complete, nevertheless much of the content required will be similar.</p>
