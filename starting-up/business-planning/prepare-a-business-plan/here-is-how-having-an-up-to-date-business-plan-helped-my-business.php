		<h2>Here's how having an up-to-date business plan helped my business</h2>
		<p>Darren Jones launched his care business, AKC Home Support Services, in 1991 with his wife Sharron. Although writing their business plan was one of the first things the couple did, Darren admits he originally saw it as a bit of a chore. Now, he takes a different view, believing it has helped the business stay on track and true to its goals.</p>
		<p><strong>What I did</strong></p>
		<h3>Write the plan</h3>
		<p>"When we started the firm I knew we needed a business plan but saw it more as a document for everyone else than something to help us. If I started another business tomorrow I would write one much more willingly as it brings a number of benefits - from helping you secure finance to keeping you focused on your goals.</p>
		<p>"We got help from our local enterprise centre, looked at examples from other businesses and a template from the bank. We mixed and matched bits from these sources because not everything applied to us. For example, because we were going into a new market we couldn't write about our competitors but needed a lot of information about the market for care services."</p>
		<h3>Consult the plan</h3>
		<p>"We used our business plan to set out the financial and strategic goals we wanted to achieve in the short and long-term. We review it annually now unless there's a significant shift in our market and then we use it to immediately re-evaluate our goals.</p>
		<p>"Our business plan has also helped us to avoid expanding too quickly. Early on, we were offered work in another county. This seemed great but when we looked at our business plan - and particularly our cashflow forecasts - we realised it was important to establish a firm base in one county before taking on work in another otherwise we would overstretch ourselves."</p>
		<h3>Use the plan</h3>
		<p>"We purchased a residential unit four years ago and our business plan definitely helped us demonstrate why the bank should lend us the money. Without it being put down on paper I don't think it would have sounded like a very viable suggestion.</p>
		<p>"The home added a different dimension to the business in which we had no trading record so the bank lent us the money according to our past performance. We could also show that we would offset some of the cost by using part of the new building as office space.</p>
		<p>"Our plan also helped us to get support from Shell LiveWire - the organisation that assists 16-30 year olds to start and develop businesses - as you must have a business plan to enter its competitions. We were awarded prizes twice - not only bringing in extra money but publicity too."</p>
		<p><strong>What I'd do differently</strong></p>
		<h3>Work on the plan's presentation</h3>
		<p>"I would have tried to get more assistance and perhaps made the document look a bit more professional. It's your way of gaining support for your business and is the one thing that your bank manager will remember apart from how you were dressed."</p>
		<h3>Get as much help and advice as possible</h3>
		<p>"Show the plan to an independent third party - such as friends or family who have run their own businesses - who will be able to point out if anything is missing. It's much better to make mistakes on a practice run than when it really matters."</p>
