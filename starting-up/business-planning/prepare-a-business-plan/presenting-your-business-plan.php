		<h1>Prepare a business plan</h1>
		<h2>Presenting your business plan</h2>
		<p>To make sure your business plan has maximum impact, there are a number of points to observe.</p>
		<p>Keep the plan <strong>short</strong> - it's more likely to be read if it's a manageable length. Think about the <strong>presentation</strong> and keep it professional - even if you only intend to use the plan in-house. Remember, a well-presented plan will reinforce the positive impression you want to create of your business.</p>
		<h3>Tips for presenting your plan</h3>
		<ul>
			<li>Include a cover or binding and a contents page with page and section numbering.</li>
			<li>Start with the executive summary.</li>
			<li>Ensure it's legible - make sure the type is ten point or above.</li>
			<li>You may want to email it, so ensure you use email-friendly formatting.</li>
			<li>Even if it's for internal use only, write the plan as if it's intended for an external audience.</li>
			<li>Edit the plan carefully - get at least two people to read it and check that it makes sense.</li>
			<li>Show the plan to expert advisers - such as your accountant - and ask for feedback. Redraft sections they say are difficult to understand.</li>
			<li>Avoid jargon and put detailed information - such as market research data or balance sheets - in an appendix at the back.</li>
			<li>You may have detailed plans for specific areas of your business, such as a sales plan or a staff training plan, but it is best not to include these, though it is good practice to mention that they exist.</li>
		</ul>
		<p>While it is sensible to seek advice from external advisers, it is not a good idea to get them to write the plan for you. Investors and lenders need to have confidence that you personally understand your business plan and are committed to the vision for the business.</p>
		<p>Make sure your plan is <strong>realistic</strong>. Once you have prepared your plan, use it. If you update it regularly, it will help you keep track of your business' development. See our guides on <a href="/finance-and-grants/financial-planning-and-accounts/business-budgeting.php">budgeting and business planning</a> and how to <a href="/grow-your-business/setting-a-vision-for-growth/prepare-a-business-plan-for-growth.php">prepare a business plan for growth</a>.</p>
