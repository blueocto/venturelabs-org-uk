		<h1>Prepare a business plan</h1>
		<h2>Financial forecasts</h2>
		<p>As part of your plan you will need to provide a set of financial projections which translate what you have said about your business into numbers.</p>
		<p>You will need to look carefully at:</p>
		<ul>
			<li>how much capital you need if you are seeking external funding</li>
			<li>the security you can offer lenders</li>
			<li>how you plan to repay any borrowings</li>
			<li>sources of revenue and income</li>
		</ul>
		<p>You may also want to include your personal finances as part of the plan at this stage.</p>
		<h3>Financial planning</h3>
		<p>Your forecasts should run for the next three (or even five) years and their level of sophistication should reflect the sophistication of your business. However, the first 12 months' forecasts should have the most detail associated with them.</p>
		<p>Include the assumptions behind your projection with your figures, both in terms of costs and revenues so investors can clearly see the thinking behind the numbers.</p>
		<h3>What your forecasts should include</h3>
		<p><strong>Sales forecast</strong> - the amount of money you expect to raise from sales. See our guide on how to <a href="/sales-and-marketing/selling/forecast-and-plan-your-sales.php">forecast and plan your sales</a>.</p>
		<p><strong>Cashflow statements</strong> - your cash balance and monthly cashflow patterns for at least the first 12 to 18 months. The aim is to show that your business will have enough working capital to survive so make sure you have considered the key factors such as the timing of sales and salaries. See our guide on <a href="/finance-and-grants/manage-your-cashflow/cashflow-management-the-basics.php">cashflow management: the basics</a>.</p>
		<p><strong>Profit and loss forecast</strong> - a statement of the trading position of the business: the level of profit you expect to make, given your projected sales and the costs of providing goods and services and your overheads.</p>
		<p>Your forecasts should cover a range of scenarios. New businesses often forecast over-optimistic sales and most external readers will take this into account. It is sensible to include subsidiary forecasts based on sales being significantly slower than you are actually predicting, with one for sales starting three months later than expected, and another forecasting a 20 per cent lower level of sales.</p>
		<p>You can <a href="/_files/cashflow_profit-and-loss_sales-forecast.xls" target="_blank" rel="noopener noreferrer">download our sample profit and loss forecast template (XLS, 50K)</a>.</p>
		<h3>Risk analysis</h3>
		<p>Alongside your financial forecasts it is good practice to show that you have reviewed the risks your business could be faced with, and that you have looked at contingencies and insurance to cover these. Risks can include:</p>
		<ul>
			<li>competitor action</li>
			<li>commercial issues - sales, prices, deliveries</li>
			<li>operations - IT, technology or production failure</li>
			<li>staff - skills, availability and costs</li>
			<li>acts of God - fire or flood</li>
		</ul>
		<p>See our guide on <a href="/grow-your-business/managing-risk.php">managing risk</a>.</p>
		<p>See our videos and e-learning resources to <a href="https://www.gov.uk/business-finance-support-finder" title="How to forecast your finances and raise finance when starting up" target="_blank" rel="noopener noreferrer">think about your financial planning</a>.</p>
		
