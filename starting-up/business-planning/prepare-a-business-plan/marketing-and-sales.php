		<h1>Prepare a business plan</h1>
		<h2>Marketing and sales</h2>
		<p>This section should describe the specific activities you intend to use to promote and sell your products and services. It's often the weak link in business plans so it's worth spending time on it to make sure it's both realistic and achievable.</p>
		<p>A strong sales and marketing section means you have a clear idea of how you will get your products and services to market.</p>
		<p>Your plan will need to provide answers to these questions:</p>
		<ul>
			<li>How do you plan to <strong>position</strong> your product or service in the market place? For further information, see our guide on how to <a href="/sales-and-marketing/marketing/create-your-marketing-strategy.php">create your marketing strategy</a>.</li>
			<li>Who are your <strong>customers</strong>? Include details of customers who have shown an interest in your product or service and explain how you plan to go about attracting new customers. See our guide: <a href="/sales-and-marketing/keeping-your-customers/know-your-customers-needs.php">know your customers' needs</a>.</li>
			<li>What is your <strong>pricing</strong> policy? How much will you charge for different customer segments, quantities, etc? See our guide on how to <a href="/sales-and-marketing/pricing/price-your-product-or-service.php">price your product or service</a>.</li>
			<li>How will you <strong>promote</strong> your product or service? Identify your sales process methods, eg direct marketing, advertising, PR, email, e-sales, social marketing. See our guide on sales &amp; marketing: the basics.</li>
			<li>How will you <strong>reach</strong> your customers? What channels will you use? Which partners will be needed in your distribution channels? See our guide on how to <a href="/sales-and-marketing/keeping-your-customers/reach-your-customers-effectively.php">reach your customers effectively</a>.</li>
			<li>How will you do your <strong>selling</strong>? Do you have a sales plan? Have you considered which sales method will be the most effective and most appropriate for your market, such as selling by phone, over the internet, face-to-face or through retail outlets? Are your proposed sales methods consistent with your marketing plan? And do you have the right skills to secure the sales you need? See our guide on <a href="/sales-and-marketing/selling/the-sales-process.php">the sales process</a>.</li>
		</ul>
