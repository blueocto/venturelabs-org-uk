			<h1>Set up a social enterprise</h1>
			<h2>Trusts</h2>
			<p>A trust is a legal device which governs how assets given by an individual or organisation are to be used. Many organisations, such as those involved in education, healthcare, providing advice and conservation, can be structured as trusts.</p>
			
			<h3>How trusts work</h3>
			<p>Trusts are unincorporated bodies and do not distribute their profits. They're managed by trustees. Trustees do not benefit from the trust, but act on behalf of the community for whose benefit the trust is set up. Trusts make their own governing rules and have no legal identity of their own.</p>
			<p>Trusts have a <strong>trust deed</strong> which protects their social objectives. This covers the terms under which an individual or organisation is given assets. It also lists the intended beneficiaries and the conditions under which the trust's assets may be used.</p>
			<p>As trusts can hold property and other assets for the community, they can act as sister bodies to unincorporated associations. See the page in this guide on <a href="/starting-up/business-structures/set-up-a-social-enterprise/unincorporated-associations.php">unincorporated associations</a>.</p>
			<p>Trusts are relatively straightforward and cost-effective to set up. As they have no separate legal identity, the trustees are personally liable for the trust's liabilities. You should seek legal advice on whether you need to register the trust's name with Companies House.</p>
			
			<h3>Asset locks</h3>
			<p>Trusts may write an asset lock into their rules to <strong>secure assets for their intended community</strong>. Some other social enterprises can also do this. See the pages in this guide on <a href="/starting-up/business-structures/set-up-a-social-enterprise.php">Community Interest Companies</a>, <a href="/starting-up/business-structures/set-up-a-social-enterprise/community-benefit-societies.php">community benefit societies</a> and <a href="/starting-up/business-structures/set-up-a-social-enterprise/charitable-incorporated-organisations.php">charitable incorporated organisations</a>.</p>
			
			<h3>Charitable trusts</h3>
			<p>If a trust's aims are exclusively charitable and those aims are for the public benefit, the trust should (in most cases) apply to the Charity Commission to be registered as a charity. <a href="https://www.gov.uk/topic/running-charity/setting-up" target="_blank" rel="noopener noreferrer">Read about registering as a charity on the Charity Commission website</a>.</p>
			
			<h3>Development trusts</h3>
			<p>Development trusts are community owned and led organisations, and do not distribute any profits. They provide a hub of trade or service with the aim of regenerating their local community - eg to develop or manage property, restore buildings or improve the environment.</p>
			<p>There is no standard legal form for a development trust. Most register as a company limited by guarantee and in a few cases as an industrial and provident society. Many register as charities. For the legal and financial issues, see the pages in this guide on <a href="/starting-up/business-structures/set-up-a-social-enterprise/limited-companies-with-a-social-purpose.php">limited companies with a social purpose</a> and on <a href="/starting-up/business-structures/set-up-a-social-enterprise/community-benefit-societies.php">community benefit societies</a>.</p>
			
