			<h1>Set up a social enterprise</h1>
			<h2>Social enterprises as registered charities</h2>
			<p>Many social enterprises have charitable status. It is only possible to gain this status if the purposes of your organisation are exclusively charitable and are for the public benefit. Charitable purposes include advancing education or religion, and relieving financial hardship. Over many years, a host of other charitable purposes that benefit the community have been recognised as charitable by the courts or the Charity Commission.</p>
			<h3>Organisational restrictions on charitable social enterprises</h3>
			<ul>
				<li>A charitable social enterprise must have exclusively charitable purposes, and those purposes must be for the public benefit.</li>
				<li>The directors or trustees are responsible for administration and management and generally must not be paid for this work. However, charities can pay trustees (including the director) for providing goods and non-trustee or employee services to the charity. A range of safeguards are in place to prevent conflicts of interest or abuse.</li>
				<li>Any profits or surpluses made by the organisation must be invested back into it and used to support its charitable purposes. Any profit or surplus must not be paid out to members of the charity.</li>
				<li>Assets must always be used for the charitable purposes of the organisation.</li>
			</ul>
			
