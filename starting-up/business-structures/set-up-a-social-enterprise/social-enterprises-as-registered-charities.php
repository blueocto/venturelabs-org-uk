			<h3>Incorporated charities</h3>
			<p>Most charities that seek the benefits of incorporation currently incorporate as a company limited by guarantee. A new incorporated legal form designed exclusively for charities is being developed, called the <strong>charitable incorporated organisation (CIO)</strong>. See the page in this guide on <a href="/starting-up/business-structures/set-up-a-social-enterprise/charitable-incorporated-organisations.php">charitable incorporated organisations</a>.</p>
