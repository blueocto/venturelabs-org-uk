			<h1>Set up and register as self-employed</h1>
			<h2>Checking your self-employment status</h2>
			<p>If your work comes from more than one source it is possible to be <strong>employed</strong> in one or more jobs - with your employer deducting tax and National Insurance - and <strong>self-employed</strong> in others.</p>
			<p>If you're not sure whether your work counts as self-employment, ask yourself these questions:</p>
			<ul>
				<li>Do you have the final say in how the business is run?</li>
				<li>Do you risk your own money in the business?</li>
				<li>Are you responsible for meeting the losses as well as taking the profits?</li>
				<li>Do you provide the main items of equipment you need to do your job, not just the small tools many employees provide for themselves?</li>
				<li>Are you free to hire other people on your own terms to do the work you have taken on? Do you pay them out of your own pocket?</li>
				<li>
					Do you have to correct unsatisfactory work in your own time and at your own expense?
				</li>
			</ul>
			
			<p>If you answer 'yes' to most of these questions then you are probably self-employed already. To find out how to tell HM Revenue &amp; Customs (HMRC) about your self employment, see the page in this guide on <a href="/starting-up/business-structures/set-up-and-register-as-self-employed/registering-for-tax-and-national-insurance.php">registering for tax and National Insurance</a>.</p>
			<p>The latest you can register with HMRC is 5 October after the tax year for which you need to submit a Self Assessment tax return - for example 5 October 2011 for the 2010-11 tax year. If you register late, you may be charged a penalty.</p>
			<p>To better understand your employment status, <a href="https://www.gov.uk/guidance/check-employment-status-for-tax" target="_blank" rel="noopener noreferrer">use the employment status indicator on the HMRC website</a>.</p>
			
