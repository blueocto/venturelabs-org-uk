			<h1>Set up and register as self-employed</h1>
			<h2>Arranging to make National Insurance contributions</h2>
			<p>As well as registering with HM Revenue &amp; Customs (HMRC) so that they can set up records for your tax and National Insurance contribuions (NICs) you'll need to arrange to pay Class 2 NICs. For the tax year 2011-12 the Class 2 NICs rate is &pound;2.50 a week.</p>
			<p>The most convenient way to pay Class 2 NICs is via Direct Debit. Paying by Direct Debit has many advantages:</p>
			<ul>
				<li>it's easy to set up - just fill in the application form and send it to HMRC - you will need a suitable account at a bank or building society</li>
				<li>it runs automatically - once set up, you don't have to remember to make payments</li>
				<li>you can choose to pay monthly or six monthly</li>
				<li>it helps your budgeting - for people in business, good cash flow and budgeting are essential and paying by Direct Debit means that your payments are always made in arrears of your liability</li>
				<li>it gives you peace of mind - payments are made on time, every time and there is no risk of losing benefit because you forgot to pay</li>
			</ul>

			<p><a href="https://www.gov.uk/government/publications/national-insurance-application-to-pay-self-employed-national-insurance-contributions-by-direct-debit-ca5601" target="_blank" rel="noopener noreferrer">Find an application form to pay Class 2 NICs by Direct Debit on the HMRC website</a>.</p>
			<p>If you are unable to pay by Direct Debit, HMRC will issue you with bill reminders in April and October showing the amount due by 31 July and 31 January respectively.</p>
			
			<h3>Applying to be exempt from paying Class 2 NICs</h3>
			<p>If you expect your income from your business to be low for the tax year you may not have to pay Class 2 NICs. <strike>To find out more please download form CF10 Self-employed people with small earnings.</strike></p>
			<p><strike><a href="http://webarchive.nationalarchives.gov.uk/20140603092408/http://search2.hmrc.gov.uk/kb5/hmrc/forms/view.page?record=ykpjabzqcc8&formid=433" target="_blank" rel="noopener noreferrer">Find form CF10 for self-employed people with small earnings on the HMRC website</a>.</strike></p>
