			<h1>Set up and register as self-employed</h1>
			<h2>Registering for tax and National Insurance</h2>
			<p>You can let HM Revenue &amp; Customs (HMRC) know about your new self-employment by registering online for business taxes. HMRC will need to know information about yourself and your business to set up tax records for:</p>
			<ul>
				<li>Self Assessment</li>
				<li>National Insurance</li>
				<li>PAYE if you have people working for you</li>
			</ul>

			<p>A Self Assessment Online account will be set up for you automatically at the same time.</p>
			<p>Once you've registered, HMRC will send you a Unique Taxpayer Reference number and you'll then complete a Self Assessment tax return each year.</p>
			<p>Even if you already complete a Self Assessment tax return, you can still complete a Self Assessment registration form to tell HMRC about your self-employment.</p>
			<p>It's best if you register as soon as you become self-employed.  You must register at the latest by 5 October after the end of the tax year for which you need a tax return. The tax year runs from 6 April one year to 5 April the next. So if, for example, you have tax to pay on your business profits for the 2010-11 tax year, you need to let HMRC know by 5 October 2011.</p>
			<p><a href="/do-it-online/register-online-for-business-taxes.php">Register your business for HMRC taxes</a> </p>
			<p>If you register late you may have to pay a penalty.</p>
			<h3>Registering for the Construction Industry Scheme</h3>
			<p>If you are a contractor and/or a sub-contractor in the construction industry, you must also register with the Construction Industry Scheme.</p>
			<p>For more information, see our guide: <a href="/tax-payroll-and-company-information/construction-industry-scheme/what-is-the-construction-industry-scheme.php">what is the Construction Industry Scheme?</a> </p>
			
			<h3>Registering for VAT</h3>
			<p>If you expect your business to have turnover of more than &pound;73,000, you will need to charge VAT to your customers and pass this on to HMRC. <a href="/starting-up/vat/when-to-register-for-VAT.php">Use our interactive tool to find out if you need to register for VAT</a>.</p>
			<h3>Registering as an employer</h3>
			<p>If you employ other people in your business you need to collect and pay income tax and NICs, which will mean operating a PAYE (Pay As You Earn) system. See our guide on <a href="/tax-payroll-and-company-information/paye-for-employers/introduction-to-paye-for-employers/paye-for-employers-the-basics.php">PAYE for employers: the basics</a>.</p>
			
