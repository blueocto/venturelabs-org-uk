			<h1>Legal structures: the basics</h1>
			<h2>Starting a public limited company</h2>
			<p>Public limited companies (PLCs) exist in their own right. This means the company's finances are separate from the personal finances of their members.</p>
			
			<h3>Set-up</h3>
			<p>PLCs must:</p>
			<ul>
				<li>Have at least two shareholders.</li>
				<li>Have issued shares to the public to a value of at least &pound;50,000 or the prescribed equivalent in euros before it can trade.</li>
				<li>Be registered (incorporated) at Companies House - you can register either via a paper application form or electronically using a third party with access to the necessary software, eg an incorporation agent, software provider or solicitor.</li>
				<li>Have at least two directors - at least one must be an individual. Each director who is an individual must be at least 16 years of age.</li>
				<li>Have a qualified company secretary.</li>
			</ul>
			<p>You can <a href="/do-it-online/incorporate-your-company-online.php">incorporate your limited company online with Companies House</a>. The web incorporation service is used for incorporating a private company, limited by shares, with model articles of association.</p>
			
			<h3>Management and raising finance</h3>
			<p>Businesses that are PLCs are the only type of business that can raise money by selling shares to the general public. Shareholders can be individuals or other companies.</p>
			<p>The shares may or may not be traded on the stock exchange.</p>
			<p>Finance can also be raised through loans and retained profits.</p>
			<p>Note that directors may be asked to give personal guarantees of loans to the company.</p>
			<p>A board of directors usually makes the management decisions.</p>
			
			<h3>Records and accounts</h3>
			<p>PLCs must:</p>
			<ul>
				<li>send an annual return to Companies House</li>
				<li>file accounts with Companies House once a year - the accounts must be audited unless the company is exempt</li>
			</ul>
			<p>You can send your annual return and other documents to Companies House electronically using its WebFiling service. For more information, see the page on <a href="/tax-payroll-and-company-information/company-administration-an-introduction/submitting-documents-to-companies-house/submitting-documents-to-companies-house-using-webfiling.php">submitting documents to Companies House using WebFiling</a> in our guide on <a href="/tax-payroll-and-company-information/company-administration-an-introduction/submitting-documents-to-companies-house.php">submitting documents to Companies House</a>.</p>
			<p>Directors must notify Companies House of changes in the structure and management of the business.</p>
			
			<h3>Profits</h3>
			<p>Profits are usually distributed to shareholders in the form of dividends, apart from profits retained in the business as working capital.</p>
			
			<h3>Tax and National Insurance</h3>
			<p>If a PLC has any taxable income or profits, it must tell HM Revenue &amp; Customs (HMRC) that it exists and is liable to <strong>Corporation Tax</strong>. It must then pay any Corporation Tax that's due and submit a Company Tax Return to HMRC. A PLC must also comply with HMRC's requirements for PAYE for employers, VAT, the Construction Industry Scheme etc. For more information, see our section on <a href="/tax-payroll-and-company-information.php">tax, payroll and company information</a>.</p>
			<p>Company directors are an office holder of the company and therefore regarded as an employed earner for the purposes of paying National Insurance contributions (NICs). As such, company directors must pay both income tax and Class 1 NICs on their director's earnings.</p>
			<p>However, while regular employees' Class 1 NICs are calculated on their monthly or weekly earnings separately, directors' NICs are calculated on an annual cumulative basis.</p>
			<p>For more information, see the page on <a href="/tax-payroll-and-company-information/paye-for-employers/monthly-weekly-payroll-and-form-filling/calculating-nics-deductions-paper-methods/calculating-directors-national-insurance-contributions.php">calculating directors' National Insurance contributions</a> in our guide on <a href="/tax-payroll-and-company-information/paye-for-employers/monthly-weekly-payroll-and-form-filling/calculating-nics-deductions-paper-methods.php">calculating NICs deductions (paper methods)</a>.</p>
			<p>Company directors must complete a Self Assessment tax return each year. You will need to give details of the income from your directorship on the employment pages. If you do not usually fill in a tax return you must register for Self Assessment.  See our guide on <a href="/tax-payroll-and-company-information/self-assessment/your-tax-return-the-basics/registering-for-self-assessment.php">registering for Self Assessment</a>. </p>
			
			<h3>Liability</h3>
			<p>The liability of each member is limited to the amount unpaid on their shares.</p>
			<p>Members are not responsible for the company's debts unless they have given guarantees - eg when taking out a bank loan.</p>

