			<h1>Legal structures: the basics</h1>
			<h2>European Economic Interest Groupings</h2>
			<p>A European Economic Interest Grouping (EEIG) is an association of businesses and/or individuals from different European Union (EU) countries who need to operate together across national borders.</p>
			<p>Its aim is to facilitate or develop the economic activities of its members but is a separate entity from their own businesses.</p>
			<p>An EEIG may be set up in any one of the Member States, and operate in any part of the EU. It can also enter into arrangements with organisations outside the EU, although these organisations cannot themselves become members of an EEIG.</p>
			<p>An EEIG must be registered in the EU Member State where it has its 'official address'. The official address must be where either:</p>
			<ul>
				<li>the EEIG has its central administration</li>
				<li>one of its members has its central administration or - in the case of an individual who is a member - their principal activity, provided that the grouping carries on an activity there</li>
			</ul>
			<p>You must register with Companies House if:</p>
			<ul>
				<li>it's an new EEIG and its central administration is going to be in the UK</li>
				<li>you want to transfer an existing EEIG's central administration to the UK</li>
				<li>you want to set up an establishment for an existing EEIG in the UK - even if its central administration will remain elsewhere in the EU</li>
			</ul>
			<p>Registration with Companies House is via paper application only.</p>
			
