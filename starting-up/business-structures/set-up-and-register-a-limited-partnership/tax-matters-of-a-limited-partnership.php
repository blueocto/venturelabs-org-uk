			<h1>Set up and register a limited partnership</h1>
			<h2>Tax matters of a limited partnership</h2>
			<p>Once you've registered the limited partnership with Companies House, using form LP5, they will notify HM Revenue and Customs (HMRC), so you don't need to do this. HMRC will then set up the right tax records for the partnership. Each partner in the limited partnership must register with HMRC separately to get their own tax records set up.</p>
			<p><a href="https://www.gov.uk/government/publications/self-assessment-register-a-partnership-for-self-assessment-sa400" target="_blank" rel="noopener noreferrer">Find the form you need to register as a partner on the HMRC website</a>.</p>
			<p>Generally, limited partnerships are treated the same as general partnerships for tax purposes. Like general partnerships, profits are shared amongst the members of a limited partnership. Individual members - not the limited partnership - pay tax on income or gains. Unlike limited companies, limited partnerships are not liable for Corporation Tax. The nominated partner will need to complete a Self Assessment tax return for the partnership every year. Each member of the partnership will need to show their share of the profits on their own tax return.</p>
			<p>For more on tax see the page on <a href="/starting-up/business-structures/set-up-and-register-a-partnership/tax-matters-of-a-partnership.php">tax matters of a partnership</a> in our guide on how to <a href="/starting-up/business-structures/set-up-and-register-a-partnership.php">set up and register a partnership</a>. This guide includes information on VAT, PAYE for employers and the Construction Industry Scheme.</p>
			<p>For information about Self Assessment see our guide: <a href="/tax-payroll-and-company-information/self-assessment/your-tax-return-the-basics/introduction-to-self-assessment.php">introduction to Self Assessment</a>.  </p>
			
