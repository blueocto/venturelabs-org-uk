			<h1>Set up and register a limited liability partnership (LLP)</h1>
			<h2>The members of an LLP</h2>
			<p>Any group of two or more people who want to set up a profit-making business together can form a limited liability partnership (LLP), unless one of them has a disqualification order (disqualified to act as a company director or LLP member) in place. It is also possible for companies, and other LLPs, to be members of an LLP.</p>
			
			<h3>How many members should an LLP have?</h3>
			<p>An LLP must have at least two members, and at least two must be 'designated' members. If there are fewer than two designated members then every member is deemed to be a designated member.</p>
			
			<h3>The rights and responsibilities of LLP members</h3>
			<p>The members of an LLP normally share in both the responsibilities of running the business and the profits that it makes. Exactly how their rights and responsibilities are defined and divided depends on the LLP's partnership agreement or 'deed of partnership'. Designated members have some extra responsibilities on top of those of ordinary members.</p>
			
			<h3>The responsibilities of designated members</h3>
			<p>Designated members have to ensure that the LLP meets its legal obligations by:</p>
			<ul>
				<li>registering the LLP at Companies House</li>
				<li>appointing an auditor - if one is needed</li>
				<li>preparing and signing the accounts on behalf of the members</li>
				<li>delivering the accounts to Companies House</li>
				<li>notifying Companies House of any membership changes, or of a change to the registered office address or name of the LLP</li>
				<li>preparing, signing and delivering the annual return to Companies House</li>
				<li>acting on behalf of the LLP if it is wound up and dissolved</li>
			</ul>
			<p>Designated members are legally accountable if they fail to carry out their duties properly.</p>
			
			<h3>Changes to members' details or status</h3>
			<p>It is important that you notify Companies House whenever any of the members' details change. For more information on what you need to file, see our guide on <a href="/tax-payroll-and-company-information/changes-to-your-business/changes-within-a-partnership.php">changes within a partnership</a>.</p>
			
