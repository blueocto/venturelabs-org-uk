			<h1>Set up and register a limited liability partnership (LLP)</h1>
			<h2>Tax matters of an LLP</h2>
			<p>Once you've registered the LLP with Companies House, using form LL IN01, they will notify HM Revenue and Customs (HMRC), so you don't need to do this. HMRC will then set up the right tax records for the partnership.</p>
			<p>Each partner in the LLP must also register separately with HMRC for get their own tax records set up. Find out more about how to register partners in our guide on <a href="/tax-payroll-and-company-information/self-assessment/your-tax-return-the-basics/registering-for-self-assessment.php">registering for Self Assessment</a>.</p>
			<p>On the whole, LLPs are treated the same as general partnerships for tax purposes. Like general partnerships, profits are shared among the members of a limited partnership. Individual members - not the limited partnership - pay tax on income or gains. Unlike limited companies, LLPs are not generally liable for Corporation Tax. The nominated partner will need to complete a Self Assessment tax return for the partnership every year. Members of the LLP will need to also complete a Self Assessment tax return and show their share of the profits on it.</p>
			<p>For more on tax see the page on <a href="/starting-up/business-structures/set-up-and-register-a-partnership/tax-matters-of-a-partnership.php">tax matters of a partnership</a> in our guide on how to <a href="/starting-up/business-structures/set-up-and-register-a-partnership.php">set up and register a partnership</a>. This guide includes information on VAT, PAYE for employers and the Construction Industry Scheme.</p>
			
