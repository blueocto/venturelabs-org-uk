		<p>A partnership is a relatively simple and flexible way for two or more people to own and run a profit-making business together. Social enterprises and other non-profit making organisations should not use this business structure.</p>
		<p>Unlike the shareholders in a limited company, the members of a general partnership have no financial protection if the business runs into trouble - each partner is responsible for the debts of the partnership as a whole. This means that each partner's personal assets may be at risk if the business fails.</p>
		<p>Disputes between partners can cause difficulties, and the partnership may have to be dissolved if one of its members resigns or dies. It's possible to resolve these issues in advance by drawing up a deed of partnership. It is&nbsp;a good idea&nbsp;for each partner to get legal advice before finalising their level of responsibility in a deed.</p>
