			<h1>Set up and register a limited company (private or public)</h1>
			<h2>Checklist: setting up and registering a limited company (private or public)</h2>
			<p>As well as registering your business as a company, there are several other things that you need to do to put it on a proper legal footing. Make sure you:</p>
			<ul>
				<li>
					Display your company's name so it can be clearly seen by all visitors to its registered office or other places of business.
				</li>
				<li>
					Display your company's name clearly on all its business stationery, including letters, invoices, receipts and cheques and your company website if you have one.
				</li>
				<li>
					Show your company's place of registration, registered number and registered office address on all its business letters, order forms, emails, website and faxes.
				</li>
				<li>
					If any company directors' names are included in letters (other than in the text or as the person signing it) you must include the names of <strong>all</strong> the directors.
				</li>
				<li>
					Send all the necessary registration documents and forms to the Registrar of Companies, completed and signed. See the page in this guide on <a href="/starting-up/business-structures/set-up-and-register-a-limited-company-private-or-public/registration-documents-and-forms.php">registration documents and forms</a>.
				</li>
				<li>
					Check you have received confirmation - certificate of incorporation - from the Registrar of Companies that your application for limited company status has been successful. The company comes into existence only when the Registrar of Companies issues a certificate of incorporation.
				</li>
				<li>
					Contact HM Revenue &amp; Customs to inform them that your company exists.
				</li>
			</ul>
			<p>It's worth remembering, though, that the above checklist only covers the basics. There are many other tax and reporting obligations that your company must meet from year to year. <a href="/tax-payroll-and-company-information/find-out-what-records-you-should-be-keeping.php">Use our interactive tool to find out the records you must keep as a business</a>.</p>
			
