			<h1>Set up and register a limited company (private or public)</h1>
			<h2>Registration documents and forms</h2>
			<p>To set up as a limited company in the UK, you - or the agent acting for you - will need to send the following documents to Companies House:</p>
			<ul>
				<li>
					<strong>application form IN01 to register a company</strong> which includes the company's registered office, type of articles, the names and addresses of its directors, statement of capital and initial shareholdings
				</li>
				<li>
					<strong>memorandum of association</strong> giving the names of each subscriber and authentication that they have agreed to become members of that company
				</li>
				<li><strong>articles of association</strong> (unless the company adopts model articles in their entirety), describing how the company will be run, shareholders' rights, any restricted objects and details of the directors' powers</li>
				<li><strong>additional information</strong> if your application includes a prescribed or sensitive word or expression</li>
			</ul>
			<p><a href="https://www.gov.uk/guidance/model-articles-of-association-for-limited-companies" target="_blank" rel="noopener noreferrer">Find the model articles of association on the Companies House website</a>.</p>
			<p>You can <a href="https://www.gov.uk/government/publications/register-a-private-or-public-company-in01" target="_blank" rel="noopener noreferrer">download form IN01 from the Companies House website (PDF, 1.03MB)</a>.</p>
			
			<h3>What is the memorandum of association?</h3>
			<p>The memorandum of association of a company incorporated under the Companies Act 2006 confirms the subscribers' intention to form and become members of a company. In the case of a company limited by shares, the memorandum will also provide evidence of the members' agreement to take at least one share each in the company.</p>
			<p>Information on capital and shareholdings is no longer part of the memorandum and is contained in form IN01 as either a 'statement of capital and shareholdings' or, for those companies limited by guarantee, a 'statement of guarantee'.</p>
			<p>Once the company has been incorporated, the memorandum no longer affects the operation of the company and cannot be amended. It becomes, to a large extent, a historical document.</p>
			<p><a href="https://www.gov.uk/government/publications/give-notice-of-subscribers-company-with-share-capital" target="_blank" rel="noopener noreferrer">Download a template memorandum from the Companies House website (PDF, 228K)</a>.</p>
			
			<h3>What are the articles of association?</h3>
			<p>A company's articles of association is an internal rulebook. Every company formed under company law will have articles of association - commonly referred to simply as the company's 'articles'.</p>
			<p>The articles are chosen by the members and form a contract between the company and its members. They help ensure the company runs as smoothly and efficiently as possible and set out how the company makes decisions and includes various matters connected with the shares. Every company is required to have articles and they are legally binding on the company and all of its members.</p>
			<p>The articles cannot contain rules that do not comply with the Companies Act 2006 or make the company operate outside the law. Provided members observe this general principle they can choose which rules go into their company's articles, although they may find it convenient to use model articles. If the members decide to draw up their own rules, they should consider taking legal or other professional advice before having the articles drawn up for their company.</p>
			<p>On incorporation, a company can adopt model articles, model articles with amendments or bespoke articles. Model articles are available for private companies limited by shares, private companies limited by guarantee and public companies.</p>
			<p><a href="https://www.gov.uk/guidance/model-articles-of-association-for-limited-companies" target="_blank" rel="noopener noreferrer">Find the model articles of association on the Companies House website</a>.</p>
			<p>When you complete form IN01, you need to indicate if the proposed company is adopting:</p>
			<ul>
				<li>model articles in their entirety - the model articles should not be sent with the IN01</li>
				<li>model articles with amendments - a copy of the amended articles must be sent with the IN01</li>
				<li>bespoke model articles - a copy of the articles must be sent with the IN01</li>
			</ul>
			<p>If you do not indicate which articles you are adopting, the model articles appropriate to your company type will be automatically applied.</p>
			
