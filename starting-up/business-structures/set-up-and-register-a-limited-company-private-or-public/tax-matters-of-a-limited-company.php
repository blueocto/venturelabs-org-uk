			<h1>Set up and register a limited company (private or public)</h1>
			<h2>Tax matters of a limited company</h2>
			<p>Limited companies have to pay Corporation Tax on their income and profits. They also need to operate a PAYE (Pay As You Earn) system to collect and pay Income Tax and National Insurance contributions from their employees - including company directors.</p>
			<p>The company itself must work out how much Corporation Tax it needs to pay, using a self assessment system. To avoid paying penalties, it's important to understand how this system works and when your company needs to make returns. Your accountant or auditor will be able to advise you.</p>
			<p>You can also <a href="http://webarchive.nationalarchives.gov.uk/20120823131012/http://online.businesslink.gov.uk/bdotg/action/keydates"target="_blank" rel="noopener noreferrer">get regular reminders of important tax dates with our Tax deadline email alerts</a>.</p>
			
			<h3>What do I need to do about taxes when I set up a company?</h3>
			<p>When you register a new company, Companies House will pass on the details to HM Revenue &amp; Customs (HMRC). Additionally, you must tell HMRC if your company is active for Corporation Tax purposes.</p>
			<p>For more information see our guide on <a href="/tax-payroll-and-company-information/corporation-tax/corporation-tax-for-new-companies-and-organisations/starting-a-company-or-organisation-and-corporation-tax.php">starting a company or organisation and Corporation Tax</a>.</p>
			<p>If you have any questions about tax for companies, you should contact the HMRC Employer Helpline on Tel 08457 143 143. For new businesses contact the HMRC New Employer Helpline on Tel 0845 60 70 143.</p>
			
