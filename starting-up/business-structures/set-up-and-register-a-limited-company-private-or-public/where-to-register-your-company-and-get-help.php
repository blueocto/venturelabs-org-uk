			<h1>Set up and register a limited company (private or public)</h1>
			<h2>Where to register your company and get help</h2>
			<p>The registration process for a limited company can be handled directly with Companies House or you can use the services of a company formation agent, accountant or solicitor to do it for you. A third option is to <a href="/do-it-online/incorporate-your-company-online.php">use the online web incorporation service with Companies House</a>. This allows a company limited by shares, with model articles in their entirety with a non-sensitive name to be incorporated online.</p>
			<p><a href="https://www.gov.uk/guidance/model-articles-of-association-for-limited-companies" target="_blank" rel="noopener noreferrer">Find the model articles of association on the Companies House website</a>.</p>
			<h3>How much will registration cost?</h3>
			<p>The cost of registering a company online is considerably lower than on paper. <a href="https://www.gov.uk/government/organisations/companies-house/about/about-our-services#companies-house-fees" target="_blank" rel="noopener noreferrer">Find out more about Companies House fees on the Companies House website</a>.</p>
			<p>The <strong>quickest</strong> way to incorporate a company is to file the application electronically. To find software filers, who provide this as a chargeable service, <a href="https://www.gov.uk/government/publications/formation-and-company-secretarial-agents" target="_blank" rel="noopener noreferrer">see the list of online filers on the Companies House website</a>.</p>
			
			<h3>Where can I get help?</h3>
			<p>Companies House can advise you on the basics of the registration process, but they cannot give you detailed advice on how to draw up the necessary documentation. It is a good idea to seek <b>professional advice</b>. A company formation agent, solicitor or accountant can offer advice or carry out the process for you for a fee. Again, look in your local telephone directory or search the web.</p>
