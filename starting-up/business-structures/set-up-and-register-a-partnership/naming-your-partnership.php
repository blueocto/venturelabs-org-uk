			<h1>Set up and register a partnership</h1>
			<h2>Naming your partnership</h2>
			<p>A partnership can trade under the names of the partners, 'Wright, Brown and Ali LP', for example, or it can use another business name - such as 'Fantastic Design Solutions Limited Partnership'.</p>
			<p>If your trading name does not include the partners' names, you must still make sure that your business website and stationery - such as letters and invoices - display all of their names as well as the trading name - for example, 'Wright, Brown and Ali, trading as Fantastic Design Solutions Limited Partnership'.</p>
			<p>If there are more than 20 partners then the business website and stationery do not have to list them, but they must show the address of the partnership's principal place of business.</p>
			<p>The trading name should not be the same as, or too similar, to that of any existing business, and it should not contain words that people might find offensive or misleading.</p>
			<p>For more information see our guide on how to <a href="/starting-up/name-your-business/choose-the-right-name-for-your-business.php">choose the right name for your business</a>.</p>
			
