			<h1>Set up and register a partnership</h1>
			<h2>Setting up a deed of partnership</h2>
			<p>A deed of partnership is a legally binding agreement between partners who are in business together. It describes how the partnership will be run and the rights and duties of the partners themselves.</p>
			<p>It's not necessary to have a deed in order to set up a partnership, but it's a good idea, as it can help to prevent misunderstandings and disputes. It may be a good idea to enlist a solicitor to ensure that each partner understands their responsibilities before the deed is finalised.</p>
			<p>As well as giving basic information about the partnership, such as its business name and the names of the partners, the type of business and business address, the deed will usually set out:</p>
			<ul>
				<li>the amount of capital that each partner is to contribute to the business</li>
				<li>the way in which partners will share profits or losses, and whether any of the partners should be paid a salary</li>
				<li>working arrangements, such as how much time each partner should contribute to the business, who does what management tasks and what type of decisions need collective agreement between the partners </li>
				<li>
					changes to the partnership, such as how new partners can be appointed and what happens if a partner dies or wishes to leave
				</li>
			</ul>

			<p>If the partnership does not have a deed, it will be governed by the terms of the Partnership Act 1890. This does not offer solutions to many of the problems that can arise and may not suit the way that you and your partners want to work together. <a href="http://webarchive.nationalarchives.gov.uk/20140109143644/http://www.hmrc.gov.uk/manuals/bimmanual/bim72501.htm" target="_blank" rel="noopener noreferrer">Read about the Partnership Act 1890 on the HM Revenue &amp; Customs (HMRC) website</a>.</p>
			
