			<h2>"Here's how I kept my business profitable by exporting my products abroad"</h2>

			<p><em>(Opening animation and jingle. Background music begins with footage of RC2 office and products, cut to Clive Wooster)</em></p>

			<p>Clive Wooster: "My name is Clive Wooster. The company is RC2 and I'm the joint managing director."</p>

			<p><em>(cut to Damien Weight and RC2 toys and products)</em></p>

			<p>Damien Weight: "My name is Damien Weight. I'm the managing director of Finance and Operations of RC2. RC2 is a manufacturer and distributor of toys and nursery products."</p>

			<p><em>(RC2 Logos and cut to Clive and RC2 Products)</em></p>

			<p>Clive Wooster: "The main difference probably of international business is you tend to get very used to what you're doing in your local market. To a certain extent, international business does make you think outside the box. You start from the very basics. You say, 'Why does this person need my product? Does this retailer have it already? In which case, could I do a better service? Could I sell it at a better price? Could I provide add-ons that would really make it worthwhile?' So, it is a good exercise in just taking yourself out of your comfort zone and saying, 'Where is our position in this market? What do we offer?"</p>

			<p>"We made a big change in the way we run the business in terms of export about four years ago, when we, previous to that, did a little bit by using distributors very much third-hand. And now, four years on, we're much more directly involved with export."</p>

			<p><em>(Cut to Damien and Exeter main office, staff working, product advert on computer, product catalogues)</em></p>

			<p>Damien Weight: "To show we're profitable, we've got a kind of pricing model that we apply our brands and we ensure that we're pricing to market in each local country. But we'll only put forward brands that will become profitable. So it means that some brands just don't work in some of the countries we're selling to, whereas other ones work very well. So the product range might be different from country to country."</p>

			<p>"The main difference in operating in Europe to the UK is things like payment processes, credit terms and the kind of information that customers require on documents. So we've had to think globally, but act locally to fit those requirements. One of the problems we faced was the way the different countries approach toy safety regulations which we have to adhere to. And even though we're in the European Union (EU), different members of the EU have different applications of those regulations. So we've had to aim at the highest standard to fill each country's requirement."</p>

			<p>"We found that typically, the European countries expect far more credit than we would allow our UK customers so some of them might want 120 days credit before paying and they have specific payment methods they would use in their country other than payment by cheque or BACS. We've had to work with local banks to understand the type of payment method our customers would expect."</p>

			<p><em>(Cut to Clive)</em></p>

			<p>Clive Wooster: "Certainly, the last 15 or 10 years, things have got a lot better. The internet has helped of course a lot, but I do feel that countries have broken down a lot of the cultural barriers for international business... apart from one or two exceptions - Germany, Spain and so on - are very welcome to international business and accepting it as the norm these days."</p>

			<p><em>(Cut to Damien and main office; staff working)</em></p>

			<p>Damien Weight: "We have four branch offices that sell directly into those countries that have sales and marketing presence in each of them with very strong general management. But all of the back-office and logistic services are carried out here in Exeter. The major barriers in setting up these branch offices in Europe has been the bureaucratic requirements they have for things like employment law, tax filing and the creation of the branches in the first place. The benefit of setting up branch offices in the local countries we deal with is that we are portrayed to our customers as being local to their market. So for example, our French customers believe we're a French supplier which is key to them. It's meant we have had listings that otherwise we wouldn't have obtained."</p>

			<p><em>(Cut to Clive with colleague in office looking over product brochures, then Clive at his computer)</em></p>

			<p>Clive Wooster: "The way to get intelligence really is to speak to people in industries at seminars. Try and find out where they started. I think, do a lot of research, be very clear what the targets you're aiming at - where you want to go. Be very clear about the timelines, as we said earlier, it's not work of a moment. I think recruit really good people at a local level. At back-office level, make sure the people here understand what you're trying to achieve cos they can get disorientated as well. They just think, 'Why the hell am I speaking to somebody from Croatia? I don't understand this.' But if they understand where we're trying to go... And I think perseverance, keep going... If you've got the right elements in place, they eventually do come right, even though at times it feels like you're pushing water uphill."</p>

