		<h1>Exporting - an overview</h1>
		<h2>Marketing your product or service overseas</h2>
		<p>To succeed, your marketing strategy will need to be tailored to each target market.</p>
		<p>You'll need to appreciate the traditions, culture and legislation of the countries you are trading with to exploit your exporting efforts. For more information, see the page in this guide on <a href="/starting-up/trade-with-other-countries/exporting-an-overview/working-effectively-with-different-cultures.php">working effectively with different cultures</a>.</p>
		<p>You can <a href="https://web.archive.org/web/20141026164609/https://web.archive.org/web/20141026164609/http://www.cilt.org.uk/home.aspxhome.aspx" target="_blank" rel="noopener noreferrer">find language support services on the National Centre for Languages website</a>.</p>
		<p>You can <a href="http://webarchive.nationalarchives.gov.uk/20120729124841/http://www.ukti.gov.uk/es_es/export/exporting.html" target="_blank" rel="noopener noreferrer">read about export opportunities in various countries around the world on the UK Trade &amp; Investment website</a>.</p>

		<h3>Sales promotion overseas</h3>
		<p>Customisation of your marketing activities is essential if there are cultural differences affecting the consumption of your product.</p>
		<p>You should consider:</p>
		<ul>
			<li>Using different media. TV viewers in one country may belong to a particular socio-economic group, while in others TV ownership is far more widespread.</li>
			<li>Changing symbols. For example, you may need to respect different standards of dress in promotional activities in some countries.</li>
			<li>Changing the market proposition. For example, bicycles are presented as a leisure item in one country, but as essential vehicles elsewhere.</li>
		</ul>

		<h3>Using local agents</h3>
		<p>Non-specialist research can be conducted in-house but you will need to be clear about the data you require. You will also need to set a realistic budget to cover the necessary costs.</p>
		<p>Alternatively you could delegate the research to local agencies to save money. Local market research agencies have direct access to your potential customers. However, you should carefully consider the reputation of the agency.</p>
		<p>The UK Trade &amp; Investment Accessing International Markets programme can provide support and help in planning your entry into new overseas markets. <a href="http://webarchive.nationalarchives.gov.uk/20120418184810/http://www.ukti.gov.uk/export/howwehelp.html" target="_blank" rel="noopener noreferrer">Find out about growing your business internationally on the UK Trade &amp; investment website</a>.</p>
