		<h1>Exporting - an overview</h1>
		<h2>Ten key steps to successful exporting</h2>
		<ol>
			<li>
				<strong>Research your market</strong> - does your prospective foreign customer need what you are selling at the price that will yield you a profit? What is the competition and how will they react?
			</li>
			<li>
				<strong>Implement an export strategy</strong> and review your capabilities - ask yourself: what would my business gain from exporting?
			</li>
			<li>
				<strong>Construct an export plan</strong> - define how you will enter the foreign market. Finalise human resources and marketing strategy and allocate an adequate budget to cover export start-up costs.
			</li>
			<li>
				<strong>Choose your sales presence</strong> - establish whether you need a direct sales operation. Or is an agent or distributor more effective? How will you manage your overseas sales presence?
			</li>
			<li>
				<strong>Promote your product</strong> - how are you going to market and sell your product? Customise marketing to the target country.
			</li>
			<li>
				<strong>Get the Customs side right</strong> - contact HM Revenue &amp; Customs and the UK embassy of your destination country to clarify requirements. Make sure your reporting practices are watertight.
			</li>
			<li>
				<strong>Get paid on time</strong> - ensure your cashflow will remain at a safe level. Guarantee sufficient credit for your future sales. Take out insurance cover if necessary.
			</li>
			<li>
				<strong>Choose your distribution methods</strong> - consider the implications of selling over long distances and across national frontiers.
			</li>
			<li>
				<strong>Transport goods effectively</strong> - assess and choose the most effective transport method and make sure the goods are insured by you or the importer.
			</li>
			<li>
				<strong>After-sales policy</strong> - regularly liaise with customers, export agents and banks. Monitor political unrest or other adverse conditions in the country of destination. Manage regular servicing and warranty claims.
			</li>
		</ol>
