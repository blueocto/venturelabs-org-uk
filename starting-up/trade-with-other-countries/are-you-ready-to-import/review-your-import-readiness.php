		<h1>Are you ready to import?</h1>
		<h2>Review your import readiness</h2>
		<p>You can use these points to help you decide whether you are ready to start importing or discover what else you need to do to prepare.</p>
		
		<h3>Planning and strategy</h3>
		<ul>
			<li>Are you committed to importing? Have all the key people in your business agreed?</li>
			<li>Do you have a well-defined import strategy?</li>
			<li>How much time and money are you prepared to invest in starting to import?</li>
			<li>Do you know what suppliers, in which countries, you will consider? Have you planned how you will research and select them?</li>
			<li>Do you understand your own strengths and weaknesses? What will be your objectives in negotiating import contracts?</li>
			<li>Have you planned how you will store, process and/or sell the imports? Do you have the resources you need?</li>
		</ul>
		
		<h3>Product specifics</h3>
		<ul>
			<li>Do the goods you plan to import need an import licence?</li>
			<li>Do the goods need to meet any UK standards or legal requirements?</li>
			<li>What requirements are there for the goods to fit in with your production processes or customer demands?</li>
		</ul>

		<h3>Skills and administration</h3>
		<ul>
			<li>Do you understand international payment methods and can you handle them?</li>
			<li>Do you understand international delivery contract terms - ie Incoterms? Have you got the right legal advice?</li>
			<li>Do you have the skills to handle import tasks like customs clearance and delivery, or have you built relationships with third parties to do this for you?</li>
			<li>How do you plan to develop your importing skills? Have you organised any training?</li>
			<li>Have you set up administration systems to handle importing, eg tracking deliveries and payments?</li>
			<li>Do you have the personnel resources to handle the additional work? Which individuals will be responsible for each task?</li>
		</ul>

		<h3>Finances</h3>
		<ul>
			<li>Have you identified all the costs of importing, including transport, insurance and import duties and taxes?</li>
			<li>Do you have enough working capital to finance imports or will you need to negotiate credit from suppliers or arrange a trade finance package with your bank?</li>
		</ul>

