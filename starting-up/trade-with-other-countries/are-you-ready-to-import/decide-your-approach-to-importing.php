		<h1>Are you ready to import?</h1>
		<h2>Decide your approach to importing</h2>
		<p>It's up to you to decide how involved in importing you want to be, and whether you can cope with the paperwork, logistics and payment issues. Dealing with inexperienced suppliers, and organising most of the logistics yourself can offer greater profit potential. But it also requires more investment, and tends to carry greater risks.</p>
		<p>Businesses that are new to importing often prefer to keep things relatively simple and low risk:</p>
		<ul>
			<li>It's generally easiest to import from countries within the European Union. It's also relatively easy to import from developed countries such as the USA. Importing from developing countries can be more complicated.</li>
			<li>If you deal with experienced exporters, it's usually possible to negotiate a contract where they take responsibility for delivering the goods to the UK - or even to your own premises. Of course, the supplier will want to build all these costs into the price they charge you.</li>
			<li>You can also negotiate contracts in pounds sterling, rather than the supplier's currency. Again, the supplier will want to build extra costs into their price for this.</li>
			<li>Some suppliers, particularly in developing countries, require payment by letter of credit. This can cause problems, especially if you are inexperienced.</li>
			<li>You can use third parties to handle your responsibilities. For example, you can use an import agent to handle UK customs clearance, and a freight forwarder to handle onward delivery to your premises.</li>
		</ul>
		<p>As you build experience and confidence, you may want to increase the scale and complexity of your importing. You can choose which skills to develop. For example, if your supplier offers a substantial discount for dealing in their currency, you might want to arrange training in managing foreign exchange, open a foreign currency bank account and invest in accounting software that can handle transactions in other currencies.</p>
		
