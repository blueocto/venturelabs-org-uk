		<h1>Preparing to export</h1>
		<h2>Manage export contracts and logistics</h2>
		<p>Like any sales contract, you need to agree what goods you will deliver, and when. The agreement should make it clear who is at risk if the goods are lost or damaged at any stage during delivery. Your agreement also needs to cover where you will deliver the goods and whether you will arrange for them to be cleared through customs.</p>
		<p>It's good practice to use internationally recognised Incoterms in your contract to set out the responsibilities for transport and insurance. See our guides to <a href="/international-trade/importing-and-exporting-basics/licensing-and-paperwork/international-trade-paperwork-the-basics.php">international trade paperwork: the basics</a> and <a href="/international-trade/importing-and-exporting-basics/getting-paid-when-trading-internationally/international-commercial-contracts-incoterms.php">International Commercial Contracts - Incoterms</a>.</p>
		<p>Taking on more responsibility can help you to be more competitive, particularly if you are selling to customers who do not have import experience. But you also face increased costs and risks. Whatever you agree, it's important to be sure that you can handle your responsibilities. International trade paperwork can be complicated, and mistakes can be costly. You may want to use specialist help such as a freight forwarder. See the page in this guide on how to <a href="/starting-up/trade-with-other-countries/preparing-to-export/use-the-right-export-support-services.php">use the right export support services</a>.</p>
		
		<h3>Payment</h3>
		<p>You need to agree how much the customer will pay, when payment will be due, and how payment will be made. Different payment methods, such as <strong>advance payment</strong> or <strong>letters of credit</strong> give you more security than giving your customer open account credit. See our guide to <a href="/international-trade/importing-and-exporting-basics/getting-paid-when-trading-internationally/getting-paid-when-selling-overseas.php">getting paid when selling overseas</a>.</p>
		<p>Pricing in the local currency, rather than pounds sterling, can help make you more competitive but puts you at risk if exchange rates change. See our guide to <a href="/finance-and-grants/business-banking/foreign-currency-and-exchange-risks.php">foreign currency and exchange risks</a>.</p>
		
