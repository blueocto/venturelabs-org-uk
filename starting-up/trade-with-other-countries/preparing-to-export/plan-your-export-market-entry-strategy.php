		<h1>Preparing to export</h1>
		<h2>Plan your export market entry strategy</h2>
		<p>The best way to sell into an export market depends on your circumstances and local market conditions. For example, if local wholesalers dominate the market for your product, you might target them rather than trying to sell directly to end-customers.</p>
		<p>You also need to think about how to <strong>promote yourself</strong>. It's important to take local culture and regulations into account. For example, it might be illegal to advertise your product to children, or the name of your product might have an unwanted meaning in the local language.</p>
		<p>If you work with a local agent or distributor you will benefit from their local contacts and expertise. In some countries, working with a local partner is almost essential - or even legally required.</p>
		<p>For many businesses, the internet is a good way of marketing to customers overseas, or even selling directly to them. See our guide to <a href="/it-and-ecommerce/e-commerce/planning-for-e-commerce.php">planning for e-commerce</a>. Trade visits can also be an important part of creating awareness, building relationships and making sales.</p>
		<p>You should think about practical aspects as well, such as clearing goods through overseas customs. If you are dealing with experienced importers, they may be prepared to take responsibility for this themselves. Otherwise, you might want to work with an experienced agent or freight forwarder who can handle this. See the page in this guide on how to <a href="/starting-up/trade-with-other-countries/preparing-to-export/use-the-right-export-support-services.php">use the right export support services</a>.</p>
		<p>For more information, see our guide on selling and promotion overseas.</p>
		
