		<h1>Preparing to export</h1>
		<h2>Assess export market opportunities</h2>
		<p>You might first consider exporting in response to enquiries from potential customers overseas. Or you might actively decide to look for opportunities. In either case, thorough market research is essential.</p>
		<p>You need to understand what overseas customers want, and what the competition is like. As well as local suppliers, you may be competing with other exporters from around the world.</p>
		
		<h3>Reaching customers</h3>
		<p>It's worth thinking about how you will reach customers. For example, the internet might be a good way of reaching customers - or even selling directly - in a market where many customers use the internet. See the page in this guide on how to <a href="/starting-up/trade-with-other-countries/preparing-to-export/plan-your-export-market-entry-strategy.php">plan your export market entry strategy</a>.</p>
		<p>You'll also want to understand all the issues that affect doing business in that country. There may be legal considerations, like product regulations or licences or extra taxes that make it difficult to compete. See our guide: <a href="/international-trade/do-you-need-an-export-or-import-licence.php">do you need an export or import licence?</a> You might be faced with a different business culture, and need to communicate in the local language.</p>
		
		<h3>Deciding what to offer</h3>
		<p>UK products and services often need to be modified to suit local regulations and customers' requirements. You'll also need to think about what after-sales service you need to provide and the best way of doing so.</p>
		<p><a href="http://webarchive.nationalarchives.gov.uk/20120729130354/http://www.improve.businesslink.gov.uk//resources//tools//exporting-growth" target="_blank" rel="noopener noreferrer">Identify your business' current capacity to export with the Export for growth tool on the Growth and Improvement Service</a>.</p>
		<p>All this adds costs - over and above the additional costs such as delivery. Many exporters find that they need to position their product as a premium brand to justify higher prices and cover all these costs.</p>
		<p>For more information, see our guide to <a href="/international-trade/importing-and-exporting-basics/overseas-supplier-relationships/entering-overseas-markets.php">researching and entering overseas markets</a>.</p>
		
