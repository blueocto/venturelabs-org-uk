		<h1>Preparing to export</h1>
		<h2>Assess your skills and resources for exporting</h2>
		<p>To start exporting successfully, you should take a planned approach and decide what your export strategy is. You need to spend time and money planning, researching market opportunities and building relationships. You may also need to invest in modifying your product and service to suit overseas customers.</p>
		<p>Exporting is usually a way of growing a successful business, rather than an easy way out for one that's in trouble. If you're struggling with limited finances or overworked employees, you may not have the resources to take on the extra work.</p>
		
		<h3>Special skills</h3>
		<p>Once you have planned your exporting activities, you also need to devote <strong>extra resources</strong> to handling your exporting business. Marketing to overseas customers tends to be more demanding than selling within the UK. Exporting also needs <strong>special skills</strong> - such as organising international transport and handling customs clearance.</p>
		<p>Many businesses find that the best way to get started is to buy in the services they need, and build in-house skills and resources later. For example, you might use a local agent to sell, and a freight forwarder to handle deliveries. See the page in this guide on how to <a href="/starting-up/trade-with-other-countries/preparing-to-export/use-the-right-export-support-services.php">use the right export support services</a>.</p>
		
		<h3>Additional financing</h3>
		<p>Exporting can also be financially demanding. Customers often want <strong>credit</strong> from the time they receive the goods. For a long distance shipment, this could be weeks after you produced and shipped the goods, so you get paid later than you would by a customer in the UK. At the same time, you may have to meet <strong>extra costs</strong> like transport and insurance.</p>
		<p>The more successful you are, the greater the demands placed on your business will be. It's worth planning ahead to be sure you have the capacity to handle the extra production, selling and after-sales support.</p>
		<p><a href="http://webarchive.nationalarchives.gov.uk/20120418184810/http://www.ukti.gov.uk/export/howwehelp.html" target="_blank" rel="noopener noreferrer">Find out about the training, planning and support services offered by the Developing your International Trade Potential programme on the UK Trade &amp; Investment website</a>.</p>
		
