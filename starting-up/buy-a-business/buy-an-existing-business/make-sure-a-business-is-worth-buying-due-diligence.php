			<h1>Buy an existing business</h1>
			<h2>Make sure a business is worth buying: due diligence</h2>
			<p>Having done your research, you should verify the information you have been given about your prospective new business. Once an offer has been made and accepted a period of time is allowed for you to access its books and records. This is known as due diligence. It should give you a realistic picture of how the business is performing now, and how it is likely to perform in the future. It should also highlight any issues or problems which might need warranting or guaranteeing.</p>
			<p>There are traditionally three types of due diligence you should do. You might need different advisors for each:</p>
			<ul>
				<li><strong>legal due diligence</strong> - as part of a sales and purchase contract, the lawyers can check that the business has legal title to sell, ownership of all the assets and that regulatory and litigation issues are fully addressed</li>
				<li><strong>financial due diligence</strong> - checking the numbers and making sure there are no black holes or hidden financial issues</li>
				<li><strong>commercial due diligence</strong> - finding out the business' place in the marketplace, checking competitors and the regulatory environment</li>
			</ul>

			<h3>When to begin due diligence</h3>
			<p>Don't start due diligence until you have agreed a price and terms with the seller. They may agree to take the business off the market during your investigation. This is known as an exclusivity period - and the seller will often ask for a down payment to secure it.</p>
			<p>The investigation period is negotiable - but most small businesses need at least three to four weeks.</p>
			
			<h3>Where to get help</h3>
			<p>Ideally you should get accountants and solicitors to help you identify risk areas but, if it is registered with Companies House, you can also obtain copies of the company accounts, the annual return and the other key documents filed by your target business using the Companies House WebCHeck service. The documents can be downloaded from the Companies House website, some at a small fee, helping you assess the value of the business and its assets.</p>
			<p><a href="/do-it-online/search-company-information-using-companies-house-webcheck.php">Find detailed financial information about companies using the Companies House WebCHeck service</a>.</p>
			<p>Due diligence is about much more than the finances of a business. You need to come out of this period knowing exactly what you are getting into, what needs to be fixed, what it will cost to fix, and if you are the right person to take on this business.</p>
			<p>Key areas to cover are:</p>
			<ul>
				<li>employment terms and conditions</li>
				<li>outstanding litigation</li>
				<li>major contracts and orders</li>
				<li>IT systems and other technology</li>
				<li>environmental issues</li>
				<li>commercial management including customer service, research and development, and marketing</li>
			</ul>

			<h3>Information sources</h3>
			<p>Dig as deeply as you can and use whatever documents are available. For instance, if you're looking at employee records, you could check out:</p>
			<ul>
				<li>payroll records</li>
				<li>staff files</li>
				<li>copies of pension and profit-sharing plans, plus financial statements, if relevant</li>
				<li>employment contracts</li>
				<li>the staff manual</li>
				<li>union contracts, if relevant</li>
			</ul>

			<p>You may also need information from external sources such as the landlord, tax office or bank.</p>
			
