			<h1>Buy an existing business</h1>
			<h2>Step-by-step: how to buy a business</h2>
			<p>An organised approach will help you find and acquire the right business.</p>
			
			<h3>1. Get professional advice</h3>
			<p>Professional help is invaluable as you go through the negotiation, valuation and purchase process. You can find details of how to find professional help in our guides on how to <a href="/finance-and-grants/expert-financial-advice/choose-and-work-with-a-solicitor.php">choose and work with a solicitor</a> and how to <a href="/finance-and-grants/financial-planning-and-accounts/choose-and-work-with-an-accountant.php">choose and work with an accountant</a>.</p>
			<p>You can also <a href="http://www.lucasweston.com/how-to-choose-a-business-broker.htm" target="_blank" rel="noopener noreferrer">find clear advice on buying a business through a business broker on the Lucas &amp; Weston website</a>.</p>
			
			<h3>2. Research</h3>
			<p>Research the sector you're interested in, including the best time to buy, and shortlist two or three businesses.</p>
			
			<h3>3. Initial viewing and valuation</h3>
			<p>Be discreet - the owner may not want staff to know they are selling, but be thorough and record key findings.</p>
			
			<h3>4. Arrange finance</h3>
			<p>Lenders generally require:</p>
			<ul>
				<li>details of the business/sales particulars</li>
				<li>accounts for the last three years</li>
				<li>financial projections - if no accounts are available</li>
				<li>details of your personal assets and liabilities</li>
			</ul>
			
			<p>There are several possible sources of finance you could consider. For specific advice, see our guides on <a href="/finance-and-grants/finance-options/borrowing/bank-finance.php">bank finance</a>, <a href="/finance-and-grants/finance-options/borrowing/financing-from-friends-and-family.php">financing from friends and family</a> and <a href="/finance-and-grants/finance-options/shares-and-equity-finance/equity-finance.php">equity finance</a>.</p>
			
			<p><a href="/finance-and-grants/finance-options/choose-the-right-finance-options/identify-the-right-finance-options-for-your-business.php">Use our interactive tool to identify the right finance for your business</a>.</p>
			
			<h3>5. Make a formal offer</h3>
			<p>If you make your initial offer by phone, follow this up in writing. Head your letter <strong>subject to contract</strong> and include this phrase in all written communication.</p>
			
			<h3>6. Negotiation</h3>
			<p>Before completing the sale, it may be worth trying to negotiate an overlap period so you have time to become familiar with the business before taking over.</p>
			<p>You and your solicitor need to verify the information you have based your offer on. See the page in this guide on how to <a href="/starting-up/buy-a-business/buy-an-existing-business/make-sure-a-business-is-worth-buying-due-diligence.php">make sure a business is worth buying: due diligence</a>.</p>
			<p>If you're buying premises, you may want to arrange an independent survey and valuation, even if a lender is also carrying out their own survey and valuation at your expense. You can <a href="https://www.ricsfirms.com/" target="_blank" rel="noopener noreferrer">find a surveryor who specialises in commercial property on the Royal Institution of Chartered Surveyors (RICS) website</a>.</p>
			
			<h3>7. Completion</h3>
			<p>Even after you reach an agreement on the price and terms of sale, the deal could still fall through. You have to meet certain conditions of sale to complete, including:</p>
			<ul>
				<li>verification of financial statements</li>
				<li>transfer of leases</li>
				<li>transfer of contracts/licences</li>
				<li>transfer of finance</li>
				<li>transfer of existing or new VAT registration</li>
			</ul>
			
