			<h1>Buy an existing business</h1>
			<h2>Advantages and disadvantages of buying an existing business</h2>
			<p>If you get it right, there can be many good reasons why buying an existing business could make good business sense. Remember though, that you will be taking on the legacy of the previous business owner, and you need to be aware of every aspect of the business you're about to buy.</p>
			<h3>Advantages</h3>
			<ul>
				<li>Some of the groundwork to get the business up and running will have been done.</li>
				<li>It may be easier to obtain finance as the business will have a proven track record.</li>
				<li>A market for the product or service will have already been demonstrated.</li>
				<li>There may be established customers, a reliable income, a reputation to capitalise and build on and a useful network of contacts.</li>
				<li>A business plan and marketing method should already be in place.</li>
				<li>Existing employees should have experience you can draw on.</li>
				<li>Many of the problems will have been discovered and solved already.</li>
			</ul>
			<h3>Disadvantages</h3>
			<ul>
				<li>You often need to invest a large amount up front, and will also have to budget for professional fees for solicitors, surveyors, accountants etc.</li>
				<li>You will probably also need several months' worth of working capital to assist with cashflow.</li>
				<li>If the business has been neglected you may need to invest quite a bit more on top of the purchase price to give it the best chance of success.</li>
				<li>You may need to honour or renegotiate any outstanding contracts the previous owner leaves in place.</li>
				<li>You also need to consider why the current owner is selling up and how this might impact the business and your taking it over.</li>
				<li>It's possible current staff may not be happy with a new boss, or the business might have been run badly and staff morale may be low.</li>
			</ul>

