			<h2>Here's how I selected and bought my franchise</h2>
			<p>Entrepreneur and trained optometrist Stephen Halpin always intended to run his own business. In his chosen market - the high street optical services sector - a franchise seemed like a good way to get a head start. After considering the options, Stephen bought one of the first Boots Opticians franchises - in Northwich, Cheshire.</p>
			<p><strong>What I did</strong></p>
			
			<h3>Select the franchise</h3>
			<p>"The retail optical services market is highly competitive and starting an outlet in your own name is a risky affair. A franchise made sense, because it reduces some - not all - of the risks, offers a familiar brand name to build on and provides support with marketing and other aspects. There are several franchisors out there and I considered them all. I decided to go for a Boots Opticians franchise.</p>
			<p>"Boots' reputation with UK consumers is excellent and brand awareness is almost universal. Also, because I was applying for one of the first Boots Opticians franchises, there was more scope to get involved in developing the operational systems. This was important to me- I found that longer-established franchisors had a less flexible attitude."</p>
			
			<h3>Work out the figures</h3>
			<p>"With the help of an accountant, I put together a detailed business plan. The store I wanted to take on had been trading as a Boots Opticians for several years, so it had financial records to assess. Other factors I considered were the store location, local competitors and current operational practices. Without a clear idea of how much the business could make and how much cash I needed, it would have been impossible to tell if the franchise agreement on offer was worthwhile."</p>
			
			<h3>Check the agreement</h3>
			<p>"The business plan also formed the basis of the presentation I gave to Boots' management. This was a key part of the process of being accepted for the franchise. The presentation and plan also gave me the information I needed to negotiate a contract. Following the presentation we had a number of conversations about the principles of any agreement.</p>
			<p>"Once I had been accepted for the franchise, Boots drew up a franchise agreement setting out terms, conditions and fees. I got advice from specialist franchise solicitors before signing. One of my key objectives was to ensure that the agreement benefited both parties-that I made money and so did Boots.</p>
			<p>"Starting from this position, I didn't accept the initial agreement and was able to change a few things. I wanted an incentive to grow the business, rather than just keep it ticking over, and Boots recognised that. One very useful point we negotiated was a deferment of payment on the upfront licence fee, which is one of the biggest franchise costs. I paid a portion on signing, the rest a few months later. It meant I had to borrow less in the early days."</p>
			<p><strong>What I'd do differently</strong></p>
			
			<h3>Get even more advice</h3>
			<p>"I made use of professional advisers and also had help from Boots, who paid for a course covering tax planning, regulatory compliance and so on. Even so, I wish I'd had more advice, especially with regard to employment law and the impact of VAT on the business."</p>
			
			<h3>Understand TUPE better</h3>
			<p>"TUPE stands for the Transfer of Undertakings (Protection of Employment) Regulations. It's a piece of legislation that's concerned with the transfer of staff from one employer to another. As I was taking on existing staff with the franchise, a better understanding of TUPE would have been helpful during negotiations."</p>
