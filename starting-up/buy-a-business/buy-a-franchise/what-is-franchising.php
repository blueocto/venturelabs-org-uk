			<h1>Buy a franchise</h1>
			<h2>What is franchising?</h2>
			<p>The term 'franchising' can describe some very different business arrangements. It is important to understand exactly what you're being offered.</p>
			<h3>Business format franchise</h3>
			<p>This is the most common form of franchising. A true business format franchise occurs when the owner of a business (the franchisor) grants a licence to another person or business (the franchisee) to use their business idea - often in a specific geographical area.</p>
			<p>The franchisee sells the franchisor's product or services, trades under the franchisor's trade mark or trade name, and benefits from the franchisor's help and support.</p>
			<p>In return, the franchisee usually pays an initial fee to the franchisor and then a percentage of the sales revenue.</p>
			<p>The franchisee owns the outlet they run. But the franchisor keeps control over how products are marketed and sold and how their business idea is used.</p>
			<p>Well-known businesses that offer franchises of this kind include Prontaprint, Dyno-Rod and McDonald's.</p>
			
			<h3>Other types of arrangement</h3>
			<p>Different types of sales relationships are also sometimes referred to as franchises. For example:</p>
			<ul>
				<li><strong>Distributorship and dealership</strong> - you sell the product but don't usually trade under the franchise name. You have more freedom over how you run the business.</li>
				<li><strong>Agency</strong> - you sell goods or services on behalf of the supplier.</li>
				<li><strong>Licensee</strong> - you have a licence giving you the right to make and sell the licensor's product. There are usually no extra restrictions on how you run your business.</li>
			</ul>
			
			<h3>Multi-level marketing</h3>
			<p>Some businesses offer franchises that are really multi-level marketing schemes. This is where self-employed distributors sell goods on a manufacturer's behalf. You get commission on any sales you make, and also on sales made by other distributors you recruit.</p>
			<p>Be aware that some multi-level marketing schemes may be dishonest or illegal. <a href="/_files/dti_the_trading-schemes-guide_june-2005.pdf" title="Guidance on trading schemes from BIS" target="_blank" rel="noopener noreferrer">Download the Trading Schemes Guide from the Department for Business, Innovation &amp; Skills (BIS) website (PDF, 66K)</a>.</p>
			
