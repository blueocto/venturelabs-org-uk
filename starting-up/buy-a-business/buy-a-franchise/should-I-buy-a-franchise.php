			<h1>Buy a franchise</h1>
			<h2>Should I buy a franchise?</h2>
			<p>As with any new business venture, you need to consider carefully whether you have got the right skills and attitude to run a successful franchise. You need to consider how much you enjoy managing people, selling to the public or working alone - as these are common requirements of different types of franchise. This page will help you decide whether franchising is right for you and which type of franchise plays best to your strengths.</p>
			
			<h3>Assess yourself</h3>
			<ul>
				<li>You must be prepared to <strong>sell</strong> and you will need entrepreneurial flair. A franchise gives you a business blueprint - but it won't automatically give you customers.</li>
				<li>You'll need to <strong>work hard</strong>, probably for long hours. Do you have the necessary dedication?</li>
				<li>Running your own business can be <strong>stressful</strong>. Think how you react to pressure.</li>
				<li>You may be starting up in business because you want to be <strong>your own boss</strong>. If so, would you be happy with the restrictions imposed by a franchise arrangement?</li>
				<li>On the other hand, you may want to limit <strong>your risk</strong>. You might be more comfortable with a franchise than starting a new business from scratch.</li>
			</ul>
			
			<h3>The right franchise for you</h3>
			<ul>
				<li>Do you like office work? Or would you prefer a business that involves physical labour or using a particular skill?</li>
				<li>Are you happy working on your own? Or would you be good at recruiting, training and managing employees?</li>
				<li>Do you like dealing with members of the public? Or would you prefer a franchise where you sell to business customers?</li>
				<li>Are you weak in particular business skills such as finance? Can you find a franchise that offers the support you need in those areas?</li>
			</ul>

