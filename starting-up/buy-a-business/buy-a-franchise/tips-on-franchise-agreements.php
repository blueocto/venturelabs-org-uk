			<h1>Buy a franchise</h1>
			<h2>Tips on franchise agreements</h2>
			<p>The franchise agreement is crucial. Don't sign any agreement, or pay any fees or deposit, until you have taken <strong>legal advice</strong> from an experienced franchise solicitor accredited by the British Franchise Association. Get a specimen contract for them to review.</p>
			
			<h3>Areas covered by a typical agreement</h3>
			<ul>
				<li><strong>Term -</strong>how long does the franchise last? Will you have the option to renew it, and on what terms?</li>
				<li><strong>Territory -</strong> what area does your franchise cover? Do you have exclusive rights to sell within it?</li>
				<li><strong>Fees -</strong> what initial fee will you pay? Whatpercentage of sales revenuewill you pay? Will you pay a regular management fee - and if so, what does it cover? Will you have to pay other costs? How are the costs worked out?</li>
				<li><strong>Support -</strong>how much help will you get starting the business? What continuing support will you get?</li>
				<li><strong>Restrictions -</strong>what restrictions are there on what you're allowed to do and how you must run the business?</li>
				<li><strong>Exit -</strong>what happens if you can't continue in the business for some reason - perhaps due to ill health? What happens if you want to sell your franchise?</li>
			</ul>

