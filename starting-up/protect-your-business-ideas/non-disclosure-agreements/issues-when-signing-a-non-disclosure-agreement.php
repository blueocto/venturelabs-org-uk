			<h1>Non-disclosure agreements</h1>
			<h2>Issues when signing a non-disclosure agreement</h2>
			<p>Once you sign a non-disclosure agreement (NDA), you have entered into a legally binding contract. This allows you to share ideas with business partners while preventing them from passing this information on.</p>
			<p>Unfortunately, the contract is only as good as the person signing it. If your partner breaches the contract, you can take them to court for damages, but this might be expensive and time consuming. It can also be difficult to quantify the damages.</p>
			<p>If you suspect that your business partner may be about to breach the NDA, you can get an <strong>injunction</strong>, ie a court order preventing them from breaching the agreement, to stop this happening.</p>
			<p>To put you in a better position if you do have to go to court, make sure that all definitions and exceptions in the agreement meet your requirements and are appropriate to the type of trade secret you are sharing. If the contract is flawed, it might not provide enough protection.</p>
			<p>It is a good idea to get to the patent-pending stage of a patent application before approaching investors, marketing or licensing companies. <a href="http://webarchive.nationalarchives.gov.uk/20120729155641/http://www.ipo.gov.uk/patent/p-applying/p-apply.htm" target="_blank" rel="noopener noreferrer">Find information about applying for a patent on the Intellectual Property Office website</a>.</p>
			<p>The biggest difficulty is determining exactly what is and what isn't covered by the agreement. The person disclosing the information wants the contract to be interpreted in its broadest form, whereas the partner prefers to see the contract interpreted in a narrower way.</p>
			<p>If any issues arise and worry either party, it is possible to change or add clauses to resolve the issue.</p>
			<p>You can <a href="/_files/ipo_non-disclosure-agreements.pdf" target="_blank" rel="noopener noreferrer">download information about confidentiality and a sample agreement from the Intellectual Property Office website (PDF, 149K)</a>.</p>
			
