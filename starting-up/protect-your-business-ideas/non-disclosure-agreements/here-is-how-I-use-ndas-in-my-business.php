			<h1>Here's how I use NDAs in my business</h1>
			<p>The Anglesey Sea Salt Company produces Halen Mon sea salt from the pure waters of the Atlantic just off Anglesey, North Wales. Director David Lea-Wilson describes how the use of non-disclosure agreements (NDAs) enables the company to share sensitive information with suppliers and customers.</p>
			
			<h2>What I did</h2>
			<h3>Recognise the need for protection</h3>
			<p>"Our sea salt is highly regarded for its purity, crunchy texture and distinctive taste. A key factor is the exceptional cleanliness of the seawater we use, but other qualities are imparted via our manufacturing process. As such, we're keen to keep the details of the process under wraps.</p>
			<p>"However, we often need to share information for commercial reasons, for example collaborating with plant designers, negotiating joint ventures or raising finance.</p>
			<p>"We can't patent our process because evaporating sea water is deemed to be a natural chemical process, so non-disclosure agreements have become our principal protection against people copying our idea."</p>
			
			<h3>Define agreement terms</h3>
			<p>"We wrote our NDA by purchasing several off-the-shelf agreements, then using parts from each that best suited our business. A specialist lawyer checked the agreement before we began using it.</p>
			<p>"You have to be very specific about what you're willing to share and what you're not in order to minimise the risks. We went through each stage of our process and assessed the business consequences of people knowing a particular thing before deciding what to include. For example, we have to share technical details with plant designers, but the exact temperature we use during evaporation is something we keep to ourselves.</p>
			<p>"You also have to be as precise as possible about how the information provided can and can't be used. We have two versions of our NDA - a full version and a simple one-page version that's useful for highlighting the key points."</p>
			
			<h3>Apply the agreement</h3>
			<p>"We introduce the NDA into negotiations upfront and we make it a rule to leave the first meeting with it signed. We also make our use of NDAs clear to all staff at induction and include a clause in contracts about what they can and can't talk about.</p>
			<p>"Occasionally a potential partner refuses to sign our NDA or wants to use their own version, but we always stick to our guns. If someone wants to take the NDA away to study, that's fine, but we end the meeting immediately - you can't afford to give anything away before they've signed on the dotted line."</p>
			
			<h2>What I'd do differently</h2>
			<h3>Keep a central database</h3>
			<p>"We keep individual files on everyone who's ever signed our NDA. However, a centralised record would have been beneficial and we're now working towards that."</p>
			
			<h3>Do patent searches sooner</h3>
			<p>"Looking back, we should have done patent searches right at the start to ensure that no-one had a claim to any part of our idea."</p>
