		<h1>Who is liable for Corporation Tax</h1>
		<h2>What activities are exempt from Corporation Tax?</h2>
		<p>HM Revenue &amp; Customs (HMRC) uses the term 'exempt' to refer to certain activities carried out by organisations that are otherwise subject to Corporation Tax requirements.</p>
		<p>These include:</p>
		<ul>
			<li>trading profits generated by charities where those profits arise from - and are applied to - charitable purposes</li>
			<li>profits from any fundraising events run by charities or voluntary organisations provided that those profits are applied to charitable purposes</li>
		</ul>
		<p>HMRC defines charitable purposes as carrying out the primary purpose of the charity and/or directly serving the beneficiaries of the charity.</p>
		<p>Other activities exempt from Corporation Tax include:</p>
		<ul>
			<li>agricultural exhibitions or shows if the agricultural society that's running them uses any profits solely for the purposes of the society</li>
			<li>the sale of permanent health insurance or sickness insurance by a friendly society</li>
			<li>non-commercial activities connected with core health care delivery undertaken by NHS foundation trusts</li>
		</ul>
