		<h2>Business and marketing plans</h2>
		<p>Your social enterprise will need to be competitive wherever it operates. It is essential that you have a realistic, working business plan when you're starting up a social enterprise.</p>
		<p>A business plan is a written document that will help you describe your social enterprise and its objectives, its strategies, the market it is in and its financial forecasts. It has many functions, from securing external funding to measuring success within your social enterprise. See our guide on how to <a href="/starting-up/business-planning/prepare-a-business-plan.php">prepare a business plan</a>.</p>
		<p>Attracting customers is vital to success. Highlighting the social and/or environmental credentials of your service/product could give you the edge over your competitors.</p>
		
