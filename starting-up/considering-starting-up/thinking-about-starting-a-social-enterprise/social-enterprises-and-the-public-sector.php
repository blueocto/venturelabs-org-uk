		<h1>Thinking about starting a social enterprise?</h1>
		<h2>Social enterprises and the public sector</h2>
		<p>As well as opportunities in the commercial world, social enterprises are increasingly seeking to diversify income streams, with some deciding to operate solely or partly within the public sector. Working with the government to deliver public services can provide a consistency of demand for your services and be extremely rewarding. However, it is sensible to ensure that your entire income stream is not reliant on a single source.</p>
		<p>Many social enterprises are attracted into public service delivery because they see a need to inject innovative approaches and fresh thinking into traditional ways of conducting business in this area.</p>
		<p>There are a wide variety of opportunities in the delivery of public services, where social enterprises work in partnership with government, including:</p>
		<ul>
			<li>the health and social care sector</li>
			<li>fostering and adoption</li>
			<li>transport services</li>
			<li>refuse collection/recycling</li>
			<li>sports and leisure services</li>
		</ul>
		<h3>Barriers facing social enterprises competing for public service contracts</h3>
		<p>There are certain issues you may face when trying to compete with commercial operators for public service contracts. These can include financial and structural barriers and a general lack of understanding of the social enterprise business model among financiers and commissioners.</p>
		<h3>Public sector opportunities for social enterprises</h3>
		<p>A good example of how social enterprises are successfully contributing to the delivery of public services is in health and social care. <a href="http://webarchive.nationalarchives.gov.uk/20130107105354/http://www.dh.gov.uk/en/Publicationsandstatistics/Publications/PublicationsPolicyAndGuidance/DH_072928" title="Provides a gateway to setting up and expanding your operation within health and social care services provision" target="_blank" rel="noopener noreferrer">Read a resource pack for social enterprise providers on the Department of Health (DoH) website</a>.</p>
		<p>Local authorities may also offer opportunities, for example, in the provision of domestic help for elderly and disabled people.</p>
		<p>The provision of immigration advice or services may constitute additional opportunities for social enterprises and other business entities. However, this area is regulated and prospective advisers need to demonstrate that they are fit and competent to take on such roles. You can <a href="https://www.gov.uk/government/publications/how-to-become-a-regulated-immigration-adviser" target="_blank" rel="noopener noreferrer">find out more about the regulatory scheme and how to become an immigration adviser on the Office of the Immigration Services Commissioner website</a>.</p>
		
