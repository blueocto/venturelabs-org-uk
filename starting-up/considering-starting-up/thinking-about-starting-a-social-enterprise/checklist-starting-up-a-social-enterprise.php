		<h1>Thinking about starting a social enterprise?</h1>
		<h2>Checklist: starting up a social enterprise</h2>
		<p>Before setting up a social enterprise, you might want to ask yourself the following questions to see if you are ready and whether a social enterprise is right for you:</p>
		<ul>
			<li>Have you considered what legal form your social enterprise is going to take? Options include unincorporated associations, trusts, limited companies, industrial and provident societies, Community Interest Companies and charitable incorporated organisations.</li>
			<li>Are you innovative, flexible and independent enough to cope with competing against other businesses with fewer social and/or environmental considerations?</li>
			<li>Are you completely committed to the social and/or environmental aims of the company?</li>
			<li>Have you considered other key regulations that might affect your business - eg if working with vulnerable people, children and the elderly?</li>
			<li>Have you thought about how you will finance the social enterprise, both at start-up and during periods of expansion? If using your own money, will you be able to survive while the business finds its feet?</li>
			<li>Do you have a viable business idea - ie is there a market for your service or product?</li>
		</ul>
		
