		<h1>How do I survive until my business is off the ground?</h1>
		<h2>Forecast your personal financial needs</h2>
		<p>When starting your business you will need to make a realistic forecast of your personal financial needs. A <strong>personal budget</strong> is a plan detailing the personal living expenses you will need to fund from the business or other sources. It should set limits to the amount you plan to spend each month on items like rent, food and housekeeping.</p>
		<p>Tracking your personal spending can help you find out how much money you will need to take from the business. You can <a href="../../../_files/personal-budget-spreadsheet.xls" target="_blank" rel="noopener noreferrer">download our personal budget spreadsheet (XLS, 25K)</a> and adapt it to your individual needs.</p>
		<p>You can now work out how much money you will need each month. If you multiply the monthly figure by 12, and make adjustments to cover one-off spending such as holidays or car tax, you will know how much you need to live on during your first year of trading.</p>
		<p>It is important to be realistic. You may need to find other funds, borrow money or ask HM Revenue &amp; Customs or Jobcentre Plus whether you qualify for tax credits or benefits. Financial advisers usually say that the equivalent of three months money should be held on deposit for a rainy day.</p>
		<p>Some expenses, such as your rent or mortgage, are likely to be fixed, whilst your spending on other items may change from month to month. You need to keep a close eye on the areas where savings can be made - such as leisure or travel. The first year in business is vital, so you may have to accept that a financial sacrifice of some sort is required to keep on trading.</p>
		
