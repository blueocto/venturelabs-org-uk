		<h1>Family-run businesses</h1>
		<h2>Advantages of family businesses</h2>
		<p>If you start or join a family business you're likely to benefit from a range of advantages which you often don't find in other enterprises. These include:</p>
		<ul>
			<li><strong>Common values</strong> - you and your family are likely to share the same ethos and beliefs on how things should be done. This will give you an extra sense of purpose and pride - and a competitive edge for your business.</li>
			<li><strong>Strong commitment</strong> - building a lasting family enterprise means you're more likely to put in the extra hours and effort needed to make it a success. Your family is more likely to understand that you need to take a more flexible approach to your working hours.</li>
			<li><strong>Loyalty</strong> - strong personal bonds mean you and family members are likely to stick together in hard times and show the determination needed for business success.</li>
			<li><strong>Stability</strong> - knowing you're building for future generations encourages the long-term thinking needed for growth and success - though it can also produce a potentially damaging inability to react to change.</li>
			<li><strong>Decreased costs</strong> - family members may be more willing to make financial sacrifices for the sake of the business. For example, accepting lower pay than they would get elsewhere to help the business in the longer term, or deferring wages during a cashflow crisis. You may also find you don't need employers' liability insurance if you only employ close family members.</li>
		</ul>
		
