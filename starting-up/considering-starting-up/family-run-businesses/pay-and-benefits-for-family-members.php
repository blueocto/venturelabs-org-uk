		<h1>Family-run businesses</h1>
		<h2>Pay and benefits for family members</h2>
		<p>Remuneration needn't be a thorny issue. The goal is to have a <strong>remuneration strategy</strong> which is consistent, fair and open.</p>
		<p>Resentment and conflict tend to occur when these three attributes are missing - for example, if family staff members are paid more than non-family employees without good reason.</p>
		<p>Family members who hold shares but who aren't active in the business may also question the remuneration of those who are.</p>
		<h3>Develop a remuneration strategy</h3>
		<ul>
			<li>An individual's pay should be based on their value rather than their personal need. Look at what the market rate is for the job.</li>
			<li>Family members shouldn't be lured into the business with inflated salaries. Likewise, they shouldn't need to endure unreasonably small salaries to prove their loyalty.</li>
			<li>Benefits, bonuses and incentives should be based on set criteria.</li>
			<li>Unreasonably high salaries and phantom jobs shouldn't be used to transfer tax-deductible wealth to family members.</li>
			<li>Post-retirement remuneration plans should be agreed before they come into play.</li>
			<li>Non-family employees doing the same work as family members should receive the same remuneration.</li>
		</ul>
		<p>It's important that your remuneration policy is seen to be <strong>fair and objective</strong>. Write it down, be open about it and review it regularly.</p>
		<p>Read about pay and pay structures in our guide on how to <a href="/employment-and-skills/pay-and-pensions/set-the-right-pay-rates.php">set the right pay rates</a>.</p>
		<p>Advice from an outsider - an HR consultant, for example - can be invaluable in avoiding remuneration disputes.</p>
		
