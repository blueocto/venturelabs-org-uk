		<h1>Family-run businesses</h1>
		<h2>Communication and family businesses</h2>
		<p>Many misunderstandings and potential areas for dispute in family businesses can be avoided if you ensure good communication channels are in place.</p>
		<p>The risks are that:</p>
		<ul>
			<li>family members assume they know what other family members feel or want</li>
			<li>personal ties inhibit honest opinions being expressed</li>
			<li>the head of the family may automatically assume control of the business even if they don't have the best business skills</li>
			<li>one family member ends up dominating the business</li>
			<li>family-member shareholders not active in the business fail to understand the objectives of those who are active, and vice versa</li>
			<li>personal resentments become business resentments, and vice versa</li>
			<li>non-family board or management members feel excluded</li>
		</ul>
		<p>To avoid these pitfalls, you should foster an atmosphere in which open discussion and communication is welcomed and concerns can be voiced without blame being cast.</p>
		<p>There are a number of practical things you can do. You might:</p>
		<ul>
			<li>remove personal issues from business discussions by holding all meetings in a work rather than home environment</li>
			<li>create mechanisms for providing constructive feedback - this can help prevent staff, particularly non-family employees, from feeling demotivated and uninvolved</li>
			<li>arrange occasional away days to discuss the business' strategy and direction</li>
			<li>appoint a non-executive director to the board, or establish an advisory board to provide an impartial viewpoint and help prevent emotions from clouding business decisions</li>
			<li>establish a family constitution creating policies that will guide the family's relationship with the business</li>
		</ul>

