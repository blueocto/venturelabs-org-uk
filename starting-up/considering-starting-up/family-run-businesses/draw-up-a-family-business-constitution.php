		<h1>Family-run businesses</h1>
		<h2>Draw up a family-business constitution</h2>
		<p>One way to successfully manage conflict in a family business is to have a family-business constitution. When well drawn-up, such a document can even <strong>prevent conflict</strong> occurring in the first place.</p>
		<p>A family-business constitution - sometimes known as a family charter - is partly a statement of general principles. It outlines your business' core values and vision, and your family's commitment to them.</p>
		<p>Importantly, it is also a <strong>practical guide</strong> for running the business and a framework you can use to deal with family business issues that have the potential to cause disputes.</p>
		<p>The process of drawing up a family constitution should be <strong>collaborative</strong>, involving everybody with a stake in the business. The document should be regularly reviewed.</p>
		<h3>Structure your constitution</h3>
		<p>A typical family-business constitution might include the following sections:</p>
		<ul>
			<li>business goals, vision and values</li>
			<li>leadership</li>
			<li>management structure</li>
			<li>entry principles for family members</li>
			<li>succession and exit policies</li>
			<li>rights, responsibilities and obligations of family appointments</li>
			<li>rights, responsibilities and obligations of family members not working in the business</li>
			<li>appointment and rights of non-family board members, management and employees</li>
			<li>training, remuneration and appraisal of employees - both family and non-family</li>
			<li>developing the next generation</li>
			<li>involvement of non-executive directors and other outsiders</li>
			<li>communication channels</li>
			<li>dispute-resolution procedures</li>
		</ul>
		
