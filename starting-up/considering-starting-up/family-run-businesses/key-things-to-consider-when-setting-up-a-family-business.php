		<h1>Family-run businesses</h1>
		<h2>Key things to consider when setting up a family business</h2>
		<p>Setting up a family business can be an exciting challenge. Before you go ahead it's important to consider how you'll deal with a number of issues that commonly confront such ventures. Think about how:</p>
		<ul>
			<li>the business' <strong>shares</strong> will be allocated between family members and if there will be non-family shareholders</li>
			<li>to ensure <strong>business decisions</strong> are taken for business reasons, rather than personal ones</li>
			<li>the <strong>roles</strong> and <strong>responsibilities</strong> will differ between family shareholders who are active in the business, those who aren't, and outside shareholders</li>
			<li>to <strong>reward</strong> family members, whether it will be different to remuneration for non-family members and what problems this could cause</li>
			<li>you will cope with <strong>conflict</strong> when it arises</li>
			<li>to communicate with your family when you are also their boss</li>
			<li>to avoid resentment when deciding who will <strong>succeed</strong> you</li>
			<li>to ensure that the family's finances aren't entirely dependent on the business</li>
		</ul>
		
