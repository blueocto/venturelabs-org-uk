		<h1>Starting a business: the basics</h1>
		<h2>Tax and legal issues for start-ups</h2>
		<p>Once you have decided to start a new business, you need to make sure that you have all of the legal formalities in place. This includes choosing the right legal structure to suit your particular circumstances and ambitions as well as registering with HM Revenue and Customs. Should you choose the limited company route you will also need to register with Companies House.</p>
		<p>Depending on your business idea, you may need to seek specialist advice on Intellectual Property Rights (IPR) protection, whether this is to cover copyright, trade marking, design registration or patenting.</p>
		<p>Keeping and maintaining accurate records as well as ensuring that you pay tax and National Insurance are essential requirements when you set up in business or go self employed.</p>
