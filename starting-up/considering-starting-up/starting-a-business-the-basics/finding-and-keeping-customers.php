		<h1>Starting a business: the basics</h1>
		<h2>Finding and keeping customers</h2>


		<p>When you start up your new business or become self employed, you will need to have a clear understanding of what an ideal or typical customer looks like. You will also require a way of identifying groups of customers with a similar profile.</p>
		<p>Understanding target customers and their needs is a key aspect of your market research. Developing a marketing and sales plan is an important next step in planning how you will reach these customers through promotional activities while also enabling you to budget for the cost.</p>
		<p>Once you have gained new customers it is vital that you build a long-term relationship with them so that you can enjoy their repeat business. It is significantly easier and cheaper to do business with existing profitable customers than to find new ones.</p>
		<p>Retaining valuable customers also requires you to understand in detail the effectiveness of your customer service as seen from the customer's point of view. This customer feedback on your performance will help you to make informed decisions when making adjustments to your offer. It also increases the likelihood that your customers will talk about you positively to others and make referrals.</p>
