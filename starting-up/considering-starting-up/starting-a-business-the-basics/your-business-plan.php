		<h1>Starting a business: the basics</h1>
		<h2>Your business plan</h2>
		<p>Planning and thinking about your business is a necessary process to undertake before, during and after you start. The headings in a business plan can be simply thought of as a checklist of questions you need to ask to reassure yourself that your venture will work. Writing the plan down is just as important because it helps to clarify your thinking and demonstrates your commitment to move forward. It also identifies where you intend to get to and how you intend to get there.</p>
		<p>Other benefits include the chance for you to focus your mind on how you intend to run your business and to identify early on any areas or issues that you may have missed. You will also be able to review your progress against your forecast and make any necessary adjustments to get you back on track. Having a clearly presented document will also make it easier for any specialist you may use to provide support. In the case of raising funds from a bank or investor a business plan will be a necessary requirement.</p>
		<p>Although the structure of business plans do vary, they will typically include an overview, a short description of the business idea and opportunity, what makes it different, who will be involved in the business, how you will provide your product or service, your marketing and sales strategy, and financial forecasts that will show the expected profitability.</p>
