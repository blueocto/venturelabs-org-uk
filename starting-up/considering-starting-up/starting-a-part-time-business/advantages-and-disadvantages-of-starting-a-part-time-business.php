		<h1>Starting a part-time business</h1>
		<h2>Advantages and disadvantages of starting a part-time business</h2>
		<p>There are advantages and disadvantages to running a business on a part-time basis.</p>
		<p>Advantages include:</p>
		<ul>
			<li>you are able to stay in paid work and have the security of a regular income until the business is up and running</li>
			<li>you can use skills you have learned working for an employer in starting and building your own business</li>
			<li>if you have obligations such as caring for dependents, it is a way of having an income</li>
			<li>you can test whether there is a market for your products or services without a major financial commitment</li>
			<li>if you have a hobby that you would spend time on outside work anyway, you may be able to make money from your interests</li>
		</ul>
		<p>However, disadvantages include:</p>
		<ul>
			<li>finding the time to run a business can be difficult and it may take up more time than you have available</li>
			<li>you may find it hard to concentrate on paid work if the business has a problem</li>
			<li>you may have to put in long hours, which can cause stress</li>
			<li>it might take a long time for the new business to develop and become viable</li>
			<li>if you stay in work, you'll have to pay tax on both sets of income, which may mean the business is not financially viable</li>
		</ul>

