		<h1>Common mistakes when starting up - and how to avoid them</h1>
		<h2>Taking your eye off the competition</h2>
		<p>During the busy start-up phase it can be easy to forget to set aside enough time to monitor the competition. However, it's essential that you are ready to respond to competitors in your market place and to new developments.</p>
		<h3>Failing to actively monitor the competition</h3>
		<p>Failing to monitor your rivals will stop you from seeing what competition or threats to your business exist in your market place.</p>
		<p>Competition is not just another business that might take money away from you. It can be another product or service that's being developed which you ought to be selling or looking to license before somebody else takes it up.</p>
		<p>You can get clues to the existence of competitors from:</p>
		<ul>
			<li>advertising</li>
			<li>press reports</li>
			<li>exhibitions and trade fairs</li>
			<li>questionnaires</li>
			<li>searching on the web for similar products or services</li>
			<li>approaches reported by your customers</li>
			<li>flyers and marketing literature that have been sent to you - this is quite common if you're on a bought-in marketing list</li>
			<li>planning applications and building work in progress</li>
		</ul>
		<p>For more information, see our guide on how to <a href="/sales-and-marketing/marketing/understand-your-competitors.php">understand your competitors</a>.</p>
		<h3>Failing to act on competitors' information</h3>
		<p>Failing to use information gathered about your competitors will weaken your position in the market.</p>
		<p>Feed any useful information into your marketing plan. Your marketing plan and research will help you to set realistic targets and deadlines, and allocate appropriate resources. You can then decide to focus on building relationships with your existing clients or attract new customers. Your marketing can then be turned into sales by deciding on your sales methods.</p>
		<p>For more information, see our guide on how to <a href="/sales-and-marketing/marketing/write-a-marketing-plan.php">write a marketing plan</a>.</p>

