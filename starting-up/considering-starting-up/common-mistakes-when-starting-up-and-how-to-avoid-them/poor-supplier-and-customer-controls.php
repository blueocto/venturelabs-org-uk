		<h1>Common mistakes when starting up - and how to avoid them</h1>
		<h2>Poor supplier and customer controls</h2>
		<p>Common mistakes for new businesses include setting up unsatisfactory credit arrangements and not taking due care when choosing suppliers. Choose carefully as your business' profitability and reputation could be at stake.</p>
		<h3>Setting up poor supplier contracts</h3>
		<p>Finding a reliable and competitively priced supplier can be vital to the success of your business. This is because you rely on your suppliers to provide you with the goods and services your business needs to operate. And getting the best deals can have a significant effect on your business' profits.</p>
		<p>When selecting your suppliers, price is an obvious concern. However, other factors such as value for money, quality, reliability and service must also be taken into consideration.</p>
		<p>Establish exactly what you are looking for in a supplier. Carry out a credit check to ensure that the supplier can deliver what you need and is not about to fold. Once you have identified your chosen supplier, you can then discuss terms and conditions and draw up a formal contract.</p>
		<p>For more information, see our guides on how to <a href="/grow-your-business/growth-through-strategic-sourcing/manage-your-suppliers.php">manage your suppliers</a> and <a href="/grow-your-business/growth-through-strategic-sourcing/negotiate-the-right-deal-with-suppliers.php">negotiate the right deal with suppliers</a>, and our guide on <a href="/grow-your-business/growth-through-strategic-sourcing/choosing-the-right-suppliers.php">choosing the right suppliers</a>.</p>
		<h3>Setting up poor credit arrangements</h3>
		<p>If you are dealing with a potential new customer, it can be tempting to offer credit without carrying out checks. But this can leave your business exposed to delayed or non-payment. You may find that you cannot pay your suppliers or bank on time. In turn, they may withdraw their supplies or funds, putting your business at risk.</p>
		<p>To avoid potential problems with customer payments, you may want to:</p>
		<ul>
			<li>carry out credit checks on new and existing customers</li>
			<li>check bank references, trade references and online credit-ratings, from a credit-reference agency</li>
			<li>ensure that your customer is aware of your credit terms (eg they must pay within 30 days) and that the payment terms for your debtors are longer than the terms you offer your customers</li>
			<li>motivate customers to make early payments by offering discounts</li>
			<li>investigate legally enforceable ways of encouraging prompt payment</li>
		</ul>
		<p>See our guide on <a href="/finance-and-grants/debt-recovery/ensuring-customers-pay-you-on-time.php">managing late payment</a>.</p>

