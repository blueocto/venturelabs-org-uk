		<h1>Common mistakes when starting up - and how to avoid them</h1>
		<h2>Poor stock and asset management</h2>
		<p>Poor stock control and over-investment in fixed assets can mean your capital is tied up unnecessarily.</p>
		<h3>Poor stock control</h3>
		<p>Efficient stock control (inventory) will mean you have the right amount of stock in the right place at the right time. It ensures that capital is not tied up unnecessarily, and protects production when there are problems with the supply chain.</p>
		<p>You need to put systems in place to keep close track of stock levels and values. Taking control will allow you to free up cash, while also having the right amount of stock on hand.</p>
		<p>There are a number of ways you can approach stock control. You can:</p>
		<ul>
			<li>re-order when stock reaches a minimum level</li>
			<li>carry out regular reviews of stock</li>
			<li>use <strong>just in time</strong> (JIT) delivery to avoid excessive stock build up</li>
		</ul>
		<p>See our guide on <a href="/starting-up/find-and-manage-the-money/stock-control-and-inventory.php">stock control and inventory</a>.</p>
		<h3>Over-investing in fixed assets</h3>
		<p>In the early years of your new business, you need to limit drawing on your cash reserves unnecessarily. Over-investment in fixed assets, such as office furniture or computer equipment, can be a problem. Acquiring fixed assets outright gives you ownership straightaway, but you have to pay for the full cost upfront, which drains cash.</p>
		<p>The alternatives include:</p>
		<ul>
			<li><strong>Leasing assets</strong> - at least while your business finds its feet. This allows you to spread payments in regular instalments over a fixed period, thus freeing up more cash. You may be able to upgrade equipment without having to buy more up-to-date models.</li>
			<li><strong>Hire purchase</strong> - you own the asset at the end of the payment process. This is not the case with leasing.</li>
			<li><strong>Buying second hand</strong> - for office furniture, fittings, etc. You can find second hand furniture in a number of places. For example, check your local press and local auctions. Green Works is a registered charity that recycles and sells on used office furniture. <a href="#">Find second hand furniture on the Green Works website</a>.</li>
		</ul>
		<p>See our guide on how to <a href="/finance-and-grants/manage-your-cashflow/decide-whether-to-lease-or-buy-assets.php">decide whether to lease or buy assets</a>.</p>

