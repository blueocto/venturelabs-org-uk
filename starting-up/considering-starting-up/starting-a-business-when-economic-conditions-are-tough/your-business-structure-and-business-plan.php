		<h1>Starting a business when economic conditions are tough</h1>
		<h2>Your business structure and business plan</h2>
		<p>You need to decide what legal structure will best suit your business. Carefully consider the advantages and disadvantages of each option because the structure will affect:</p>
		<ul>
			<li>the tax and National Insurance you have to pay</li>
			<li>the records and accounts you have to keep</li>
			<li>the way management decisions are made</li>
		</ul>
		<p>Some of the options are:</p>
		<ul>
			<li>Sole trader - The simplest way to run a business. Accounts are straightforward and profits belong to you, however you are also personally liable for any debts.</li>
			<li>Limited liability company - Including private limited companies. You must comply with strict regulations, but can raise money through shareholders. The company's finances are separate from the personal finances of their owners.</li>
			<li>Partnership - Two or more people share the responsibilities of running the business, and take equal share of both profits and debts.</li>
			<li>Limited liability partnership - Similar to a partnership but liability is limited to what each partner has invested and any personal guarantees they have given.</li>
		</ul>
		<p>Other company formats include franchises, social enterprises and co-operatives.</p>
		<p>For guidance on deciding which structure would best suit your business, see our videos and e-learning resources to <a href="https://www.gov.uk/business-legal-structures" target="_blank" rel="noopener noreferrer">choose the right business model and legal structure</a>.</p>
		<h3>Creating a business plan</h3>
		<p>A business plan describes the business' objectives, its strategies, the market it is in and its financial forecasts.</p>
		<p>Going through the process of writing the plan should focus your mind on everything from where the business will be based to where you are going to find your first customer.</p>
		<p>It is also a blueprint for the future development of the business that potential investors will want to read before they lend to you.</p>
		<p>Your business plan should include:</p>
		<ul>
			<li>an executive summary</li>
			<li>a description of the business</li>
			<li>a marketing and sales strategy - who you think your customers are going to be and how you are going to reach them</li>
			<li>information on your management team and personnel</li>
			<li>information on your operations, including your business premises, your IT and management information systems</li>
			<li>financial forecasts - how much you need to start the business and where you are going to get this money from, how much you think the business is going to make and what salary you plan to take, etc</li>
		</ul>
		<p>See our videos and e-learning resources to <a href="https://www.gov.uk/write-business-plan" target="_blank" rel="noopener noreferrer">create your business plan</a>.</p>
		<p>After the business plan, you should consider writing a marketing plan, which sets out how you will target and interest potential customers in your product or service. See our guide on <a href="/sales-and-marketing/marketing/write-a-marketing-plan.php" title="How to identify your objectives and write a plan that will help your marketing generate sales">how to write a marketing plan</a>.</p>
		<p>Business support organisations can help with setting out your marketing and business plans and you may also be able to attend start-up events or courses which cover writing a marketing plan as part of their content.</p>
		
