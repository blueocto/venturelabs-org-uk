		<h1>Starting a business when economic conditions are tough</h1>
		<h2>Develop an effective survival strategy</h2>
		<p>It's never too early to identify risks and learn how to address and manage them.</p>
		<h3>Risk management</h3>
		<p>It is important to identify the key risks your business may face and develop strategies to eliminate or reduce them. This will make your business more flexible and more able to withstand volatile market conditions.</p>
		<p>Common risks include:</p>
		<ul>
			<li>losing customers and failing to attract new ones</li>
			<li>increased competition</li>
			<li>poor cashflow</li>
			<li>failure to anticipate problems/inability to adapt to changing market environment</li>
		</ul>
		<p>For more information, see our guide on <a href="/grow-your-business/managing-risk.php">managing risk</a>.</p>
		<h3>Customers and competitors</h3>
		<p>You must have a clear strategy for identifying and looking after key customers and for developing relationships with them. You should try to plan for worst-case scenarios, such as losing a major customer, and expand your customer base as quickly as possible to reduce the impact such a loss would have on your business.</p>
		<p>You also need to consider potential opportunities that could arise, for example, if one of your competitors ceases trading.</p>
		<p>Read our guides on how to <a href="/sales-and-marketing/keeping-your-customers/manage-your-customer-care.php">manage your customer care</a> and <a href="/sales-and-marketing/keeping-your-customers/retain-and-grow-your-customer-base.php">retain and grow your customer base</a>.</p>
		<h3>Keep your cashflow healthy</h3>
		<p>Cashflow is the balance of money entering and leaving a business. It is important to anticipate cashflow problems as early as possible, so that solutions can be found before the problem becomes too serious.</p>
		<p>At all times you should know how much money your business has in the bank, how much it owes and how much it is owed. If you regularly update your financial records and develop forecasts showing likely sales, profit and loss, you should be able to identify when additional funds might be required.</p>
		<p><a href="http://www.cicm.com/resources/cashflow-guides/" target="_blank" rel="noopener noreferrer">Find out about good cashflow management on the Department for Business, Innovation and Skills (BIS) website</a>.</p>
		<p>If you anticipate serious cashflow or funding problems in the early stages of your business, seek advice from your accountant or your bank.</p>
		<p>For more advice, read our guides: <a href="/finance-and-grants/manage-your-cashflow/cashflow-management-the-basics.php">cashflow management: the basics</a> and <a href="/finance-and-grants/manage-your-cashflow/identify-potential-cashflow-problems.php">identify potential cashflow problems</a>.</p>
		
