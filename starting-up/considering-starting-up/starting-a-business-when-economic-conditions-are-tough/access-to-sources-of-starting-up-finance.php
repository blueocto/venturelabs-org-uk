		<h1>Starting a business when economic conditions are tough</h1>
		<h2>Access to sources of starting-up finance</h2>
		<p>If you need to raise money to start up your business, there are various options, including:</p>
		<p>
			<ul>
				<li><strong>Using personal money</strong> - such as savings, redundancy payments or a pension windfall.</li>
				<li><strong>Approaching a bank or business investor for a business loan</strong> - this may be more difficult during a credit crunch. Any lender will want to review a detailed business plan before loaning any funds. See our guide on how to <a href="/finance-and-grants/prepare-to-raise-finance/how-to-attract-investment/use-your-business-plan-to-get-funding.php">use your business plan to get funding</a>.</li>
				<li><strong>Applying for a grant</strong> - there are many business grant schemes nationwide, geared to different industry sectors and project scales. You should be aware that applying for a grant may take some time. <a href="https://www.gov.uk/growing-your-business" target="_blank" rel="noopener noreferrer">Search our business support finder for grants, loans, expertise and advice for which your business may be eligible</a>.</li>
			</ul>
		</p>
		<p>Essential to raising finance is convincing investors or grant bodies that your business is sound. This means showing that your idea is worth investing in and that you have what it takes to run your own business successfully.</p>
		<h3>Top five tips on getting finance</h3>
		<ul>
			<li>Make sure you have a realistic business proposal. Have you calculated the money needed to start up?</li>
			<li>Think about and plan your ongoing budget</li>
			<li>Start to plan your finances by thinking about costs, living expenses and sales targets</li>
			<li>Make time to do your market research. It helps to show you are serious about your business</li>
			<li>Be prepared for probing questions</li>
		</ul>
		<h3>Loans and grants</h3>
		<p>Banks are not the only option when it comes to business loans. You could also explore the following:</p>
		<ul>
			<li><strong>Angel investors</strong>, or business angels, usually provide funds in exchange for a small stake. They typically invest between &pound;10,000 and &pound;750,000 and are often just as valuable for their contacts, insight and experience as for the investment they can offer.</li>
			<li><strong>Venture capitalists</strong> may demand a large percentage of your business in exchange for investment. Ensure you have a clear understanding of their expectations.</li>
		</ul>
		<p>Find out more about angel investors and venture capitalists in our guide on <a href="/finance-and-grants/finance-options/shares-and-equity-finance/equity-finance.php">equity finance</a>.</p>
		<p>The <strong>Enterprise Finance Guarantee</strong> aims to help smaller credit-worthy companies that are unable to obtain a conventional loan because of current tight lending conditions. <a href="https://www.gov.uk/government/policies/business-enterprise?keywords=%22finance+guarantee%22&public_timestamp%5Bfrom%5D=&public_timestamp%5Bto%5D=" target="_blank" rel="noopener noreferrer">Find out more about the Enterprise Finance Guarantee on the Department for Business, Innovation &amp; Skills (BIS) website</a>.</p>
		<p>See our guide on <a href="/finance-and-grants/finance-options/borrowing/bank-finance.php">bank finance</a>.</p>
		
