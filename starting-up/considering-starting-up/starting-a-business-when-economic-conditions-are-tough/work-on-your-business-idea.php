		<h1>Starting a business when economic conditions are tough</h1>
		<h2>Work on your business idea</h2>
		<p>A good business idea is essential to setting up a successful business. You can use what you know about the volatile financial climate to help you develop a targeted and specific business idea.</p>
		<h3>Your business idea</h3>
		<p>If you don't already have an idea, consider which of your skills could be used as a base for starting a business. See our videos and e-learning resources to <a href="https://www.gov.uk/set-up-business" title="Find out about researching your chosen market and how to generate or develop a new business idea" target="_blank" rel="noopener noreferrer">develop your business idea</a>.</p>
		<p>Some types of business are more likely to succeed in a volatile financial climate than others. If you want to start up a business but you haven't decided which area you'd like to work in, find out what sorts of business are thriving, and what about them makes them 'recession-resistant'. You could also look into:</p>
		<ul>
			<li>the 'essentials' markets - eg food and house maintenance</li>
			<li>the affordable luxuries market - in an economic downturn, the market aimed at people aged over 50 may still be growing, as they may have equity in their property and savings</li>
			<li>existing businesses - not only is buying a business which already has established revenue a safer option, but you might get a bargain in difficult economic times</li>
			<li>franchises - an established franchise stands a good chance of surviving when economic conditions are tough as they have experience and time-honed, streamlined systems and processes</li>
		</ul>
		<p>Once you have a business idea, you should research whether there is a market for your product or service and formulate a business plan. If you need start-up capital, lenders will usually request a business plan.</p>
		<h3>Test your business idea</h3>
		<p>You can use the economic downturn as an opportunity to define what you and your business want to achieve - and how best to set about achieving this. Ensure that the customer is central to your business idea. Consider whether they would really buy your product or service, particularly in the context of the current market.</p>
		<p>Difficult market conditions are often caused by very specific issues, such as an unavailability of loan finance. Diagnosing these issues and seeing to what extent they affect you will allow you to rethink the nature and scale of, as well as the market for, your business.</p>
		<p>It is worth looking at the business pages of newspapers for information about the issues involved in an economic downturn and their effect on businesses. Understanding these issues will help you develop strategies for coping with them.</p>
		<p>Once you have tweaked your business idea for current market conditions, it needs to be implemented through a full business plan and a decision on the legal structure of your business.</p>
		
