		<h1>Rules for naming your limited company or limited liability partnership (LLP)</h1>
		<h2>Choosing a name for a company or LLP</h2>
		<p>There are several rules to consider when choosing a name for your company or LLP.</p>
		<p>You will need to check that it is not the 'same as' that of an existing company or LLP. A 'same as' name may not be identical to an existing company name, but if it is very similar it could cause confusion. For example, if a company called 'Smith Transport Services Limited' already exists, you could not register 'Smith Transport Services UK Limited', unless the two companies are part of the same group and other conditions are satisfied.</p>
		<p>To check whether a company or LLP name is already registered, <a href="https://www.gov.uk/get-information-about-a-company" target="_blank" rel="noopener noreferrer">use our interactive tool to search for an available company name and trade mark</a>.</p>
		<h3>Required words</h3>
		<p>Limited companies and LLPs are required to include certain words in their names:</p>
		<ul>
			<li>private limited company names must end with 'Limited or 'Ltd'</li>
			<li>private unlimited company names may end with 'unlimited' but are not required to do so</li>
			<li>public limited company names must end with 'public limited company' or 'plc', in capital or lower-case with or without full stops</li>
			<li>LLP names must end with 'limited liability partnership' or 'LLP'</li>
		</ul>
		<p>The names of limited companies registered in Wales may end with the Welsh equivalent of 'Limited', 'public limited company' or their abbreviations.</p>
		<h3>When will Companies House refuse to register a company name?</h3>
		<p>Companies House will not register a name if:</p>
		<ul>
			<li>its use would constitute an offence or is offensive</li>
			<li>it implies a connection with central or local government, or with some public authorities</li>
			<li>it includes a 'sensitive' word or expression - see the page in this guide on <a href="/starting-up/name-your-business/rules-for-naming-your-limited-company-or-limited-liability-partnership-llp/prescribed-and-sensitive-words.php">prescribed and sensitive words</a></li>
			<li>it includes characters, signs, symbols and punctuation which are not permitted</li>
			<li>it is identical to, or the same as, another name appearing on the index of company names</li>
			<li>it uses any of the name endings 'limited', 'ltd', 'unlimited', 'public limited company' or 'plc' in a way that misleads or causes confusion</li>
		</ul>

