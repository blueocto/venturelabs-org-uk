		<h3>Opportunistic registrations</h3>
		<p>If a company has registered a name with the main objective of getting money or other consideration from an individual or another company, they can be ordered to change the name. <a href="https://www.gov.uk/government/organisations/company-names-tribunal" target="_blank" rel="noopener noreferrer">Find guidance on opportunistic registrations on the Intellectual Property Office (IPO) website</a>.</p>
		
