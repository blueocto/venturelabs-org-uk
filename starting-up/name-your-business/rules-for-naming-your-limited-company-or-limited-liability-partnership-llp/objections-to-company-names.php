		<h1>Rules for naming your limited company or limited liability partnership (LLP)</h1>
		<h2>Objections to company names</h2>
		<p>Once the name of your company or LLP has been registered, there is usually no need to change it. However, there are some exceptions. You may need to change your company or LLP name if:</p>
		<ul>
			<li>there is an objection, upheld by the Secretary of State, that the name is 'too like' another business' name</li>
			<li>within five years of registration it is found that misleading information was given at the time of registration</li>
			<li>within five years of registration any conditions attached to the registration - eg the provision of support documentation for a sensitive name - have not been fulfilled</li>
			<li>someone objects that the company or LLP name is the same as one in which they have goodwill and that the reason for registration was 'opportunistic'</li>
			<li>the name is misleading and as a result may cause harm to the public</li>
		</ul>
		<h3>What is 'too like'?</h3>
		<p>If a company name is found to be very similar to that of an existing company name - eg if the names differ in a minor way - they are likely to be 'too like'. Most names that are 'too like' could cause some confusion, such as the names A &amp; B Consultants Limited and A &amp; B Consulting Limited.</p>
		<p>If names differ by one or more words, they will usually be acceptable, unless the names have very distinctive elements in common, and differ only by words such as 'services' or 'trading'.</p>
		<h3>Objecting to a name</h3>
