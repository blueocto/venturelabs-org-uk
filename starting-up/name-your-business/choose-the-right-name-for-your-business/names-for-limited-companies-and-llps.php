		<h1>Choose the right name for your business</h1>
		<h2>Names for limited companies and LLPs</h2>
		<p>If you have decided to form a limited company or limited liability partnership (LLP), you must register your name and other details with Companies House.</p>
		<p>It is essential to check first that your proposed name does not breach the rules on name endings, 'same as' rules or include a prescribed or sensitive word without permission.</p>
		<h3>Company and LLP names - the rules</h3>
		<p>To make sure the name you choose is acceptable, work through this list before you send your application to Companies House. Ensure that your name:</p>
		<ul>
			<li>ends with 'limited' (or Ltd), 'public limited company' (or plc) or Welsh equivalents if you are a company</li>
			<li>if you have a limited liability partnership your name ends with 'limited liability partnership' or LLP (including Welsh equivalents) if you are an LLP</li>
			<li>isn't offensive</li>
			<li>isn't the same as one already on the index of company names</li>
			<li>doesn't include any sensitive words or expressions - unless you have obtained permission to use them</li>
		</ul>
		<p>For more information, see our guide on <a href="/starting-up/name-your-business/rules-for-naming-your-limited-company-or-limited-liability-partnership-llp.php">rules for naming your limited company or limited liability partnership (LLP)</a>.</p>
		<p>For an explanation of how to register with Companies House, see our guide on how to <a href="/starting-up/business-structures/set-up-and-register-a-limited-company-private-or-public.php">set up and register a limited company (private or public)</a>.</p>
		<h3>Trade marks</h3>
		<p>You should ensure your proposed name is not the same or very similar to a registered trade mark.</p>
		<p><a href="https://www.gov.uk/get-information-about-a-company" target="_blank" rel="noopener noreferrer">Use our interactive tool to search for an available company name and trade mark</a>.</p>
		<h3>Complaints about company or LLP names</h3>
		<p>You can make a complaint about a company or LLP name to Companies House if:</p>
		<ul>
			<li>the name is too similar to an existing company or LLP name</li>
			<li>within five years of registration, it is found that misleading information was given at the time of registration</li>
			<li>within five years of registration any conditions attached to the registration have not been fulfilled - eg the provision of support documentation for a sensitive name</li>
			<li>the name is misleading and as a result may cause harm to the public</li>
		</ul>
		<p><a href="https://www.gov.uk/government/publications/incorporation-and-names#ch4" target="_blank" rel="noopener noreferrer">Find out how to complain about company names on the Companies House website</a>.</p>
		<p>You can also make a complaint about a company or LLP name to the <strong>Company Names Tribunal</strong> at the Intellectual Property Office if you believe the name has been chosen for opportunistic reasons.</p>
		<p><a href="https://www.gov.uk/government/organisations/company-names-tribunal" target="_blank" rel="noopener noreferrer">Find out about the Company Names Tribunal on the Intellectual Property Office website</a>.</p>
		<p><a href="https://www.gov.uk/get-information-about-a-company" target="_blank" rel="noopener noreferrer">Use our interactive tool to search for an available company name and trade mark</a>.</p>
		
