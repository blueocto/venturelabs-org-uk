		<h1>Choose the right name for your business</h1>
		<h2>Use of sensitive words and expressions in business names</h2>
		<p>There are some words and expressions that you can't use in a business name unless you have official permission. These are words that might give a false impression about your business. They are known as <b>sensitive</b> words.</p>
		<p>The rules about sensitive words apply to all types of businesses and fall into five main groups:</p>
		<p><strong>Sensitive words</strong></p>
		
		<table>
			<tbody>
				<tr>
					<th scope="col"><strong>Type of expression</strong></th>
					<th scope="col"><strong>Examples</strong></th>
				</tr>
				<tr>
					<th>Words that suggest your business is of national importance or status</th>
					<td>British, National, International, European</td>
				</tr>
				<tr>
					<th>Words that suggest a special status</th>
					<td>Association, Authority, Chartered, Council, Institute, Society</td>
				</tr>
				<tr>
					<th>Words that suggest a particular function</th>
					<td>Charity, Insurance, Register, Trust</td>
				</tr>
				<tr>
					<th>Words that suggest a specialised activity</th>
					<td>Health Centre</td>
				</tr>
				<tr>
					<th>Words that suggest connections with government or royalty</th>
					<td>Parliament, Government, Royal, Queen, Prince</td>
				</tr>
			</tbody>
		</table>

