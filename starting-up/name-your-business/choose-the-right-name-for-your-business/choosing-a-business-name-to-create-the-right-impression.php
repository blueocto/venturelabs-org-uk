		<h1>Choose the right name for your business</h1>
		<h2>Choosing a business name to create the right impression</h2>
		<p>When generating ideas about a business name, you may initially focus on personal preference. However, an objective approach will enable you to consider the customer first.</p>
		<p>Your business name will be the cornerstone of your brand. It should work well wherever you use it - on the phone, in your logo, signage, stationery, advertisements, website, email and any other media you plan to use to reach the market. See our guide on <a href="/sales-and-marketing/advertising-branding-and-design/branding-the-basics.php">branding: the basics</a>.</p>
		<h3>Points to help you decide on a name for your business</h3>
		<p>When choosing a name for your business, you need to ask:</p>
		<ul>
			<li>Do you want the name to reflect what your business does - moving, cleaning, building? Or would something more abstract be suitable?</li>
			<li>Would it be a good idea to include your own name?</li>
			<li>Do you want a traditional-sounding name, conveying durability and old-fashioned values, or a modern name, suggesting a fresh, innovative approach?</li>
			<li>Think about the future - avoid words or phrases that are likely to date quickly.</li>
			<li>If you're likely to be trading overseas, check that the name doesn't mean anything inappropriate in the relevant languages.</li>
			<li>Think about callers and customers - avoid very long names, strange wordings and unusual spelling. If you're planning to advertise in directories such as the Yellow Pages, think about using a name that appears near the beginning of the listings for your type of business.</li>
			<li>If you're focusing on the local market for your product or service, think about using the name of the city or town in the business name.</li>
			<li>Keep your trading name creative, but your corporate name bland. This will give you the flexibility to develop other brands and trading names in the future.</li>
		</ul>
		<p>Note that there are rules that could affect your choice of business name. For more information on business names, see our guide on <a href="/starting-up/name-your-business/rules-for-naming-your-limited-company-or-limited-liability-partnership-llp.php">rules for naming your limited company or limited liability partnership (LLP)</a>.</p>
		
