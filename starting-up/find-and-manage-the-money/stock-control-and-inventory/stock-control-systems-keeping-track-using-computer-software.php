		<h1>Stock control and inventory</h1>
		<h2>Stock control systems - keeping track using computer software</h2>
		<p>Computerised stock control systems run on similar principles to manual ones, but are more flexible and information is easier to retrieve. You can quickly get a stock valuation or find out how well a particular item of stock is moving.</p>
		<p>A computerised system is a good option for businesses dealing with many different types of stock. Other useful features include:</p>
		<ul>
			<li>Stock and pricing data integrating with accounting and invoicing systems. All the systems draw on the same set of data, so you only have to input the data once. <strong>Sales Order Processing</strong> and <strong>Purchase Order Processing</strong> can be integrated in the system so that stock balances and statistics are automatically updated as orders are processed.</li>
			<li>Automatic stock monitoring, triggering orders when the re-order level is reached.</li>
			<li>Automatic batch control if you produce goods in batches.</li>
			<li>Identifying the cheapest and fastest suppliers.</li>
			<li>Bar coding systems which speed up processing and recording. The software will print and read bar codes from your computer.</li>
			<li>Radio Frequency Identification (RFID) which enables individual products or components to be tracked throughout the supply chain. See the page in this guide on <a href="/starting-up/find-and-manage-the-money/stock-control-and-inventory/using-rfid-for-inventory-control-stock-security-and-quality-management.php">using RFID for inventory control, stock security and quality management</a>.</li>
		</ul>

		<p>The system will only be as good as the data put into it. Run a thorough <strong>stocktake</strong> before it goes 'live' to ensure accurate figures. It's a good idea to run the previous system alongside the new one for a while, giving you a back-up and enabling you to check the new system and sort out any problems.</p>
		
		<h3>Choose a system</h3>
		<p>There are many software systems available. Talk to others in your line of business about the software they use, or contact your trade association for advice.</p>
		<p>Make a checklist of your requirements. For example, your needs might include:</p>
		<ul>
			<li>multiple prices for items</li>
			<li>prices in different currencies</li>
			<li>automatic updating, selecting groups of items to update, single-item updating</li>
			<li>using more than one warehouse</li>
			<li>ability to adapt to your changing needs</li>
			<li>quality control and batch tracking</li>
			<li>integration with other packages</li>
			<li>multiple users at the same time</li>
		</ul>

		<p>Avoid choosing software that's too complicated for your needs as it will be a waste of time and money.</p>
		
