		<h1>Stock control and inventory</h1>
		<h2>Using RFID for inventory control, stock security and quality management</h2>
		<p>Radio Frequency Identification (RFID) allows a business to identify individual products and components, and to track them throughout the supply chain from production to point-of-sale.</p>
		<p>RFID is a technology that uses radio waves for communication between a tag (data carrier or RF transponder) and a reading device. The tag usually consists of a microchip attached to an antenna. The reader (or interrogator) is usually capable of reading data from and writing data to the tag.</p>
		<p>An RFID tag is a tiny microchip, plus a small aerial, which can contain a range of digital information about a particular item. Tags are encapsulated in plastic, paper or similar material, and fixed to the product or its packaging, to a pallet or container, or even to a van or delivery truck.</p>
		<p>The tag is interrogated by an RFID reader which transmits and receives radio signals to and from the tag. Readers can range in size from a hand-held device to a 'portal' through which several tagged devices can be passed at once, eg on a pallet. The information that the reader collects is collated and processed using special computer software. Readers can be placed at different positions within a factory or warehouse to show when goods are moved, providing continuous inventory control.</p>
		<p>Using RFID tagging for stock control offers several advantages over other methods such as barcodes:</p>
		<ul>
			<li>tags can be read remotely, often at a distance of several metres</li>
			<li>several tags can be read at once, enabling an entire pallet-load of products to be checked simultaneously</li>
			<li>tags can be given unique identification codes, so that individual products can be tracked</li>
			<li>certain types of tag can be overwritten, enabling information about items to be updated, eg when they are moved from one part of a factory to another</li>
		</ul>

		<p>RFID tagging can be used:</p>
		<ul>
			<li>to prevent over-stocking or under-stocking a product or component</li>
			<li>for stock security, by positioning tag-readers at points of high risk, such as exits, and causing them to trigger alarms</li>
			<li>for quality control, particularly if you make or stock items with a limited shelf life</li>
		</ul>

		<p>The costs associated with RFID tagging have fallen over recent years, and continue to do so, to bring the process within the reach of more and more businesses. The benefits of more efficient stock control and improved security make it particularly attractive to retailers, wholesalers or distributors who stock a wide range of items, and to manufacturers who produce volume runs of products for different customers.</p>
		<p>You can <a href="http://www.rfidc.com/docs/introductiontorfid.htm" target="_blank" rel="noopener noreferrer">find out about RFID on the RFID Centre website</a>.</p>
		
