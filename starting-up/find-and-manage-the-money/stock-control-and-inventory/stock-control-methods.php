		<h1>Stock control and inventory</h1>
		<h2>Stock control methods</h2>
		<p>There are several methods for controlling stock, all designed to provide an efficient system for deciding what, when and how much to order.</p>
		<p>You may opt for one method or a mixture of two or more if you have various types of stock. For further information, see the page in this guide on <a href="/starting-up/find-and-manage-the-money/stock-control-and-inventory/types-of-stock.php">types of stock</a>.</p>
		<ul>
			<li><b>Minimum stock level</b> - you identify a minimum stock level, and re-order when stock reaches that level. This is known as the Re-order Level.</li>
			<li><b>Stock review</b> - you have regular reviews of stock. At every review you place an order to return stocks to a predetermined level.</li>
		</ul>

		<p><strong>Just In Time (JIT)</strong> - this aims to reduce costs by cutting stock to a minimum - see our guide on how to <a href="/finance-and-grants/financial-planning-and-accounts/avoid-the-problems-of-overtrading.php">avoid the problems of overtrading</a>. Items are delivered when they are needed and used immediately. There is a risk of running out of stock, so you need to be confident that your suppliers can deliver on demand.</p>
		<p>These methods can be used alongside other processes to refine the stock control system. For example:</p>
		<p><b>Re-order lead time</b> - allows for the time between placing an order and receiving it.</p>
		<p><b>Economic Order Quantity</b> (EOQ) - a standard formula used to arrive at a balance between holding too much or too little stock. It's quite a complex calculation, so you may find it easier to use stock control software. <a href="http://www.inventoryops.com/economic_order_quantity.htm" target="_blank" rel="noopener noreferrer">Find out about the EOQ formula on the InventoryOps website</a>.</p>
		<p><b>Batch control</b> - managing the production of goods in batches. You need to make sure that you have the right number of components to cover your needs until the next batch.</p>
		<p>If your needs are predictable, you may order a <b>fixed quantity</b> of stock every time you place an order, or order at a <b>fixed interval</b> - say every week or month. In effect, you're placing a standing order, so you need to keep the quantities and prices under review.</p>
		<p><b>First in, first out</b> - a system to ensure that perishable stock is used efficiently so that it doesn't deteriorate. Stock is identified by date received and moves on through each stage of production in strict order.</p>
		
