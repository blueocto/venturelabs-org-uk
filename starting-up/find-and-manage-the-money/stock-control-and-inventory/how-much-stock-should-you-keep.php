		<h1>Stock control and inventory</h1>
		<h2>How much stock should you keep?</h2>
		<p>Deciding how much stock to keep depends on the size and nature of your business, and the type of stock involved. If you are short of space you may be able to buy stock in bulk and then pay a fee to your supplier to store it, calling it off as and when needed.</p>
		
		<p><strong>Keeping little or no stock and negotiating with suppliers to deliver stock as you need it</strong></p>
		<table>
			<tbody>
				<tr>
					<th>Advantages</th>
					<th>Disadvantages</th>
				</tr>
				<tr>
					<th valign="top">Efficient and flexible - you only have what you need, when you need it</th>
					<td valign="top">Meeting stock needs can become complicated and expensive</td>
				</tr>
				<tr>
					<th valign="top">Lower storage costs</th>
					<td valign="top">You might run out of stock if there's a hitch in the system</td>
				</tr>
				<tr>
					<th valign="top">You can keep up to date and develop new products without wasting stock</th>
					<td valign="top">You are dependent on the efficiency of your suppliers</td>
				</tr>
			</tbody>
		</table>
		
		<p>This might suit your business if it's in a fast-moving environment where products develop rapidly, the stock is expensive to buy and store, the items are perishable or replenishing stock is quick and easy.</p>
		
		<p><strong>Keeping lots of stock</strong></p>
		<table>
			<tbody>
				<tr>
					<th><strong>Advantages</strong></th>
					<th><strong>Disadvantages</strong></th>
				</tr>
				<tr>
					<th valign="top">Easy to manage</th>
					<td valign="top">Higher storage and insurance costs</td>
				</tr>
				<tr>
					<th valign="top">Low management costs</th>
					<td valign="top">Certain goods might perish</td>
				</tr>
				<tr>
					<th valign="top">You never run out</th>
					<td valign="top">Stock may become obsolete before it is used</td>
				</tr>
				<tr>
					<th valign="top">Buying in bulk may be cheaper</th>
					<td valign="top">Your capital is tied up</td>
				</tr>
			</tbody>
		</table>
		
		<p>This might suit your business if sales are difficult to predict (and it is hard to pin down how much stock you need and when), you can store plenty of stock cheaply, the components or materials you buy are unlikely to go through rapid developments or they take a long time to re-order.</p>
		
		<h3>Stock levels depending on type of stock</h3>
		<p>There are four main types of stock:</p>
		<p><strong>Raw materials and components</strong></p>
		<p>Ask yourself some key questions to help decide how much stock you should keep:</p>
		<ul>
			<li>How reliable is the supply and are alternative sources available?</li>
			<li>Are the components produced or delivered in batches?</li>
			<li>Can you predict demand?</li>
			<li>Is the price steady?</li>
			<li>Are there discounts if you buy in bulk?</li>
		</ul>

		<p><strong>Work in progress - stocks of unfinished goods</strong></p>
		<p>Keeping stocks of unfinished goods can be a useful way to protect production if there are problems down the line with other supplies.</p>
		<p><strong>Finished goods ready for sale</strong></p>
		<p>You might keep stocks of finished goods when:</p>
		<ul>
			<li>demand is certain</li>
			<li>goods are produced in batches</li>
			<li>you are completing a large order</li>
		</ul>
		
		<p><strong>Consumables</strong></p>
		<p>The type of consumables you will use will depend on your type of business, but could include fuel and stationery. How much stock you keep will depend on factors such as:</p>
		<ul>
			<li>reliability of supply</li>
			<li>expectations of price rises</li>
			<li>how steady demand is</li>
			<li>discounts for buying in bulk</li>
		</ul>

