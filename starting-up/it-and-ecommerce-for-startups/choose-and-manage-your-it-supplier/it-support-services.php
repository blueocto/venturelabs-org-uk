		<h1>Choose and manage your IT supplier</h1>
		<h2>IT support services</h2>
		<p>Consider carefully exactly how much IT support you need. This depends on how much IT expertise you have <strong>in-house</strong>, how important it is to keep systems running smoothly, and how much you can sensibly afford for a support contract.</p>
		<p>Services to consider include:</p>
		<ul>
			<li><strong>Installation and configuration</strong> - will you need the supplier to install and configure hardware and software so that it's fully functioning and ready to use?</li>
			<li><strong>Training</strong> - will your staff require training?</li>
			<li><strong>Telephone support</strong> - will your staff require telephone support, and during what hours? Does the supplier provide a helpdesk? Do they have a guaranteed response time? Will you be paying per call, or a fixed sum? Are you restricted to a single caller, or will they accept calls from different members of staff? What percentage of problems does the supplier resolve on the first call - a first-time fix? Is this support provided from the UK or overseas? If it is overseas-based - does this impact upon when it is available because of time differences?</li>
			<li><strong>Email support</strong> - will email support be adequate for some or all of your needs? What's the supplier's guaranteed response time for email messages?</li>
			<li><strong>Online support</strong> - does the supplier offer real-time online chat or other support technology? Is there a peer support forum where other users can suggest fixes?</li>
			<li><strong>Site visits</strong> - does the supplier provide on-site support? If so, how much does it cost?</li>
		</ul>

