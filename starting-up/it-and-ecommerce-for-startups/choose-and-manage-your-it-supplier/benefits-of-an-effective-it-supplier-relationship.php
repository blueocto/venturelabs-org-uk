		<h1>Choose and manage your IT supplier</h1>
		<h2>Benefits of an effective IT supplier relationship</h2>
		<p>Building a <strong>successful relationship</strong> with your IT supplier is essential to your system's success.</p>
		<p>Trying to purchase IT systems and services at the lowest possible cost - with no regard to your business goals for the project or the overall cost of ownership - is likely to lead to a strained relationship with your supplier and cause both system and business problems.</p>
		<p>Ideally, your supplier should have a clear view of your business goals and the role of IT in achieving these. This will help you build a relationship that can deliver a <strong>number of benefits</strong> including:</p>
		<ul>
			<li>ensuring service-level agreements are met and that system availability is high</li>
			<li>speedy resolution of problems or queries</li>
			<li>valuable guidance from the supplier on your business' future IT direction through their knowledge of your industry sector and the latest IT developments</li>
			<li>delivery of the anticipated business benefits</li>
		</ul>

		<p>To build a successful relationship with any IT supplier you should be clear from the outset about what you both <strong>expect from the arrangement</strong>. Make sure:</p>
		<ul>
			<li>you both have clear expectations of exactly what you'll get in terms of hardware, software and services</li>
			<li>the business relationship is mutually beneficial - your supplier will have no interest in whether you achieve your business goals if you just want goods as cheaply as possible</li>
			<li>there are specific targets to be reached at set dates representing what your business gets out of its IT systems - not the delivery of boxes and cables</li>
			<li>the supplier provides regular reports of progress towards the agreed business objectives for the system</li>
			<li>you hold regular reviews with your supplier to see how things are going</li>
			<li>you have enough flexibility to accommodate change - whether this is through new technologies, the business environment or refined business goals</li>
			<li>you have a signed contract which you both agree to, that includes details of each party's obligations and what should happen in the event of a disagreement or dispute</li>
		</ul>

