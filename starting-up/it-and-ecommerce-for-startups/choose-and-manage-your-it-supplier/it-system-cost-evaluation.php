		<h1>Choose and manage your IT supplier</h1>
		<h2>IT system cost evaluation</h2>
		<p><strong>System management</strong> is usually the biggest cost over the lifetime of an IT system. It includes installation, upgrades, expansion, maintenance, support, training and the time your staff spend dealing with system problems.</p>
		<p>So it's essential to consider full system lifecycle costs. Saving on purchase costs could result in significant additional costs on an ongoing basis, while cutting back on services could waste a fortune in staff time or lost business.</p>
		<p><strong>Initial costs</strong> could include:</p>
		<ul>
			<li>consultancy</li>
			<li>hardware costs</li>
			<li>software licence fees</li>
			<li>software configuration and customisation work</li>
			<li>installation</li>
			<li>staff training</li>
			<li>providing necessary infrastructure such as network cabling or an electricity supply</li>
			<li>compliance with health and safety law relating to computer use</li>
		</ul>

		<p><strong>Ongoing IT management costs</strong> could include:</p>
		<ul>
			<li>software support contracts - fees for licence renewals and upgrades</li>
			<li>hardware upgrades</li>
			<li>expansion of memory, disk size, communications capacity</li>
			<li>support - telephone, email, online, on site</li>
			<li>maintenance and replacements</li>
			<li>compliance with health and safety legislation relating to computer use</li>
			<li>staff training</li>
			<li>costs of consumables, such as printer cartridges, ink and paper</li>
			<li>communications charges - telephone costs, internet access and subscription charges</li>
		</ul>

		<p>Agreeing a budget and contract with a supplier can be a lengthy process. You need to specify every aspect of the project in as much detail as possible. This will help to control costs and highlight any problems you encounter during the project.</p>
		<p>When you negotiate a contract for IT systems or services with your supplier, they will usually expect payment upon delivery of the system. However, it is worthwhile seeking to extend the payment period until the system is actually installed and working to your satisfaction.</p>
		
