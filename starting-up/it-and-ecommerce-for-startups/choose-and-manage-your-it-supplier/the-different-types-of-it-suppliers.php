		<h1>Choose and manage your IT supplier</h1>
		<h2>The different types of IT suppliers</h2>
		<p>Though most IT suppliers can provide a comprehensive range of hardware, software and services, there are some differences between them. For example:</p>
		<ul>
			<li><strong>resellers</strong> act as agents for hardware manufacturers, but can also offer software and a wide range of IT services and support</li>
			<li><strong>system integrators</strong> select the appropriate hardware and software for your specific needs and deliver an integrated, working system</li>
			<li><strong>specialist suppliers</strong> have expertise in one specific area, such as customer-relationship management systems</li>
			<li><strong>consultants</strong> may just provide consultancy, with no hardware, software or maintenance services</li>
			<li><strong>retail, mail order</strong> and <strong>online suppliers</strong> may be appropriate if you're confident you don't need advice and simply need someone to supply the IT system</li>
		</ul>

		<h3>Size of supplier</h3>
		<p>IT suppliers range from small local outfits to global organisations. Even the largest suppliers can provide systems, services and consultancy to small businesses.</p>
		<p>You may rely on your supplier for a system that's central to your business success. You need complete confidence in them. A local business, or a larger company with a local office, can be a reassuring presence.</p>
		
		<h3>Single source or multiple suppliers</h3>
		<p>Unless you have extensive IT expertise in-house, it's generally best to use a single supplier for all hardware, software, services and support.</p>
		<p>If you buy from multiple suppliers, you'll have to decide which one is responsible when there's a problem - unless you have a service provider prepared to support your whole system.</p>
