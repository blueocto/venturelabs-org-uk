		<h1>Computer software: the basics</h1>
		<h2>Choosing and buying software</h2>
		<p>There are many ways to get advice and support when choosing software, including:</p>
		<p>
			<ul>
				<li>software or business consultants - although they may be biased towards software they are familiar with</li>
				<li>software vendors - again, remember their possible bias</li>
				<li>professional and trade associations</li>
				<li>the computer press - magazines like Computer Weekly and Computing can be a good way to get information on the basic issues, so you know the right questions to ask</li>
			</ul>
		</p>
		
		<h3>Document your business</h3>
		<p>Diagrams may make documenting your business easier - for example, using boxes for processes and arrows to show documents, invoices etc, entering or leaving processes.</p>
		<p>Documenting your business in this way will help you identify areas where new software could improve your business processes. It will also help you explain your business and its requirements to potential suppliers when buying software.</p>
		<p>Compare your list of requirements with the information that you have about each package. Reject all packages that cannot meet your 'must have' requirements. From the remainder, select the one that delivers the greatest number of your advantageous and 'nice to have' features at a price your business can afford.</p>
		
		<h3>Additional costs</h3>
		<p>As well as the price of software, you should also consider support costs, future upgrade costs and the need for hardware upgrades to use your software to full effect.</p>
		
		<h3>Future suitability</h3>
		<p>You also need to consider if software will still be suitable as your business evolves - your software system must be able to accommodate an increased load should your customer base grow.</p>
		
		<h3>Licensing</h3>
		<p>Make sure you buy licensed software and keep safe your proof of ownership, such as the original distribution CDs or hologrammed certificate of authenticity. See the page in this guide on <a href="/starting-up/it-and-ecommerce-for-startups/computer-software-the-basics/licences-for-proprietary-software.php">licences for proprietary software</a>.</p>
		
