		<h1>Computer software: the basics</h1>
		<h2>Open source and proprietary software</h2>
		<p><strong>Proprietary software</strong> is software developed by a supplier and made available for you to use under an end-user licence agreement (EULA), which you effectively accept when you install the software. The licence sets out how the software can be used and usually prohibits you from doing certain things, like:</p>
		<ul>
			<li>making copies of the software and passing them on</li>
			<li>selling your licence to someone else</li>
		</ul>

		<p>Most proprietary software comes without the source code. This is the code originally written by the programmer. Without this code neither you nor any other software supplier can make changes to the software package.</p>
		<p>When you buy a proprietary software package you are not buying the software - you are buying the right to use the software in a specific way. The software company owns the software.</p>
		<p>Most off-the-shelf packages are proprietary software.</p>
		<p><strong>Open source software</strong> is made available under a licence that allows you to make copies and pass them on to anyone. The software comes with its source code - which you can change to meet your needs.</p>
		<p>Licences for open source software do vary. Some licences require you to make any changes made to the source code publicly available, while others will allow you to keep those changes private. You should read the licence before using or changing the software.</p>
		<p>Open source software is usually available for free download or off-the-shelf at a low cost.</p>
		<p>The advantages of open source software include:</p>
		<ul>
			<li>lower software licensing costs</li>
			<li>no supplier lock-in</li>
			<li>freedom to do what you want with the software</li>
			<li>open standards that support collaborative development</li>
			<li>freedom to upgrade software as it suits your business</li>
		</ul>

		<p>The main disadvantages are:</p>
		<ul>
			<li>it may be difficult to get support</li>
			<li>some proprietary formats such as Microsoft Word's '.doc' format are so widely used that other formats may be less acceptable for business</li>
		</ul>

