		<h1>Computer software: the basics</h1>
		<h2>Business benefits of new software</h2>
		<p>Before investing in new software, you should think about what you want it to do for you.</p>
		<p>For example, you might want to:</p>
		<ul>
			<li>cut costs by automating routine tasks</li>
			<li>improve customer service - eg with an interactive online contact system for after-sales support or to check order status</li>
			<li>enable your employees to work more efficiently</li>
			<li>communicate and collaborate electronically with suppliers or partners</li>
		</ul>

		<p>You should take time to talk to your employees, suppliers and customers who may have ideas for improving your business processes using IT.</p>
		<p>It will be helpful to write down the objectives and potential benefits of new software, prioritising the list to work out the best returns on investment. See the page in this guide on <a href="/starting-up/it-and-ecommerce-for-startups/computer-software-the-basics/choosing-and-buying-software.php">choosing and buying software</a>.</p>
		<p>Choose software that will run on your current hardware, as long as this doesn't reduce potential benefits. You should include any hardware upgrade costs in your budgets - see our guide to <a href="/it-and-ecommerce/introduction-to-it/computer-hardware-the-basics.php">computer hardware: the basics</a>.</p>
		<p>Alternatively, look at outsourcing your software requirements - eg cloud computing - which could help reduce both your software and hardware costs. See the page in this guide on <a href="/starting-up/it-and-ecommerce-for-startups/computer-software-the-basics/cloud-computing-software-as-a-service.php">cloud computing - software as a service</a> and our guide on <a href="/it-and-ecommerce/software-and-business-applications/cloud-computing.php">cloud computing</a>.</p>
		
		<h3>Planning for the future</h3>
		<p>Ideally you should develop a long-term strategy, covering your future IT needs. This should take into account potential changes in customers, staffing levels and/or your products and services.</p>
		
