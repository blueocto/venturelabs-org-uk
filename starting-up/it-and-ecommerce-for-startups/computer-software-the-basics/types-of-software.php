		<h1>Computer software: the basics</h1>
		<h2>Types of software</h2>
		<p>There are various types of software and you need to know the differences between them.</p>
		
		<h3>Bespoke</h3>
		<p>It is possible to write software specifically for your business. You can either:</p>
		<ul>
			<li>Write the software yourself. If you run a small business, writing the software yourself is unlikely to be cost-effective as you need to have significant expertise and lots of time.</li>
			<li>Have the software written by an external supplier. With specialist help, you should get the exact functionality you need. However, the price is likely to be high, and you will be tied to that supplier for future support.</li>
		</ul>

		<p>Producing any bespoke software can take a long time and your staff will need to have an input in the development process. For most small businesses, bespoke software may not be the best choice.</p>
		
		<h3>System or application software</h3>
		<p>System software is not directly useful for business purposes but makes the computer hardware useful. An example is the Microsoft Windows operating system.</p>
		<p>Application software is designed for a specific use - like word processing or accounts. You will need both types of software.</p>
		
		<h3>Packaged software</h3>
		<p>Packaged software is standardised and generally low priced. The functionality may not be exactly what you need, but it could make sense to change your business practices to suit the software rather than having software specially written.</p>
		<p>The high volume of sales of such software usually means that you will be able to get support and training from a number of different sources.</p>
		<p>Standard software packages are usually the best choice for small businesses. In some cases, the software can be tailored to meet your requirements if it doesn't include all the functions you need.</p>
		
