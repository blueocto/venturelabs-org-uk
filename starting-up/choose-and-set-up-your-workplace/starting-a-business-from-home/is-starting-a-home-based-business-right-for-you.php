		<h1>Starting a business from home </h1>
		<h2>Is starting a home-based business right for you?</h2>
		<p>Becoming a home business owner is an option if you don't actually need to run your business from rented or owned business premises.</p>
		<p>It's a common choice for people who just need a small office, or who spend most of their time working on premises owned by their clients.</p>
		<p>However, working from home may not be an option if it significantly changes the use of your home, or affects your local area, for example if you have lots of visitors. If you rent your home, your licence or lease may include restrictions on using it for business purposes.</p>
		<h3>Advantages and disadvantages of home-based business</h3>
		<p>The major <strong>advantages</strong> are that you:</p>
		<ul>
			<li>do not have a sizeable expenditure on office rental or office purchase</li>
			<li>save time and money by cutting your commute</li>
			<li>can be flexible around the hours you work</li>
		</ul>
		<p>The <strong>disadvantages</strong> are:</p>
		<ul>
			<li>it may prove difficult to keep work and home-life separate, and there may be domestic distractions and interruptions</li>
			<li>a lack of contact with other people and businesses</li>
			<li>you might end up working long hours</li>
			<li>your mortgage, home insurance and tax situation may be affected - see our guide on how to <a href="/premises-and-property/choosing-the-right-premises/use-your-home-as-a-workplace.php">use your home as a workplace</a></li>
		</ul>
		<h3>Addressing the disadvantages</h3>
		<p>It is important to keep your home and work life separate, especially if you have a family. There are simple steps you can take to make this separation - see the page in this guide on <a href="/starting-up/choose-and-set-up-your-workplace/starting-a-business-from-home/your-work-environment.php" title="Work equipment and workspace requirements, planning permission and questions to ask yourself">your work environment</a>.</p>
		<p>If you are feeling isolated from other people, Venture Labs and other organisations can provide you with details of networking events. You can also get involved in online networking - see our guide on <a href="/sales-and-marketing/online-marketing/online-business-networking-and-social-networking.php" title="How to use online business networking and social media to develop your business">online networking</a>.</p>
		
