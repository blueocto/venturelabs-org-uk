		<h2>Start-up costs and financing your home-based business</h2>
		<p>If you are looking for some extra funds, there are various options to consider.</p>
		<p>For more information, see our guides on how to <a href="/finance-and-grants/finance-options/choose-the-right-finance-options/choose-the-right-finance-when-starting-up.php">choose the right finance when starting up</a> and <a href="/finance-and-grants/prepare-to-raise-finance/how-to-attract-investment/use-your-business-plan-to-get-funding.php">use your business plan to get funding</a>.</p>
