		<h3>Initial costs</h3>
		<p>You will need to buy, lease or rent the equipment for your home office and any materials you need for the service you are supplying. Find out more in our guide <a href="/finance-and-grants/manage-your-cashflow/decide-whether-to-lease-or-buy-assets.php">decide whether to lease or buy assets</a>.</p>
		<p>Finding and keeping in touch with customers is a major issue, so investing in a good computer with relevant software and a broadband connection will be a priority in most cases.</p>
		<p>The most common costs to consider when starting out are:</p>
		<ul>
			<li>a computer or laptop</li>
			<li>broadband access</li>
			<li>mobile phone</li>
			<li>office desk or chair</li>
			<li>business cards</li>
			<li>stock - if you're supplying products</li>
		</ul>
		<p><a href="/it-and-ecommerce/introduction-to-it/setting-up-a-basic-it-system.php">Use our interactive tool to find out which computer equipment you should buy for your business</a>.</p>
		<p>When you are factoring software packages into your start up costs, take a look at the much cheaper or even free options - open-source software such as OpenOffice, Zoho, Google Docs and StarOffice.</p>
		<p>For more information, see our guides on <a href="/it-and-ecommerce/introduction-to-it/computer-software-the-basics.php">computer software: the basics</a> and <a href="/it-and-ecommerce/software-and-business-applications/open-source-software.php">open source software</a> and the page in this guide on <a href="/starting-up/choose-and-set-up-your-workplace/starting-a-business-from-home/it-and-home-business.php">IT and home business</a>.</p>
		
