			<h1>Environmental responsibilities</h1>
			<h2>Conservation and biodiversity issues for businesses</h2>
			<p>Biodiversity refers to all species of plants and animals, including any genetic variations within those species, and the complex ecosystems they live in.</p>
			<p>The world is losing biodiversity at an ever-increasing rate as a result of human activity. All types of business operating near protected areas should be aware of their responsibilities for conservation and protecting biodiversity. This doesn't just apply to land-based industries such as forestry or farming, but to all offices, factories and other business activities based on or near these areas.</p>
			<p>The business benefits of conserving biodiversity include:</p>
			<ul>
				<li>a competitive advantage - businesses that can demonstrate that they are committed to environmental protection will attract and retain customers, suppliers and employees who share those values</li>
				<li>compliance with environmental legislation</li>
				<li>environmental reporting - your business will be able to report on its environmental performance and management</li>
			</ul>

			<p>Protected areas include:</p>
			<ul>
				<li><strong>Common land</strong> - for more information, see our guide on <a href="/your-business-sector/farming/land-and-conservation/common-land.php">common land</a>.</li>
				<li><strong>Local nature reserves</strong> - places with wildlife or geological features that are of special interest locally.</li>
				<li><strong>Areas of outstanding natural beauty</strong> - areas of particular natural beauty in need of conservation.</li>
				<li><strong>National parks</strong> - tracts of the countryside that have been given protection for the conservation and enhancement of their special qualities.</li>
				<li><strong>National nature reserves</strong> - important areas of wildlife habitat.</li>
				<li><strong>Sites of special scientific interest</strong> - good examples of natural heritage of wildlife habitats, geological features and landforms. For more information, see our guide on <a href="/your-business-sector/farming/land-and-conservation/sites-of-special-scientific-interest-sssi-and-historical-monuments.php">Sites of Special Scientific Interest (SSSI) and historical monuments</a>.</li>
				<li><strong>Special areas of conservation</strong> - strictly protected sites for habitat types and species that are considered to be most in need of conservation at a European level.</li>
				<li><strong>Special protection areas</strong> - strictly protected sites classified for rare and vulnerable birds.</li>
				<li><strong>Wetlands</strong>.</li>
				<li><strong>UNESCO biospheres</strong> - areas of terrestrial and coastal/marine ecosystems which are internationally recognised under UNESCO's Man and the Biosphere programme.</li>
			</ul>
			
			<p>To find out whether you operate on or near a protected area, <a href="http://www.magic.gov.uk/home.htm" target="_blank" rel="noopener noreferrer">use the interactive map on the MAGIC website</a>. Alternatively, you can call the Defra Helpline on Tel 08459 33 55 77 or the Environment Agency Helpline on Tel 03708 506 506.</p>
			<p>There are also a number of species that are protected. You can <a href="http://jncc.defra.gov.uk/page-5705" target="_blank" rel="noopener noreferrer">find a list of priority species and habitats at the Joint Nature Conservation Committee (JNCC) website</a>. For information about importing endangered species, see our guide on <a href="/international-trade/food-and-agriculture/trading-in-live-animals-and-animal-products/enforcing-cites-controls.php">enforcing CITES controls</a>.</p>
			<p>For more information, see our guide on <a href="/environment-and-efficiency/your-environmental-responsibilities-an-overview/conservation-and-biodiversity-an-overview.php">conservation and biodiversity - an overview</a>.</p>
			
