			<h1>Environmental responsibilities</h1>
			<h2>The business benefits of good environmental and sustainable practices</h2>
			<p>Complying with legislation and going further to adopt environmental and sustainable practices can benefit your business. For example:</p>
			<ul>
				<li>Reducing your energy use can save you money. See our guide on how to <a href="/environment-and-efficiency/cut-energy-costs/save-money-by-using-energy-more-efficiently.php">save money by using energy more efficiently</a>.</li>
				<li>Reducing your use of raw materials can cut your costs, minimise your waste and lower your waste disposal bills. See our guide on how to <a href="/environment-and-efficiency/reducing-and-recycling-waste/save-money-by-reducing-reusing-and-recycling-waste.php">save money by reducing, reusing and recycling waste</a>.</li>
				<li>Reducing your water use can cut your water bills and also lower your waste water disposal costs. See our guide on how to <a href="/environment-and-efficiency/cut-water-costs/use-water-efficiently-the-basics.php">use water efficiently - the basics</a>.</li>
				<li>Recycling equipment and materials could make you money and save on disposal costs - you may be able to sell old equipment or parts to subsidise the cost of buying new ones. Read our guide on how to <a href="/environment-and-efficiency/reducing-and-recycling-waste/save-money-by-reducing-reusing-and-recycling-waste.php">save money by reducing, reusing and recycling waste</a>.</li>
				<li>Taking steps to prevent environmental damage will reduce your risk of incurring costs and of having to carry out potentially expensive remedial work in the event of an environmental incident. You can <a href="http://webarchive.nationalarchives.gov.uk/20130822084033/http://www.defra.gov.uk/environment/quality/environmental-liability/" target="bgExternalonlinebusinesslinkgovuk" target="_blank" rel="noopener noreferrer">read information and guidance on environmental liability on the Department for Environment, Food and Rural Affairs (Defra) website</a>.</li>
			</ul>

			<p>Your business could also enjoy other benefits. Many government organisations and large companies will only work with businesses who take their environmental responsibilities seriously. You may also find it easier to attract investment from banks and other sources of finance.</p>
			<p><a href="/environment-and-efficiency/measuring-and-reporting-environmental-performance/assess-your-environmental-compliance.php">Use our interactive tool to assess your environmental compliance</a>. You can also <a href="http://online.businesslink.gov.uk/bdotg/action/logicToolStart?itemId=1079446499&amp;type=BLTTOOL" target="_blank" rel="noopener noreferrer">use our interactive tool to identify where you can save money by going green</a>.</p>
			<p><strong>Sustainable development</strong> can create opportunities for your business. As well as making financial savings, you can enhance your reputation and brand value by fostering customer loyalty and motivating staff. You can also use sustainable development as an opportunity to encourage innovation, increase investment and open new markets.</p>
