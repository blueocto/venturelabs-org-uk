		<h1>National Loan Guarantee Scheme</h1>
		<h2>New Government scheme delivering cheaper loans for businesses</h2>
		<p>Launched on 20th March 2012, this new scheme enables eligible businesses to access cheaper loans from participating banks. The Government is using the UK's budget credibility in financial markets to provide guarantees on up to &pound;20 billion of unsecured borrowing by banks. This will enable participating banks to borrow at a lower rate, and these banks will pass on the entire benefit they receive to smaller businesses through cheaper loans.</p>
		<p>This means that businesses that are granted an NLGS loan by their bank will receive a discount on their loan of 1 percentage point compared to the interest rate that they would otherwise have received from that bank. The scheme is open to businesses with turnovers up to &pound;50m and applies to new term loans, hire purchase and leasing arrangements.</p>
		<p>Note: Interest rates on NLGS loans may vary between banks as each bank operates their own pricing models and are still subject to banks' usual lending criteria.</p>
