		<h1>Act on Co2penhagen</h1>
		<h2>New Business through action on Carbon</h2>
		<p>In July, the UK Government published the Low Carbon Transition Plan - setting out how the UK will move to a low carbon economy. In December more than 180 countries will join together in Copenhagen to agree a new international deal that will aim put the world onto a growth path that will avoid dangerous climate change.</p>
		<p>The deal reached in Copenhagen will have important implication for businesses all around the world, of all sizes in all industries, driving the global transition to a prosperous low carbon future.</p>
		<p>For UK companies, the global requirement to reduce emissions will add to the business case to use less energy and water, and produce less waste. See our guide on <a href="/environment-and-efficiency/carbon-reduction/cutting-your-carbon-emissions.php">cutting your carbon emissions</a>.</p>
		<p>Reducing your carbon emissions makes good business sense, as the cost savings that can be achieved through reducing energy use go straight to the bottom line. See our guide on how to <a href="/environment-and-efficiency/cut-energy-costs/save-money-by-using-energy-more-efficiently.php">save money by using energy more efficiently</a>.</p>
		<p>In some cases, action will be voluntary, relying on a good common-sense approach to cost control, but in some key areas it will be through legislation, such as the Waste Electrical and Electronic Equipment Directive.</p>
		<p>For larger energy users there are regulatory measures to incentivise CO2 reductions, such as the new CRC Energy Efficiency Scheme (formerly known as the Carbon Reduction Commitment), which will enter into force from April 2010, and Climate Change Agreements.</p>
		<p>But Copenhagen is about more than just efficiency. An ambitious deal will also offer massive opportunities for growth in a new low carbon economy. Businesses of all sizes can benefit from growing market opportunities in:</p>
