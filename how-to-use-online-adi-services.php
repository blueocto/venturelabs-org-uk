		<h1>How to use online ADI services</h1>
		
		<h2>New services for Approved Driving Instructors (ADIs)</h2>
		<p>DSA has a range of online services for ADIs. They are available from 6.00 am to midnight, seven days a week.</p>
		<p>Here's what you can do online, and where to do it.</p>
		
		<h3>Update your registration</h3>
		<p>You can use this service to:</p>
		<ul>
			<li>update your address, phone number and email address</li>
			<li>show your details on the 'find your nearest driver instructor' service</li>
			<li>commit to the voluntary code of practice and continuing professional development</li>
			<li>view your check test history</li>
		</ul>
		<p><a href="do-it-online/update-your-approved-driving-instructor-adi_registration-details.php.html" title="Keep your address and contact details up to date, and tell the Driving Standards Agency if you want to appear on the 'find your nearest driving instructors' service">businesslink.gov.uk/manageadi</a></p>
		
		<h3>Pass Plus</h3>
		<p>You can use this service to:</p>
		<ul>
			<li>register to be a Pass Plus instructor</li>
			<li>order Pass Plus refill packs</li>
			<li>track to see if your pupils' Pass Plus certificates have been issued</li>
		</ul>
		<p><a href="do-it-online/use-pass-plus-online-services.php.html" title="Use the Driving Standards Agency's online service to register as a Pass Plus instructor, order your refill packs and track if your pupils' Pass Plus certificates have been issued">businesslink.gov.uk/passplus</a></p>
		
		<h3>Renew or re-register</h3>
		<p>You can use this service to:</p>
		<ul>
			<li>renew your ADI registration</li>
			<li>re-register as an ADI if your registration has lapsed</li>
		</ul>
		<p><a href="do-it-online/renew-your-approved-driving-instructor-adi-registration-or-re-register-online.php.html" title="Use the Driving Standards Agency's online service to renew your approved driving instructor (ADI) registration or re-register as an ADI if your registration has lapsed ">businesslink.gov.uk/renewadi</a></p>
		
		<h3>Services for new ADIs</h3>
		<ul>
			<li>Apply to start the qualifying process<br />
				<a href="do-it-online/apply-to-start-the-approved-driving-instructor-adi-qualifying-process.php.html" title="Apply online to become an Approved Driving Instructor for the first time to the Driving Standards Agency">businesslink.gov.uk/applyadi</a></li>
			<li>Apply for a trainee licence<br />
				<a href="do-it-online/apply-for-a-trainee-licence-to-give-driving-instruction.php.html" title="Apply to the Driving Standards Agency for a trainee licence to give driving instruction once you have passed the first two approved driving instructor (ADI) qualification tests">businesslink.gov.uk/traineelicence</a></li>
			<li>Apply for your first ADI certificate<br />
				<a href="do-it-online/apply-for-your-first-approved-driving-instructor-adi-registration-certificate.php.html" title="Apply to the Driving Standards Agency for your first approved driving instructor (ADI) registration certificate once you have passed the qualifying tests">businesslink.gov.uk/newadi</a></li>
		</ul>
		
		<h3>What you need</h3>
		<ul>
			<li>PRN - personal reference number - also known as your ADI number</li>
			<li>User ID - your unique ID to use online ADI services</li>
			<li>Password - your password to use online ADI services</li>
		</ul>
		
		<h3>Forgotten your password?</h3>
		<p>If you have forgotten your password, go to the service you want to use. When you reach the login screen, click on the 'Forgot password'. You'll then need to type in your user ID - DSA will then email you a new password.</p>
		
		<h3>Keep a note</h3>
		<p>You can download a guide to using online ADI services. It also includes space for you to make a note of your user ID and a reminder of your password.</p>
		<p><a href="https://www.gov.uk/government/publications/adi-online-services-user-guide/adi-online-services-user-guide" title="A guide to the ADI services available" target="_blank">Download the ADI services guide from the DFT website (PDF, 76K) - Opens in a new window</a>.</p>
