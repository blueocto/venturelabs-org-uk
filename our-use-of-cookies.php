		<h3>Cookies used by this service</h3>
		<p>There are two types of cookie:</p>
		<p><strong>Persistent cookies</strong> remain on your computer for a specified time and so are saved to your computer's hard disk. We use persistent cookies to:</p>
		<ul>
			<li>identify that a registered user has returned</li>
			<li>securely remember your login User ID - if you have asked us to do so</li>
			<li>understand how you use our site and obtain information on website usage - as described above</li>
		</ul>
		<p><strong>Session cookies</strong> are deleted when you close your browser and are usually not saved to your computer's hard disk. We use session cookies to:</p>
		<ul>
			<li>hold some types of non-private information you provide while using an interactive tool on the website</li>
			<li>identify that you have logged in to the site</li>
			<li>hold your search criteria while you are carrying out a search of the website</li>
			<li>collect information on website usage - as described above</li>
		</ul>
		
