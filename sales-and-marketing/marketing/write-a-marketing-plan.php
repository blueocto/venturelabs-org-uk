		<p>Marketing is a key part of business success - and you should use a marketing plan as a basis for executing your marketing strategy.</p>
		<p>A marketing plan sets out clear objectives and lists the actions you will take to achieve them. Perhaps most importantly, it looks at how you can ensure that your plan becomes reality.</p>
		<p>A marketing plan includes factors such as deciding which customers to target and how to reach them, how to win their business and keep them happy afterwards, as well as continually reviewing and improving everything you do to stay ahead of the competition.</p>
		<p>Remember that marketing in itself will not guarantee sales, but a well-researched and coherent marketing plan will give you a much better chance of building long-term, profitable relationships.</p>
