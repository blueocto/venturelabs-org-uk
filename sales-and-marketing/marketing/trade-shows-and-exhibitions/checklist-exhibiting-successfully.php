		<h1>Trade shows and exhibitions</h1>
		<h2>Checklist: exhibiting successfully</h2>
		<p>Attending an exhibition can involve a large commitment of time and money for your business. By preparing thoroughly in advance you can ensure your business maximises the potential benefits and the return on your investment. You should ensure you:</p>
		<ul>
			<li>find out what time you can set up your stand and get there early</li>
			<li>check that all the equipment works before you go 'live'</li>
			<li>select suitably trained or knowledgeable staff for the stand</li>
			<li>brief staff thoroughly</li>
			<li>identify everyone on your stand with a badge stating who they are and the name of your company</li>
			<li>employ sufficient staff on the stand to deal with enquiries, allowing for rest breaks</li>
			<li>avoid spending time talking to colleagues so visitors do not have to interrupt you with their queries</li>
			<li>allocate enough time to each visitor to allow for questions and note down details to follow up</li>
			<li>provide a means of recording queries and taking contact details</li>
			<li>avoid taking too many details during the event - these can be collected during your follow up</li>
			<li>take breaks away from the stand so you don't have to eat or drink while you're on duty</li>
			<li>provide plenty of promotional materials that will stand out later</li>
			<li>attach your business card to the front of promotional material and highlight points of interest</li>
			<li>give visitors your contact details to make it easy for them to get in touch after the event</li>
		</ul>

