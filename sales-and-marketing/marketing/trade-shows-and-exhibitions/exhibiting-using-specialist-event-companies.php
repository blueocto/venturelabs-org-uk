		<h1>Trade shows and exhibitions</h1>
		<h2>Exhibiting - using specialist event companies</h2>
		<p>Using specialist event companies can help you make the most of your presence at a trade fair.</p>
		
		<h3>Advantages</h3>
		<ul>
			<li>Their experience of exhibiting at similar events means they know all the pitfalls and tricks of the trade. They know where to source everything you need to make a good display.</li>
			<li>Someone else gets everything ready, leaving you free for other things (although you will need to keep closely involved).</li>
			<li>Their professional approach combined with your expertise should enable your business to make the right impression.</li>
		</ul>

		<p>You can pay someone to handle the whole event for you, or just the bits you can't manage yourself. To get the best results you need to choose the right event company and brief them properly. A good brief will include:</p>
		<ul>
			<li>what you want them to handle, and what you will do yourself</li>
			<li>key information about your product or service</li>
			<li>its benefits and major selling points</li>
			<li>who your existing customers are and who you hope to reach</li>
			<li>the image you are trying to create</li>
			<li>highlights you will be promoting or demonstrating at your stand</li>
			<li>information you want to capture, and how you want to follow up</li>
			<li>your budget</li>
		</ul>

		<h3>Finding specialist event companies</h3>
		<p>There are companies for all aspects of the events industry. They range from exhibition organisers who manage everything for you including staff and your stand, to those who produce <b>promotional merchandise</b> and point-of-sale materials - eg special display stands for your products.</p>
		<p>It may be possible to find an event company that has specific experience of your market sector. Browse the internet, ask your trade association for contacts, or try an online event service directory that lists companies by service type. You can <a href="https://www.aeo.org.uk/members" target="_blank" rel="noopener noreferrer" title="View member suppliers by product categories such as auditors, caterers, consultancy, legal and public relations">find a list of approved suppliers on the Association of Event Organisers (AEO) website</a>. Better still, get personal recommendations from other companies in your line of business.</p>
		
