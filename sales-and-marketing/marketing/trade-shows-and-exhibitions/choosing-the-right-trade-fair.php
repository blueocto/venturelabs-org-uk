		<h1>Trade shows and exhibitions</h1>
		<h2>Choosing the right trade fair</h2>
		<p>Finding the right trade fair is essential. If the area of interest it covers is too specialist or too broad, it is unlikely to attract visitors who want to buy what you have to sell or exhibitors offering the products and services you are looking to source.</p>
		
		<h3>If you are exhibiting</h3>
		<p>Start by making a profile of the customers you want to reach. Identify their special interests and where they are located. Then take a look at the most likely trade fairs for your industry sector, and try to match them with these customers.</p>
		<p>Most established exhibitions will be able to provide a detailed breakdown of the number and type of visitors from previous years. This will give a good indication of whether the types of customers you are targeting are likely to be there.</p>
		
		<h3>Attending as a customer</h3>
		<p>Work out what you want to achieve before you go. What kinds of product or service do you want to find out about? Who do you want to meet? Match your requirements against what the most likely trade fairs have to offer. You should be able to get a list of the companies planning to exhibit before you attend. This will help you avoid a wasted journey.</p>
		
		<h3>Likely trade fairs - identifying the right one</h3>
		<p>You can <a href="http://www.exhibitions.co.uk/" target="_blank" rel="noopener noreferrer" title="Get information on consumer, public, industrial and trade exhibitions in the UK">find a comprehensive listing of trade fairs at the Trade Fairs and Exhibitions UK website</a>. You can search the list by type, date, organiser or venue.</p>
		<p>You can also contact specific venues, ask a relevant trade association or contact your local Chamber of Commerce to find out what's on in your area. <a href="https://www.britishchambers.org.uk/page/join-a-chamber" target="_blank" rel="noopener noreferrer" title="Searchable map of Chambers of Commerce in the UK">Search for your local Chamber of Commerce at the British Chambers of Commerce website</a>.</p>
		<p>Before booking, find out more about the characteristics of any fairs that look promising - for example, who goes, their spending power, who exhibits, any data from past events. The organisers of a good trade fair should be able to supply this kind of information.</p>
		
