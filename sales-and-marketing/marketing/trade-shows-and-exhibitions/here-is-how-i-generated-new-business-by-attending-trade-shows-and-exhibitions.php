		<h1>Here's how I generated new business by attending trade shows and exhibitions</h1>
		<p>Preston-based precision engineering company Engineering Technologies and Manufacturing Ltd was established in 1998. The company regularly attends trade shows in the UK and overseas to promote its specialist range of tube manipulation tooling.</p>
		<p>Exhibiting at a US trade show helped the company to tap into the lucrative North American market and has already resulted in a large order. Company secretary Elaine Stewart explains how they went about it.</p>
		
		<h2>What I did</h2>
		<h3>Choose the right trade show</h3>
		<p>"Before you start, you have to be sure about why you're exhibiting and who you're targeting. Things to ask the show organisers for before you commit include attendance statistics, delegate profiles and which other companies are exhibiting.</p>
		<p>"We wanted to exhibit in North America primarily because it has a huge automotive industry. Our target customers are machine manufacturers and the companies that buy the machines, so we needed to be somewhere where we would reach both audiences.</p>
		<p>"The exhibition we selected, FABTECH, ticked all the right boxes. We also approached our local Venture Labs for advice and it helped us to source a UK Trade &amp; Investment Solo Show Support grant that covered 50 per cent of the set-up costs of exhibiting."</p>
		
		<h3>Plan ahead</h3>
		<p>"We set a budget and drew up a list of actions and deadlines from the outset, including technology requirements, display materials, promotional literature, stand furniture, advertising and so on. It helped that the FABTECH website had excellent online services, including detailed timelines and booking facilities for equipment.</p>
		<p>"Even with financial support from UK Trade &amp; Investment, costs had to be controlled. Transport and accommodation are two of the biggest expenses and booking early can save you money. We also got written quotes for everything in advance to ensure we didn't get any nasty shocks when the bills came in."</p>
		
		<h3>Maximise opportunities</h3>
		<p>"We made sure that all staff on the stand were well briefed. Each visitor was asked pre-qualifying questions to determine whether they were genuine prospects. When a good prospect did turn up, we knew exactly which key strengths we wanted to emphasise, and whether we should focus on innovation and quality rather than price.</p>
		<p>"Samples of our tooling were on hand to illustrate the products' capabilities and that proved useful for engaging people's interest. We ensured that all visitors went away with promotional materials and business cards and that we had a record of their details. The networking and fact-finding opportunities were great too and we took time out to visit other stands and talk to potential partners. When we returned to the UK, we followed up each lead by email or letter. We already have a substantial order from one company we met and several others in the pipeline."</p>
		
		<h2>What I'd do differently</h2>
		<h3>Take a laptop</h3>
		<p>"We didn't think we'd need a computer on the stand, but with hindsight it would have been a good idea. Some potential customers just wanted an informal chat, but there were occasions when a more formal presentation would have been appropriate."</p>
		
		<h3>Attract attention</h3>
		<p>"We got lots of ideas from other exhibitors about ways to attract visitors to the stand. Next time around we'll develop a mechanical device to display, or use a projector with moving images in order to draw more attention to ourselves."</p>
