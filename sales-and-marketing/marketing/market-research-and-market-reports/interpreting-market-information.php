		<h1>Market research and market reports</h1>
		<h2>Interpreting market information</h2>
		<p>Though there's a lot of readily available market information, you need to be careful how you interpret it.</p>
		<p>External data might not be in a format that's easy to use. It may have been collected for other purposes or be from a range that doesn't tally with your target market.</p>
		<p>Beware of out-of-date market information. This can be misleading, as the market may have changed significantly since the information was published. It can be particularly hard to tell how recent any information published on the internet is. Some information on the web can be unreliable or biased.</p>
		<p>Remember that statistics can sometimes mask the true picture. For example, an 'average' income for the population in your area might conceal a high proportion of low earners - meaning fewer people can afford your product than it appears.</p>
		<p>The same principle applies to your own sales records - one or two major customers could distort the picture.</p>
		<p>Most importantly, don't make up your mind in advance. Finding market information that simply confirms what you already believe is easy - but only a realistic picture of your customers and markets will be useful to your business.</p>
		
