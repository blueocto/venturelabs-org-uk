		<h1>Market research and market reports</h1>
		<h2>Planning field research</h2>
		<p>Good planning is essential if you want to get the right results from field research.</p>
		<p>First you need to decide how to collect the information you want. Popular methods include:</p>
		<ul>
			<li>A <b>survey</b>, using a fixed set of questions. The most effective way of carrying out a survey is typically with face-to-face interviews, but phone interviews, online questionnaires and postal surveys are also possibilities.</li>
			<li>A <b>discussion -</b> often held in small focus groups. Discussions are good for qualitative research as they allow you to explore people's attitudes in more detail.</li>
			<li><strong>Observation,</strong> to investigate what people do rather than what they say. For example, look at how shoppers react when they pass a particular point-of-sale display.</li>
			<li>An <strong>experiment -</strong> you might, for instance, run a blind taste test of your soft drink against your competitors' products. Alternatively, you could lend your new product to a customer and ask for feedback.</li>
		</ul>

		<p>Once you have decided how you'll gather the information, you'll need to work out how to make it happen. <b>Budget</b> how much time and money will be needed, as the time involved will normally be significant.</p>
		<p>You'll need to <b>design</b> your research. For example, drawing up a questionnaire or deciding how you'll run a focus group.</p>
		<p>Then there are the <b>logistics</b>. If you want to carry out street interviews, make sure your researchers have the required local authority licence and identity card. If you want to run a focus group or conduct face-to-face interviews or product tests, where will you hold them? Where will you find the participants? And who'll run the session?</p>
		<p>Consider carefully whether you have got the <b>skills</b> in-house to do this. If not, it's probably a good idea to get a market research agency to do your research for you.</p>
		<p>See the page in this guide on <a href="/sales-and-marketing/marketing/market-research-and-market-reports/tips-for-successful-field-research.php">tips for successful field research</a>.</p>
		
