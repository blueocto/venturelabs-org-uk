		<h1>Here's how I made the most of market research</h1>
		<h2>Here's how I made the most of market research</h2>
		<p>Bladonmore is a financial training and media business, based in London. A customised service and a gritty, real-world approach to training have enabled the company to grow rapidly during its first two years. Director Richard Rivlin explains how effective use of market research has contributed to Bladonmore's development.</p>
		<p><strong>What I did</strong></p>
		
		<h3>Spend time not money</h3>
		<p>"Like most companies starting out, we needed to research our target market but didn't have limitless cash to pay someone to do it for us. Doing it yourself is cheap in money terms - but you have to invest your time if you expect to get anything useful out of it.</p>
		<p>"Over a period of three months, I went on a virtual fact-finding mission using the internet. If you persevere, you can find an incredible amount of quality information for free, including market reports and expert analysis. Business consultants' websites, industry bodies and sector-leading companies are a good place to start."</p>
		
		<h3>Apply the research</h3>
		<p>"Throughout my research, I was careful to avoid <strong>analysis paralysis</strong>. There's no point hiding behind piles of market data and thinking you have done something constructive. You have to use the information to develop your business. Having clear objectives helps. I wanted to know the size of the market, to learn from competitors' successes and mistakes and to understand what potential clients want.</p>
		<p>"I focused on data from reputable sources and used it to help formulate a business plan. At a later stage, I also used research facts in Bladonmore's marketing material - but always bearing in mind that I didn't want to give away too much to competitors."</p>
		
		<h3>Keep up to date</h3>
		<p>"Research isn't just about reading the occasional market report. It should be an ongoing process that keeps you up to date with your market, your rivals and your clients. I find newspapers one of the best research sources. There's something relevant to our business in the press almost every day. For example an article about increased activity in venture capital (VC) markets is useful, because companies looking for VC finance often need financial presentation training."</p>
		<p><strong>What I'd do differently</strong></p>
		
		<h3>Head for the top</h3>
		<p>"I didn't wake up to the wonders of free expert research soon enough. If I had my time again, I'd head straight for the websites of top consultants. You may not be able to afford their research fees, but they publish enough in the public domain to meet the needs of many smaller businesses."</p>
		
		<h3>Be disciplined</h3>
		<p>"I now make it part of my daily routine to clip useful research out of newspapers. I even carry around a small pair of scissors for the purpose. It's yielded several business development ideas and I wish I'd got into the habit sooner."</p>
		
