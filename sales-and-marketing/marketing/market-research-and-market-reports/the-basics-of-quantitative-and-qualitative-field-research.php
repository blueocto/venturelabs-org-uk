		<h1>Market research and market reports</h1>
		<h2>The basics of quantitative and qualitative field research</h2>
		<p>Published market information and your own data can tell you a lot about your customers and your market - but it's unlikely to tell you everything.</p>
		<p>Field research can be <strong>quantitative</strong> or <strong>qualitative</strong>:</p>
		<ul>
			<li>
				quantitative research provides statistical information - for example, how many potential customers there are and what their average incomes are
			</li>
			<li>
				qualitative research examines people's feelings and attitudes towards your product or service, and what motivates them
			</li>
		</ul>

		<p>You'll probably need to carry out some of your own quantitative and qualitative field research - talking, observing or carrying out product tests with customers and potential customers. This can help you to:</p>
		<ul>
			<li>test customers' reactions to a new product, and adapt it if necessary</li>
			<li>investigate attitudes of customers and potential customers</li>
			<li>find information specific to your business or a local market, rather than the market as a whole</li>
		</ul>

