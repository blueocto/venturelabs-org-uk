		<h1>Market research and market reports</h1>
		<h2>Using market reports and other data</h2>
		<p>Once you have identified the information you need, you can start to gather it.</p>
		<p>You can find useful data from a wide range of sources, including:</p>
		<ul>
			<li>your local business reference library</li>
			<li>your trade association, local authority or Chamber of Commerce</li>
			<li>the Office for National Statistics - for information on the economy, population and social trends</li>
			<li>the business pages of national newspapers</li>
			<li>commercial publishers of market reports - including KeyNote, Euromonitor, Mintel, Datamonitor, The Economist Intelligence Unit and Market &amp; Business Development</li>
			<li>your business' own data - for analysis of your sales records or levels of enquiries</li>
			<li>your customers - monitoring their buying habits and how they behave is one of the best methods of market research</li>
		</ul>

		<p>For other useful sources of data, see the page on <a href="/sales-and-marketing/marketing/understand-your-competitors/learning-about-your-competitors.php">learning about your competitors</a> in our guide: <a href="/sales-and-marketing/marketing/understand-your-competitors.php">understand your competitors</a>.</p>
		
