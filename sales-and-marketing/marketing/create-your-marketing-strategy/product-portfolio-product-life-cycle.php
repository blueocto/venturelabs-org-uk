		<h1>Create your marketing strategy</h1>
		<h2>Product portfolio - product life cycle</h2>
		<p>Product life cycle management is an essential part of all effective marketing strategies. If you are aware of where your product is in its life cycle, you can market it in a way that will maximise its sales.</p>
		<p>Your marketing strategy should be tailored to each of your business' products or services, but should also take into account what stage your product is at in its life cycle. For example:</p>
		<ul>
			<li>mature products or services often suffer from increased competition as similar products and services enter the market</li>
			<li>as the market matures for your products or services, prices may drop or stabilise</li>
		</ul>
		<p>It is a good idea to have new products or services to introduce as others decline. That way, there's always one part of your range showing a sales peak. Alternatively, you can make changes to the current product to create a new version. For more information, see our guide on how to <a href="/starting-up/considering-starting-up/research-and-develop-your-business-ideas-new-products-and-services.php">research and develop your business ideas, new products and services</a>.</p>
		<p>Effective research into your markets and competitors will also help you manage your product's lifecycle. See our guides on <a href="/sales-and-marketing/marketing/market-research-and-market-reports.php">market research and market reports</a> and how to <a href="/sales-and-marketing/marketing/understand-your-competitors.php">understand your competitors</a>.</p>
		<p>If you're a smaller business with few resources, you don't have to necessarily be the first to market. In fact, being second can result in lowered costs and risks. However, rather than concentrating on innovation, you should focus on what makes you different to - and better than - your competitors.</p>
		
