		<h1>Create your marketing strategy</h1>
		<h2>Product strategy - positioning and differentiation</h2>
		<p>In order for your product or service to be successful against the competition in the marketplace and attract customers, it is important to make sure your product or service stands out. You should consider what makes your product or service different from others that exist and how this can be displayed through an identity or marketing campaign. Read our guide on how to <a href="/sales-and-marketing/marketing/understand-your-competitors.php">understand your competitors</a>.</p>
		<h3>Needs-based product marketing</h3>
		<p>You can use needs-based product marketing to improve on your existing products. To help position your product or service, you should focus on your existing products or services, even if you have limited cash, skills or technological resources. To do this, you should:</p>
		<ul>
			<li>define your target market - see the page in this guide on how to <a href="/sales-and-marketing/marketing/create-your-marketing-strategy/define-your-target-market.php">define your target market</a></li>
			<li>identify the main attributes of your product or service</li>
			<li>collect information from customers and potential customers about how they perceive the product or service - eg how popular it is, and how valuable, useful or cost-effective it is, especially compared to similar products or services</li>
		</ul>
		<p>See our guide on <a href="/sales-and-marketing/marketing/market-research-and-market-reports.php">market research and market reports</a>.</p>
		<h3>Tailor your product to meet customer demands</h3>
		<p>You should continually look for ways to adapt and promote your products and services to match your customers' requirements. Product or service development can focus on features, design and quality and so can the customer service you provide. These will help further your position and differentiate you from your competitors. </p>
		<p>By specifically focussing on different customer segments, you can identify ways to develop your product or service to exploit new areas and increase your profitability. For example, by offering holiday insurance at those on sport-based holidays - in addition to your standard holiday insurance schemes - you can create a new market for your service and retain your existing segments.</p>
		
