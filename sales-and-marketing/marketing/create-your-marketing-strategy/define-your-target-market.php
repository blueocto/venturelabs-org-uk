		<h1>Create your marketing strategy</h1>
		<h2>Define your target market</h2>
		<p>When creating your marketing strategy, you need to understand your target market - ie the specific group of consumers you will be aiming your products or services at. These are the customers who are most likely to buy from you, and who will make your business successful.</p>
		<p>When trying to define your target market, you should consider who will be most likely to buy your products or services. Consider aiming your strategy at certain target markets, such as:</p>
		<ul>
			<li>females or males</li>
			<li>age groups</li>
			<li>regional areas</li>
			<li>education or interests</li>
			<li>occupations</li>
			<li>average incomes</li>
		</ul>
		<p>You should also consider how different groups of potential customer might use your product or service. For example, older customers might have different wants and needs to younger customers. Therefore, you might have several target markets to focus on.</p>
		
		<h3>Understand your product or service</h3>
		<p>You can only identify your target market if you fully understand your product's benefits and features. You should consider the following points when assessing your product:</p>
		<ul>
			<li>what is the purpose of your product - is it to solve problems, satisfy basic needs, or simply a luxury item?</li>
			<li>advantages your product has over other competitors'</li>
			<li>customer problems that your product or service can solve - if applicable</li>
		</ul>
		
		<h3>Identifying your customer</h3>
		<p>It is equally important that you establish who your product or service is aimed at. Your marketing strategy will fail if you target the wrong audience from the start, regardless of how good it is.</p>
		<p>You need to establish whether your target group are individual consumers or other businesses. There are often two important differences between these target groups:</p>
		<ul>
			<li>individual customers - sales to this group can be unpredictable, and customers usually have smaller budgets and specific buying preferences</li>
			<li>businesses - sales to this group are often more predictable and stable as there is usually a greater budget available to be used on various products</li>
		</ul>
		<p>It is possible to target both types of customer, though you need to be realistic about whether your product or service is relevant for them. For example, a cleaning service may apply to both consumer groups, whereas a new clothing brand will probably only interest individuals.</p>
		
		<h3>Researching your potential customer base</h3>
		<p>Once you know your groups of customers, you can look to conduct further research to see if there are any types of customer with more specific needs than others. For example, older customer groups may buy different types of products to younger groups.</p>
		<p>'Market segmentation' can be an effective tool for this. It involves splitting your customer groups into smaller segments to find the sections of your customer base that will be most profitable to your business. You can segment customers by:</p>
		<ul>
			<li>lifestyle</li>
			<li>social class</li>
			<li>opinion</li>
			<li>activities and interests</li>
			<li>attitudes and beliefs</li>
		</ul>
		<p>You should also consider the factors such as the following when deciding which marketing segments to target:</p>
		<ul>
			<li>Is the segment large enough to support your marketing goals?</li>
			<li>Does your business have the skills and expertise to deal with the chosen segment?</li>
			<li>Is there growth in the segment?</li>
		</ul>
		<p>
			<p>By focusing on the smaller segments, you can learn a lot about them and structure your marketing campaigns accordingly. You can <a href="https://www.marketingdonut.co.uk/marketing-strategy/your-target-market" target="_blank" rel="noopener noreferrer">find information on market segmentation on the Marketing Donut website<span> - Opens in a new window</a>.</p>
		</p>
		<p>Conducting market research can be an effective way of understanding your customer base, and help you to decide the direction of your marketing strategy. For more information, see our guide on <a href="/sales-and-marketing/marketing/market-research-and-market-reports.php">market research and market reports</a>.</p>
		
