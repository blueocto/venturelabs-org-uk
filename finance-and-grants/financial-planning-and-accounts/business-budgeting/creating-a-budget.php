		<h1>Business budgeting</h1>
		<h2>Creating a budget</h2>
		<p>Creating, monitoring and managing a budget is key to business success. It should help you allocate resources where they are needed, and should not be complicated. You simply need to work out what you are likely to earn and spend in the budget period.</p>
		<p>Most businesses start preparing a budget around two to three months before the start of a new financial year.</p>
		<p>Begin by asking these questions:</p>
		<ul>
			<li>What are the <strong>projected sales</strong> for the budget period? Be realistic - if you overestimate, it will cause you problems in the future.</li>
			<li>What are the <strong>direct costs</strong> of sales - ie costs of materials, components or subcontractors to make the product or supply the service?</li>
			<li>What are the <strong>fixed costs</strong> or overheads?</li>
		</ul>

		<p>You should break down the <strong>fixed costs and overheads</strong> by type, eg:</p>
		<ul>
			<li>cost of premises, including rent or mortgage, business rates and service charges</li>
			<li>staff costs - eg pay, benefits, National Insurance</li>
			<li>utilities - eg heating, lighting, telephone or internet connection</li>
			<li>printing, postage and stationery</li>
			<li>vehicle expenses</li>
			<li>equipment costs</li>
			<li>advertising and promotion</li>
			<li>travel and subsistence expenses</li>
			<li>legal and professional costs, including insurance</li>
		</ul>

		<p>Your business may have different types of expenses, and you may need to divide the budget by department. Don't forget to add in how much you need to pay yourself, and <strong>include an allowance for tax</strong>. If you haven't been trading long enough to know how much to set aside for your tax bill, you can <a href="/_files/hmrc_budgeting-for-your-first-self-employed-tax-bill_mar-12.pdf" target="_blank" rel="noopener noreferrer">download tax budgeting advice for the self-employed from the HM Revenue &amp; Customs (HMRC) website (PDF, 81K)</a>.</p>
		<p>The budget should be in place before the start of the financial year. This will enable staff to be aware of their responsibilities for maximising revenue and managing expenditure.</p>
		<p>Your business plan should help in establishing projected sales, cost of sales, fixed costs and overheads, so it would be worthwhile preparing this first.</p>
		<p>Once you have figures for income and expenditure, you can work out how much money you're making. You can look at your costs and work out ways to reduce them. You should also be able to spot if you are likely to have cashflow problems - giving you time to do something about them.</p>
		<p>You should stick to your budget as far as possible, but review and revise it as needed. As the budget year progresses, control might be better exercised by either a rolling budget or periodic reforecasting.</p>

