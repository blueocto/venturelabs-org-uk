		<h1>Business budgeting</h1>
		<h2>Using your budget to measure performance</h2>
		<p>Your budget can be a financial action plan. This can be useful, particularly if you review your budgets regularly as part of your annual planning cycle - see our guide on how to <a href="/grow-your-business/setting-a-vision-for-growth/prepare-a-business-plan-for-growth.php">prepare a business plan for growth</a>.</p>
		<p>Your budget can serve as:</p>
		<ul>
			<li>an indicator of the costs and revenues linked to each of your activities</li>
			<li>
				a way of providing information and supporting management decisions throughout the year
			</li>
			<li>a means of monitoring and controlling your business, particularly if you analyse the differences between your actual and budgeted income</li>
		</ul>

		<h3>Benchmarking performance</h3>
		<p>Comparing your budget year on year can be an excellent way of benchmarking your business' performance - for example, you can compare your projected figures with previous years to measure your performance. See our guide on how to <a href="/sales-and-marketing/selling/forecast-and-plan-your-sales.php">forecast and plan your sales</a>.</p>
		<p>You can also compare your figures for projected margins and growth with those of other businesses in the same sector, or across different parts of your business.</p>
		
		<h3>Key performance indicators (KPIs)</h3>
		<p>To boost your business' performance you need to understand and monitor the key 'drivers' of your business - a driver is something that has a major impact on your business. There are many factors affecting every business' performance, so it is vital to focus on a handful of these and monitor them carefully.</p>
		<p>The three key drivers for most businesses are:</p>
		<ul>
			<li>sales</li>
			<li>costs</li>
			<li>working capital</li>
		</ul>

		<p>Any trends towards cashflow problems or falling profitability will show up in these figures when measured against your budgets and forecasts. They can help you spot problems early on if they are calculated on a consistent basis.</p>
		
