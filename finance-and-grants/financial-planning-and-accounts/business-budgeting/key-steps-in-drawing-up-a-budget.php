		<h1>Business budgeting</h1>
		<h2>Key steps in drawing up a budget</h2>
		<p>There are a number of key steps you should follow to make sure your budgets and plans are as realistic and useful as possible.</p>
		
		<h3>Make time for budgeting</h3>
		<p>If you invest some time in creating a comprehensive and realistic budget, it will be easier to manage and ultimately more effective.</p>
		
		<h3>Use last year's figures - but only as a guide</h3>
		<p>Collect historical information on sales and costs if they are available - these could give you a good indication of likely future sales and costs. It's also essential to consider what your sales plans are, how your sales resources will be used and any changes in your market or the competitive environment.</p>
		
		<h3>Create realistic budgets</h3>
		<p>Use historical information, your business plan and any changes in operations or priorities to budget for overheads and other fixed costs.</p>
		<p>It's useful to work out the relationship between variable costs and sales and then use your sales forecast to project variable costs. For example, if your unit costs reduce by 10 per cent for each additional 20 per cent of sales, how much will your unit costs decrease if you have a 33 per cent rise in sales?</p>
		<p>Make sure your budgets contain enough information for you to easily monitor the key drivers of your business such as sales, costs and working capital. Accounting software can help you manage your accounts. See our guide on <a href="/it-and-ecommerce/software-and-business-applications/accounting-software.php">accounting software</a>.</p>
		
		<h3>Involve the right people</h3>
		<p>It's best to ask staff with financial responsibilities within the business to provide you with estimates of figures for your budget - for example, sales targets, production costs or specific project control. If you balance their estimates against your own, you will achieve a more realistic budget. This involvement will also give them greater commitment to meeting the budget.</p>
		<p>You could also ask your accountant to review your budget figures or to put the final budget figures together.</p>
		
