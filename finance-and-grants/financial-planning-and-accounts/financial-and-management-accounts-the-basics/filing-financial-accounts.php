		<h1>Financial and management accounts: the basics</h1>
		<h2>Filing financial accounts</h2>
		<p>Limited companies are <strong>obliged by law</strong> to prepare a set of financial accounts each year and to file a copy with Companies House.</p>
		<p>For accounting periods starting on or after 6 April 2008:</p>
		<ul>
			<li>private companies must file accounts within 21 months of the business' formation, and within nine months of the end of each financial year thereafter</li>
			<li>public companies must file accounts within 18 months of the business' formation, and within six months of the end of each financial year thereafter</li>
		</ul>

		<p><a href="https://www.gov.uk/government/publications/life-of-a-company-annual-requirements" target="_blank" rel="noopener noreferrer">Read guidance on preparing and filing accounts on the Companies House website</a>.</p>
		<p>There are statutory penalties for late or incorrect filing, for which the directors are liable. <a href="https://www.gov.uk/government/publications/late-filing-penalties" target="_blank" rel="noopener noreferrer">Find information about late filing penalties on the Companies House website</a>.</p>
		<p>Small and medium-sized companies are allowed to file accounts with certain information omitted. For more information, see our guide on <a href="/tax-payroll-and-company-information/companies-house-annual-returns-and-accounts/accounting-and-audit-exemptions-for-small-companies.php">accounting and auditing exemptions for small companies</a>.</p>
		
		<h3>Small companies</h3>
		<p>A small company is one which meets at least <strong>two</strong> of the following criteria:</p>
		<ul>
			<li>annual turnover must be &pound;6.5 million or less</li>
			<li>the balance sheet total - fixed and current assets - must be &pound;3.26 million or less</li>
			<li>the average number of employees must not exceed 50</li>
		</ul>
		<p>Small companies may deliver a copy of the full accounts to Companies House or choose to deliver abbreviated accounts which include the following:</p>
		<ul>
			<li>abbreviated balance sheet and notes</li>
			<li>special auditors' report - unless the company has a small company audit exemption</li>
		</ul>
		
		<h3>Medium-sized companies</h3>
		<p>A medium-sized company must meet at least <strong>two</strong> of the following criteria:</p>
		<ul>
			<li>annual turnover must not exceed &pound;25.9 million (&pound;22.8 million or less for accounting reference periods before 6 April 2008)</li>
			<li>the balance sheet total must not exceed &pound;12.9 million (&pound;11.4 million on or before 6 April 2008)</li>
			<li>the average number of employees must not exceed 250</li>
		</ul>
		<p>Medium-sized companies may deliver a copy of the full accounts to Companies House or choose to deliver abbreviated accounts which include the following:</p>
		<ul>
			<li>full balance sheet</li>
			<li>abbreviated profit and loss account</li>
			<li>special auditors' report</li>
			<li>directors' report</li>
			<li>notes to the accounts</li>
		</ul>

