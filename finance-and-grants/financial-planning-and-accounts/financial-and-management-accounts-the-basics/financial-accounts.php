		<h1>Financial and management accounts: the basics</h1>
		<h2>Financial accounts</h2>
		<p>Financial accounts are a historical record of your <strong>business' performance</strong> over a past period - usually one year - for the benefit of external users such as shareholders, employees, suppliers, bankers and authorities.</p>
		<p>Financial accounts normally include the following elements.</p>
		
		<h3>Profit and loss account</h3>
		<p>This measures your business' performance over a given period of time, usually one year.</p>
		<p>It compares the income of your business against the cost of goods or services and expenses incurred in earning that revenue. See our guide on how to <a href="/finance-and-grants/financial-planning-and-accounts/set-up-a-simple-profit-and-loss-account-for-your-business.php">set up a simple profit and loss account for your business</a>.</p>
		
		<h3>Balance sheet</h3>
		<p>This is a snapshot of your business' assets (what you own or are owed) and your liabilities (what you owe) on a particular day - eg the last day of your financial year. See our guide on <a href="/finance-and-grants/financial-planning-and-accounts/balance-sheets-the-basics.php">balance sheets: the basics</a>.</p>
		
		<h3>Cashflow statement</h3>
		<p>This shows how your business has generated and disposed of cash and liquid funds during the period under review. A cashflow statement is different from a cashflow forecast, which is used to predict the expected rises and falls in cashflow over the coming year. See our guide on <a href="/finance-and-grants/manage-your-cashflow/cashflow-management-the-basics.php">cashflow management: the basics</a>.</p>
		
		<h3>Statement of recognised gains and losses</h3>
		<p>This records all gains and losses since the previous set of accounts. For example, changes caused by currency fluctuations, property revaluation, profits earned by associates and joint ventures not included in the normal accounts.</p>
		
		<h3>Unincorporated businesses</h3>
		<p>Unincorporated businesses such as sole traders and partnerships are required by HM Revenue &amp; Customs (HMRC) to maintain proper books and records to support annual income tax returns. These must be kept for a minimum of six years. See our guide on how to <a href="/tax-payroll-and-company-information/set-up-a-basic-record-keeping-system.php">set up a basic record-keeping system</a>.</p>
		<p>For more information on what to include in a company's annual accounts, see our guide on <a href="/tax-payroll-and-company-information/companies-house-annual-returns-and-accounts/filing-accounts-with-companies-house-dates-and-checklist.php">filing accounts with Companies House: dates and checklist</a>.</p>
		
