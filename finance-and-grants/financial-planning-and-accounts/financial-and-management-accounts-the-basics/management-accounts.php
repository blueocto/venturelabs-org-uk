		<h1>Financial and management accounts: the basics</h1>
		<h2>Management accounts</h2>
		<p>Management accounts can help you make timely and meaningful management decisions about your business.</p>
		<p>Different businesses will have different management accounting needs, depending on the business areas that are important to them. These can include:</p>
		<ul>
			<li>the sales process - such as pricing, distribution and debtors</li>
			<li>the purchasing process - such as stock records and creditors</li>
			<li>a fixed asset register - details of all fixed assets, including identification numbers, cost and date of purchase, etc</li>
			<li>employee records</li>
		</ul>

		<p>There is no legal requirement to prepare management accounts, but it is hard to run a business effectively without them. Most companies produce them regularly - eg monthly or quarterly.</p>
		<p>Management accounts analyse recent <strong>historical</strong> performance and usually include <strong>forward-looking</strong> elements such as sales, cashflow and profit forecasts. The analysis is usually performed against forecasts and budgets that have been produced at the start of the year. See our guide on <a href="/finance-and-grants/financial-planning-and-accounts/business-budgeting.php">budgeting and business planning</a>.</p>
		<p>The information in management accounts is usually broken down so that the performance of different elements of the business can be measured. For example if a business has more than one sales outlet, there might be a separate report for each. There may also be a report produced to show how well a particular product has done across different outlets.</p>
		<p>For businesses selling more than one product, it is advisable to provide a financial breakdown for each product category. This will allow you to ensure that profitable products are not subsidising those that are selling poorly, unless you intentionally promote loss leaders to attract further custom.</p>
		
