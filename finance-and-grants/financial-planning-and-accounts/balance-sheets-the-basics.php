		<h1>Balance sheets: the basics</h1>
		<h2>Introduction</h2>
		
		<img src="/_img/SU0290.gif" width="94" height="62" alt="" />

		<p>Your balance sheet is a financial statement at a given point in time. It provides a snapshot summary of what your business owns or is owed - assets - and what it owes - liabilities - at a particular date.</p>
		<p>The balance sheet therefore shows how your business is being funded and how you are using these funds.</p>
		<p>There are three ways you may use your balance sheet:</p>
		<ul>
			<li>for reporting purposes as part of a limited company's annual accounts</li>
			<li>to help you and other interested parties such as investors, creditors or shareholders to assess the worth of your business at a given moment</li>
			<li>as a tool to help you analyse and improve the management of your business</li>
		</ul>

		<p>This guide explains who needs to produce balance sheets and when, the different elements within them and how to use the information from a balance sheet to assess and manage business performance.</p>
		
