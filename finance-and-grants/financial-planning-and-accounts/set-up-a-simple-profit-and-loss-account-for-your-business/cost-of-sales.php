		<h1>Set up a simple profit and loss account for your business</h1>
		<h2>Cost of sales</h2>
		<p>The cost of sales is the cost of obtaining or creating your product.</p>
		<p>This might include:</p>
		<ul>
			<li>the cost of stock you buy for resale</li>
			<li>components/raw materials to make your product</li>
			<li>labour to produce the product</li>
			<li>machine hire</li>
			<li>small tools</li>
			<li>other production costs</li>
		</ul>

		<p>When you create your profit and loss account, you deduct your cost of sales from your overall sales, or turnover, to arrive at your 'gross profit'. This is your profit before deduction of expenses.</p>
		<p>Cost of sales does not usually apply if you supply a service only.</p>
		
