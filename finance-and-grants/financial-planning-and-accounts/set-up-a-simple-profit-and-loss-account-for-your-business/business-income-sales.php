		<h1>Set up a simple profit and loss account for your business</h1>
		<h2>Business income: sales</h2>
		<p>Business income falls into two categories for profit and loss reporting:</p>
		<ul>
			<li>sales or 'turnover'</li>
			<li>other income</li>
		</ul>

		<p>For information on the second category, see the page in this guide on <a href="/finance-and-grants/financial-planning-and-accounts/set-up-a-simple-profit-and-loss-account-for-your-business/business-income-other.php">business income: other</a>.</p>
		
		<h3>Business sales or turnover</h3>
		<p>Your business' total sales of products and/or services in a trading year is referred to as turnover. This is the starting point for your profit and loss account.</p>
		<p>How you record sales will vary according to your business type, size and whether you are VAT-registered. You may use a simple list or 'ledger' in a book, a tailored spreadsheet, or a computer software program. Whichever system you use, you need to ensure that it is accurate and updated regularly.</p>
		
		<h3>Sales records back-up</h3>
		<p>The back-up records for your sales ledger fall into two categories, and will vary according to your business type:</p>
		<p>Sales documentation:</p>
		<ul>
			<li>copies of sales invoices issued by you</li>
			<li>rolls of till receipts</li>
			<li>records of money you pay into the business when taking goods out for personal use - note that if you take goods out of your business without paying for them you still owe the business for them, and so will have to add the retail cost of the items to your overall pre-tax profit figure</li>
		</ul>

		<p>Proof of income relating to the above:</p>
		<ul>
			<li>paying-in slips</li>
			<li>bank/building society statements and similar</li>
		</ul>

		<p>If you operate on a 'cash only' basis you must keep detailed records of your income in your sales book or ledger and be able to relate these to your expenditure, cash in hand and bank statements.</p>
		
