		<h1>Set up a simple profit and loss account for your business</h1>
		<h2>Business income: other</h2>
		<p>As well as reporting sales income, you need to report income to the business from other sources, for example:</p>
		<ul>
			<li>interest on business bank accounts</li>
			<li>sale of equipment you no longer need</li>
			<li>rental income to the business</li>
			<li>money you put into a limited company from personal funds</li>
		</ul>

		<p><strong>Recording other income</strong></p>
		<ul>
			<li>Record equipment sales in your sales ledger, or on a separate schedule of assets if you prefer.</li>
			<li>Keep a record of any rental income, for example if you sub-let part of your office to someone else.</li>
		</ul>
		
		<p><strong>Back-up documentation</strong></p>
		<p>You must keep paying-in slips and/or bank statements to account for your additional business income. Ideally, you should be able to cross-reference this documentation to the above 'other income' records.</p>
		
