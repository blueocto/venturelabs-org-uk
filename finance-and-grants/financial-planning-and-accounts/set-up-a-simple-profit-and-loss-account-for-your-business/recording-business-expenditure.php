		<h1>Set up a simple profit and loss account for your business</h1>
		<h2>Recording business expenditure</h2>
		<p>Business expenditure falls into three key areas for the purpose of reporting your profit or loss. You can save yourself, or your accountant, time by grouping your costs accordingly in your purchase list or 'ledger'. The three key areas are:</p>
		<ul>
			<li>cost of sales - the base cost of obtaining or creating your product</li>
			<li>business expenses</li>
			<li>cost of equipment you have bought or leased for long-term use</li>
		</ul>

		<p>If your business is VAT-registered, you will need to record details of any VAT included in your expenditure. See our guide on <a href="/tax-payroll-and-company-information/vat/vat-returns-and-accounts/accounts-and-records-for-your-vat.php">accounts and records for your VAT</a>.</p>
		
		<h3>Business expenditure back-up</h3>
		<p>The back-up records for your business expenditure fall into two categories. As with sales records, they will vary according to your business type.</p>
		<p><strong>1. Purchase/expenditure documentation</strong></p>
		<ul>
			<li>copies of supplier invoices/receipts issued to you</li>
			<li>till receipts for items bought over the counter</li>
			<li>payroll and National Insurance records if you have employees</li>
		</ul>

		<p><strong>2. Proof of expenditure relating to the above</strong></p>
		<ul>
			<li>cheque book stubs</li>
			<li>bank statements</li>
			<li>credit card statements and receipts</li>
		</ul>

		<p>It is important for you to be able to cross-reference your records to your expenditure figures if asked. If you mislay a receipt for a small item, make sure you enter it in your purchase or petty cash book ledger and make a note that you have lost the receipt.</p>
		
