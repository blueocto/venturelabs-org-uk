		<h1>Set up a simple profit and loss account for your business</h1>
		<h2>Profit and loss accounting periods and tax</h2>
		<p>Businesses normally work out their profit and loss for a twelve month period. This makes it easy to see how well your business is doing each year, and to compare one year with the next.</p>
		<p>The way your accounts are taxed depends on what type of business you have.</p>
		
		<h3>Accounts for self-employed and partnerships</h3>
		<p>Self-employed sole traders and business partnerships are normally taxed on the profits for the 12 month accounting period ending during the tax year. The tax year runs from 6 April to 5 April the following year.</p>
		<p>For example, if you make your annual accounts up to 31 December each year, your profits to 31 December 2010 are used in your 2010-11 tax return (for the year to 5 April 2011).</p>
		<p>The simplest approach can be to make your annual accounts up to 31 March or 5 April, so that they match the tax year.</p>
		<p>Special rules apply when you start or close a business, or if you decide to change your accounting period, to ensure that all your profits are fairly taxed.</p>
		<p>To find out more about accounting periods for the self-employed and partnerships, <a href="https://www.gov.uk/government/publications/how-to-calculate-your-taxable-profits-hs222-self-assessment-helpsheet" target="_blank" rel="noopener noreferrer">download a helpsheet on how to calculate your taxable profits from the HM Revenue &amp; Customs (HMRC) website (PDF, 94K)</a>.</p>
		
		<h3>Accounting periods for limited companies</h3>
		<p>Limited companies are required to submit annual accounts to Companies House, including a profit and loss account.</p>
		<p>When you start a new company, the financial year automatically runs to the end of the month a year after the company is incorporated. For example, if a company is incorporated on 10 June, the first financial year runs from 10 June to 30 June the following year. But you can choose a different financial year end if you wish.</p>
		<p>Your profit subject to Corporation Tax is normally based on an accounting period that matches this financial year. There are special rules if your accounting period is longer than twelve months (for example, if your new company makes up its accounts to a date more than a year away).</p>
		<p>For more information, see our guide: <a href="/tax-payroll-and-company-information/corporation-tax/introduction-to-corporation-tax.php">introduction to Corporation Tax</a>.</p>
		
