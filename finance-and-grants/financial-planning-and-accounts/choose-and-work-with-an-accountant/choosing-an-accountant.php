		<h1>Choose and work with an accountant</h1>
		<h2>Choosing an accountant</h2>
		<p>Before choosing an accountant, you should consider the size and complexity of your business - your business may be small, but if the systems and organisation are complex then you will need an adviser who is able to assist with more than basic bookkeeping. Or you might need someone who understands a particular type of business. You should also consider what other services your business may need from an accountant.</p>
		<p>It's best to start looking for an accountant well before you need to use one, to give yourself enough time to make an informed decision.</p>
		<p>It is recommended that you look for someone who has professional accounting qualifications. Anyone can call themselves an accountant or bookkeeper even if they don't have any professional qualifications. But if any laws are broken or if bad decisions are made, then you, not your adviser, will be liable.</p>
		<p>It is also advisable to ensure that potential advisers have professional indemnity insurance. If there are any problems with the work the accountant has done, you could be able to claim for any financial loss suffered as a result of the accountant's advice.</p>
		<p>Accountancy is a complex area and there are many accountancy associations to choose from. Some offer specialist services such as bookkeeping or preparing draft accounts, while others can offer guidance on specific business issues. You may wish to seek advice or recommendations from business associates, your bank or lawyer, or from professional or trade associations - particularly if you're looking for an accountant that specialises in your industry.</p>
		<p>Once you have identified some potential candidates, you should draw up a shortlist of about six accountants you'd like to contact.</p>
		
		<h3>Choosing the right accountant for your business</h3>
		<p>You should speak directly to each accountant on your shortlist. Ideally, the relationship you have with your accountant will be long term, so it is important to consider their:</p>
		<ul>
			<li><strong>Experience</strong> - eg do they have experience in your sector and of businesses of a similar size? Can they deal with your business' specific needs?</li>
			<li><strong>Costs and charges</strong>.</li>
			<li><strong>Size and personnel</strong> - eg how many partners do they have and who would look after your business? Sometimes smaller accountancy practices suit smaller businesses.</li>
			<li><strong>Efficiency</strong> - what response times do they work to?</li>
			<li><strong>Additional services</strong> - can they offer specialist business advice or help beyond basic accountancy services?</li>
		</ul>

		<p>You may decide that some of the accountants on your shortlist are not right for your business. However, if their experience and services match what you are looking for, the next step should be to arrange a visit with them to discuss your business needs in more detail. You should check whether you will be charged for this meeting.</p>
		<p>For more information, see the page in this guide: <a href="/finance-and-grants/financial-planning-and-accounts/choose-and-work-with-an-accountant/checklist-ten-things-to-ask-your-prospective-accountant.php">checklist: ten things to ask your prospective accountant</a>.</p>
		<p>Once you have chosen an accountant, let them know that you have selected them and they will issue a letter of engagement. This letter will act as the contract between you and your accountant and should detail:</p>
		<ul>
			<li>your responsibilities</li>
			<li>the accountant's responsibilities</li>
			<li>their fees and how they will be charged</li>
		</ul>

		<h3>Professional associations</h3>
		<p>Qualified accountants usually have the word '<strong>chartered</strong>' attached to their title, indicating that their body was incorporated by Royal Charter.</p>
		<p>There are also business-facing chartered institutes and professional accountancy bodies and organisations that offer specific accounting and taxation services for businesses and individuals. For more information, see our page on <a href="http://webarchive.nationalarchives.gov.uk/20120406044320/http://online.businesslink.gov.uk/bdotg/action/openpopup?itemId=1084732070&r.i=1073791746&r.l1=1073858790&r.l2=1073858944&r.l3=1073963622&r.t=RESOURCES&type=ONEOFFPAGE" target="_blank" rel="noopener noreferrer">professional associations, institutes and organisations for the accountancy sector</a>.</p>
		
