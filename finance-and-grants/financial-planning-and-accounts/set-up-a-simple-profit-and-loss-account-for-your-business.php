		<h1>Set up a simple profit and loss account for your business</h1>
		<h2>Introduction</h2>
		<img src="../../_img/TR1010.gif" width="94" height="62" alt="" />
		<p>A profit and loss account is a summary of business transactions for a given period - normally 12 months. By deducting total expenditure from total income, it shows on the '<strong>bottom line</strong>' whether your business made a profit or loss at the end of that period.</p>
		<p>A profit and loss account is produced primarily for business purposes - to show owners, shareholders or potential investors how the business is performing. But most of the information is also used by HM Revenue &amp; Customs to check your tax calculations.</p>
		<p>This guide tells you about the basic financial records you need to keep to enable you to report your profit or loss each year. The information should help you decide whether you need the services of an accountant or bookkeeper, or whether you can do it yourself.</p>
		
