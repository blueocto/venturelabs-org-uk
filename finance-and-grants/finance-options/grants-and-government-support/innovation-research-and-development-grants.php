		<p>The UK government encourages businesses in technological innovation and research and development (R&amp;D) projects.</p>
		<p>If you plan to research and develop new products or services, you can get free information and advice and may also be eligible to apply for financial support - an R&amp;D grant - or for R&amp;D tax credits. There are also several other government initiatives to support you. The European Union also provides grants for some types of research.</p>
		<p>Applying for an R&amp;D grant usually involves completing application forms and demonstrating how you satisfy the funding conditions and requirements. An effective application is essential if you are to maximise your chances of obtaining a grant. Any guidelines given with the application should be closely followed.</p>
		<p>This guide highlights some of the grants and other support available to businesses to help them undertake R&amp;D and innovation.</p>
		<p>Please note that grant applications can be complex and time consuming and there is a lot of competitions for schemes. Please bear in mind that, while you may fulfil the eligibility criteria, your application may not be successful.</p>
		
