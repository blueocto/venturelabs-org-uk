		<h1>Complain against an insolvency practitioner or The Insolvency Service</h1>
		<h2>Introduction</h2>
		<p>Insolvency issues, such as disqualifying company directors of insolvent companies and paying redundancy to employees of insolvent companies, are the responsibility of The Insolvency Service. It runs the official receiver network throughout England and Wales and administers and investigates the affairs of partnerships and companies wound up by the courts, as well as those of bankrupt individuals.</p>
		<p>If you are not happy with the way The Insolvency Service handled your case or are dissatisfied with the services you received, you should submit a formal complaint to the appropriate office within The Insolvency Service. If they fail to resolve your complaint sufficiently, you may be able to make a formal complaint to the relevant complaints commissioner.</p>
		<p>You can also make a formal complaint if you are not happy about the conduct of an insolvency practitioner (IP) - either directly to the IP concerned, or to the appropriate authorising body.</p>
		<p>This guide explains the complaints process for both The Insolvency Service and an IP.</p>
		
