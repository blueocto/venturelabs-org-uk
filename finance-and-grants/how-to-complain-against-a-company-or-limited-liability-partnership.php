		<h1>How to complain against a company or limited liability partnership</h1>
		<h2>Introduction</h2>
		<p>The Insolvency Service, through its Investigation and Enforcement Services (IES), deals with misconduct or unscrupulous practices by companies and limited liability partnerships. The Corporate Complaints team assesses complaints to decide which to pursue. The Company Investigations team conducts the investigation to discover if further action is required. Company Investigations has the power to ask the court to close down companies or disqualify a person from being a director if an investigation shows it is in the public interest to do so.</p>
		<p>This guide explains the role of both the Corporate Complaints and Company Investigations teams and includes details of how to make a complaint, a breakdown of the investigation process and the possible outcomes of an investigation.</p>
		<p>Please note that, to avoid repetition, only the term 'company' is used in this guide, however the legislation can also apply to limited liability partnerships.</p>
