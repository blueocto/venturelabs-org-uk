		<h2>Second phase of recommendations published</h2>
		<p>The second phase of an independent review of the criminal records regime has now been completed and submitted to Ministers. The review was carried out in two phases, and the second phase of the review has made further recommendations about how information is stored and shared.</p>
		<p>Phase two of the review contains a number of recommendations for the administration of criminal records, including that more should be done to improve the sharing of criminal records information with other countries.</p>
