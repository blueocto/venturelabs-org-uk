		<h2>Launched by the Bank of England and HM Treasury </h2>
		<p>The Bank of England and HM Treasury launched the Funding for Lending Scheme (FLS) on 13 July. It is designed to boost lending to households and businesses. It works by allowing banks and building societies to borrow from the Bank of England for up to 4 years. As security against that lending, banks will provide assets, such as business or mortgage loans, to the Bank of England. Banks will be able to borrow during the 18 months from 1 August 2012 until 31 January 2014.</p>
		<p>Banks will have strong incentives to boost lending, by lowering interest rates and increasing the availability of business loans and mortgages. The more that they lend, the more they can borrow from the Bank of England. Banks that are increasing their lending will pay the lowest fee on their borrowing while those that reduce their lending they will pay a higher fee. The Bank of England will publish data on the usage of the FLS along with lending data from the participants on a quarterly basis.</p>
