		<h1>Ensuring your workers are eligible to work in the UK</h1>
		<h2>Introduction</h2>
		<img src="../../_img/EP0100sat1.gif" width="78" height="118" alt="" />
		<p>Some people are automatically entitled to work in the UK. Others may have restrictions on how long they can stay, whether they can work or the type of work they can do.</p>
		<p>All employers in the UK have a responsibility to prevent illegal migrant working. You must therefore check the entitlement of everyone you plan to employ to work in the UK.</p>
		<p>If you fail to do this, you may be liable to pay a fine (known as a 'civil penalty'). If you knowingly employ an illegal migrant worker, you may face criminal prosecution, which could result in an unlimited fine and/or a maximum two-year prison sentence.</p>
		<p>This guide will tell you how to comply with the laws on checking the entitlement to work in the UK.</p>
		
