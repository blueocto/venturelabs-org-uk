		<h1>Ensuring your workers are eligible to work in the UK</h1>
		<h2>Copying and retaining workers' records</h2>
		<p>When a prospective worker gives you their relevant documents to prove that they have the right to work in the UK, you must keep a record of these documents. You must do this by either photocopying them or scanning and saving them electronically on a non-rewritable disk - eg CD-R or DVD-R.</p>
		<p>With passports and travel documents, you must copy:</p>
		<ul>
			<li>the front cover</li>
			<li>the date of expiry</li>
			<li>any photographs and signature</li>
			<li>all of the pages giving the prospective worker's personal details, including nationality</li>
			<li>pages containing a UK government stamp or endorsement which allows the prospective worker to do the work you are offering</li>
		</ul>
		
		<p>You should copy other documents in their entirety.</p>
		<p>Keep copies of the documents until two years after the worker's employment ends. Having a record of every copied document will help you establish a statutory excuse if the UK Border Agency (UKBA) detects anyone working illegally for you.</p>
		<p>If you lose any records, the UKBA may look at your normal recruitment procedures when considering if you have established a statutory excuse, eg if you have a consistent practice of copying documents for each worker.</p>
		<p>You should not retain a person's original documents, except for the purpose of copying them. The only exceptions are:</p>
		<ul>
			<li>when an individual gives you a P45 as part of a combination of documents, you should retain part 2 of the document for three years to comply with HM Revenue &amp; Customs rules</li>
			<li>when you employ someone for 24 hours or less and it is not practicable to obtain a copy of the documents, you can retain the document(s) while they work for you but you must have facilities for keeping them safe</li>
		</ul>
		
		<p>You must not keep a potential worker's original documents for more than a day. If you deliberately keep, or retain without consent, a person's passport or other original documents belonging to them, you may be guilty of an offence.</p>
