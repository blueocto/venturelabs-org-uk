		<h1>Implement staff incentive schemes</h1>
		<h2>Ensuring the incentive scheme is successful</h2>
		<p>You can take measures when setting up an incentive scheme to help ensure its success. These include:</p>
		<ul>
			<li>briefing the workforce</li>
			<li>training supervisors to answer employee or trade union queries</li>
			<li>documenting how the scheme will work to avoid misunderstandings</li>
			<li>running a pilot scheme to see if any adjustments should be made</li>
		</ul>

		<p>Monitoring the system is an ongoing process - problem areas should be highlighted and promptly addressed.</p>
		
		<h3>Is the scheme working?</h3>
		<p>Establish a system that keeps a record of objectives and goals. Remember that some incentives/perks can indirectly benefit the business.</p>
		<p>Review the incentives and find out through feedback whether staff value them.</p>
		<p>Incentives should not discriminate against certain staff, eg membership of a men-only golf club.</p>
		
		<h3>Evaluating the scheme</h3>
		<p>Set a time for evaluating the scheme and involve staff or union representatives.</p>
		<p>You should aim to find out:</p>
		<ul>
			<li>whether the scheme achieved its objectives</li>
			<li>whether the scheme is fair in terms of who benefits - <a href="https://www.equalityhumanrights.com/en/search?text=gender+pay+reporting" target="_blank" rel="noopener noreferrer">find information on equal pay principles on the Equality and Human Rights Commission (EHRC) website</a></li>
			<li>whether incentives linked to performance really do motivate staff</li>
			<li>how the scheme is promoted and how well staff understand and buy into it</li>
			<li>whether staff require further information or training to help them achieve their targets</li>
			<li>how the scheme compares with those of competitors or industry norms</li>
		</ul>

