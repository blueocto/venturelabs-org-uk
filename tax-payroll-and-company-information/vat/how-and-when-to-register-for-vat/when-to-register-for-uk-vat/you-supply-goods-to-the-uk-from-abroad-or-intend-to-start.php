		<h1>When to register for UK VAT </h1>
		<h2>You supply goods to the UK from abroad, or intend to start</h2>
		<p>You may need to register for VAT, or you may be able to choose to register voluntarily, if you are doing any of the following kinds of business in the UK:</p>
		<ul>
			<li><strong>Supplying and delivering goods from another European Union (EU) country to non VAT-registered customers in the UK</strong>. If you sell and deliver goods (or arrange for their delivery) from another EU country to customers in the UK who aren't registered for VAT and don't have to be registered, this is known as distance selling. Common examples are mail order and internet sales. If the value of your distance sales in the calendar year from 1 January goes over the distance selling threshold (currently &pound;70,000) you must register for UK VAT. You can also register voluntarily, if you are trading below the threshold or you intend to start trading.</li>
			<li><strong>Supplying goods on which an 8th or 13th Directive claim has been made or is going to be made</strong>. In certain circumstances you have to register if you sell, supply or otherwise dispose of an asset on which someone has claimed - or intends to claim - an 8th or 13th Directive refund.</li>
			<li><strong>Supplying and delivering excise goods from another country</strong>. If you sell and deliver goods from another country that are liable to UK Excise Duty (such as tobacco or alcohol) to UK customers, then you must register for VAT in the UK. There's no threshold - everyone must register.</li>
		</ul>

		<p><a href="/tax-payroll-and-company-information/vat/international-trade-international-visits-and-vat/distance-selling-into-the-uk-from-other-eu-countries-and-vat.php">Distance selling into the UK from other EU countries and VAT</a></p>
