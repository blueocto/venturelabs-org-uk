		<h3>If you don't have a place of business in the UK or live here</h3>
		<p>If you have to register for one of the reasons described above, but you don't live in the UK or have a place of business here, then you must register as a Non-Established Taxable Person (NETP). You may also want to appoint a tax agent or representative to keep VAT records and accounts on your behalf - that's up to you.</p>
		<p>See the page on <a href="/tax-payroll-and-company-information/vat/how-and-when-to-register-for-vat/how-to-register-for-vat/how-to-register-for-vat-if-you-do-business-internationally.php">how to register for VAT if you do business internationally</a> in our guide on <a href="/tax-payroll-and-company-information/vat/how-and-when-to-register-for-vat/how-to-register-for-vat.php">how to register for VAT</a>.</p>
		
		<h3>Disposing of assets on which 8th or 13th Directive refunds have been claimed</h3>
		<p>The 8th Directive allows someone registered for VAT in one EU country to reclaim VAT they have paid in another EU country. The 13th Directive allows businesses established outside the EU to reclaim VAT paid within the EU.</p>
		<p>You will have to register for VAT if you claimed, or intend to claim, a repayment under either of these Directives on goods, and both the following apply:</p>
		<ul>
			<li>the goods are business assets - or are incorporated into business assets</li>
			<li>you make a VAT taxable supply of those goods, or intend to make one in the next 30 days</li>
		</ul>

		<p>You'll also have to register if the repayment was claimed by someone who then transferred the assets to you under the Transfer of a Going Concern provisions. This applies even if assets were transferred more than once - you may have to check back several transfers.</p>
		<p>There's no registration threshold. For any disposal of these assets in the circumstances outlined above, you must register.</p>
		
