			<p><a href="https://www.gov.uk/government/publications/vat-notice-742a-opting-to-tax-land-and-buildings" target="_blank" rel="noopener noreferrer">Read about opting to tax land and buildings in VAT Notice 742A on the HMRC website</a></p>

			<h3>Keeping records</h3>
			<p>After you sell your business, you must retain your VAT records.</p>
			<p>If the buyer keeps your VAT number, you must pass your records to them. Permission can be sought from HMRC for you to keep your records, but you will still have to make the information available to the buyer so that they can comply with their own VAT responsibilities.</p>
			
