			<p><a href="https://www.gov.uk/government/publications/vat-notice-7009-transfer-of-business-as-a-going-concern" target="_blank" rel="noopener noreferrer">Read about the special rules when selling a business in VAT Notice 700/9 on the HMRC website</a></p>
