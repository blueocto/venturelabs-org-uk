			<h1>Closing or selling your business and VAT registration</h1>
			<h2>Selling your business</h2>
			<p>For VAT purposes, HM Revenue &amp; Customs (HMRC) considers that you have transferred your business as a going concern if:</p>
			<ul>
				<li>you retire and another person takes over the assets of your business</li>
				<li>you sell your business and its assets to someone else</li>
				<li>you sell part of your business that is capable of being operated separately to somebody else</li>
			</ul>

			<p>When you transfer your business as a going concern, you normally have to cancel your VAT registration.</p>
			<p>However in some situations, the new owner of the business can apply to keep your VAT registration number. If you agree and allow the new owner to keep the VAT registration number, then you don't need to cancel your registration. Both you and the new owner of the business should complete form VAT 68. You need to do this within 30 calendar days of selling your business to avoid becoming liable for a financial penalty.</p>
			<p>If you decide to allow the transfer of your VAT registration number to the new owner of the business you should cancel any Direct Debit that you have set up to pay your online VAT Returns. The new owner of the business can set up a new Direct Debit through the VAT online service.</p>
			<p>Send your completed VAT 68 to:</p>
			<p>
				HM Revenue &amp; Customs<br />
				VAT Registration Service<br />
				Imperial House<br />
				77 Victoria Street<br />
				Grimsby<br />
				DN31 1DB
			</p>
			<p><a href="https://www.gov.uk/government/publications/vat-request-for-transfer-of-a-registration-number-vat68" target="_blank" rel="noopener noreferrer">Find form VAT 68 to request the transfer of a registration number on the HMRC website</a></p>
			
			<h3>Treatment of the sale for VAT purposes</h3>
			<p>Normally when you sell something, the sale is treated as a supply for VAT purposes. However when you sell your business, in certain circumstances special rules apply and the sale will not be treated as a supply, for VAT purposes, so no VAT should be charged. In other circumstances, you'll have to charge VAT in the normal way.</p>
			<p>These special rules help businesses by improving their cashflow. They also avoid the need to separately value assets that may be liable at different rates or are exempt but have been sold as a whole package.</p>
