		<h1>Introduction to VAT</h1>
		<h2>Introduction</h2>
		<img src="../../_img/VAT0001.gif" width="94" height="62" alt="" />
		<p>VAT is a tax that's charged on most goods and services that VAT-registered businesses provide in the UK. It's also charged on goods and some services that are imported from countries outside the European Union (EU), and brought into the UK from other EU countries.</p>
		<p>VAT is charged when a VAT-registered business sells to either another business or to a non-business customer.</p>
		<p>When a VAT-registered business buys goods or services they can generally reclaim the VAT they have paid.</p>
		<p>There are three rates of VAT, depending on the goods or services the business provides. The rates are:</p>
		<ul>
			<li>standard - 20 per cent</li>
			<li>reduced - 5 per cent</li>
			<li>zero - 0 per cent</li>
		</ul>
		<p>There are also some goods and services that are:</p>
		<ul>
			<li>exempt from VAT</li>
			<li>outside the UK VAT system altogether</li>
		</ul>
		<p>This guide explains the basics of how VAT works. It tells you where you can find more information and advice.</p>
		
