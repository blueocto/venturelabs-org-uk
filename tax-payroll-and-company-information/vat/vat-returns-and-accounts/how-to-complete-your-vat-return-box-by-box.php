		<h2>Introduction</h2>
		<img src="../../../_img/TR0360.gif" width="94" height="62" alt="" />
		<p>At the end of each VAT period - usually every three months - you need to complete a VAT Return. A VAT Return tells HM Revenue &amp; Customs (HMRC):</p>
		<ul>
			<li>how much VAT you charged to your customers, and how much you owe to HMRC on other supplies you have made</li>
			<li>how much VAT you are entitled to claim back</li>
			<li>the amount of VAT you must pay to HMRC or the amount of VAT you can get repaid by HMRC</li>
			<li>the total amount of sales and purchases you have made</li>
			<li>the total amount of goods sold to customers or purchased from suppliers in other European Union countries</li>
		</ul>
		<p>This guide explains in detail how to complete your return if you are not on the Flat Rate Scheme.</p>
		
