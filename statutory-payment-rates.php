		<p>For employees earning &pound;102 or more per week in 2011-12, the above rate of SSP applies from 6 April 2011, and for SMP, OSPP, ASPP and SAP the above rates apply for complete pay weeks commencing on or after 3 April 2011 (the first Sunday in April).</p>
		<p>The threshold for recovery of SSP under the percentage threshold scheme remains unchanged from last year at 13 per cent.</p>
		
		<h3>Change in the additional payment made to small employers</h3>
		<p>From 6 April 2011, employers who <strong>qualify</strong> for the small employers rate of reimbursement, can recover 100 per cent of the SMP, OSPP, ASPP and SAP paid to their employees, plus an additional amount of 3 per cent (previously 4.5 per cent) in compensation for the employers share of National Insurance Contributions (NICs) due on these payments. This means that small employers can claim back a total of 103 per cent of the SMP, OSPP, ASPP and SAP they pay out.</p>
		<p>Employers who <strong>do not qualify</strong> for the small employers' rate can recover 92 per cent of the SMP, OSPP, ASPP and SAP paid to their employees.</p>
		
		<h3>What is the additional amount paid to small employers?</h3>
		<p>Small employers can get back all of the SMP, OSPP, ASPP and SAP they pay out plus an additional amount in compensation for the employer's NICs due to be paid on those payments. This is often known as the small employers' compensation rate.</p>
		<p>A small employer is an employer who has paid total gross Class 1 NICs below an annual threshold in a qualifying tax year. For the 2011-12 tax year, the annual threshold is &pound;45,000 or less - this has not changed from the 2010-11 tax year.</p>
		
		<h3>Why has the rate of the additional amount changed?</h3>
		<p>The additional amount is calculated using a formula laid down in legislation based on the employers' share of NICs paid and is reviewed each year to see if it needs to be adjusted to reflect the shift in the ratio of SMP, OSPP, ASPP and SAP paid out and the employers' NICs paid on these payments.</p>
		<p>The employer's NICs threshold for the 2011-12 tax year rose from &pound;110 to &pound;136 per week. This means that from 6 April 2011, NICs are not payable on the &pound;128.73 weekly standard rate of any relevant statutory payment, so employers will be paying less NICs on statutory payments.</p>
		<p>The change in the additional amount - from 4.5 per cent to 3 per cent - reflects this reduction on employers NICs payments.</p>
