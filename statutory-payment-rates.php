		<h1>Statutory payment rates</h1>
		<h2>Changes for 2011-12</h2>
		<p>Employers may have to pay employees Statutory Sick Pay (SSP), Statutory Maternity Pay (SMP), Ordinary Statutory Paternity Pay (OSPP), Additional Statutory Paternity Pay (ASPP) or Statutory Adoption Pay (SAP).</p>
		<p>The following statutory payment rates have been announced for 2011-12. There is also a change to the additional amount small employers get back.</p>
		
